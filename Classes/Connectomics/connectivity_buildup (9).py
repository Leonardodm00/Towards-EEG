def plot_synapse_layer_distribution_log(mtypes, synapse_dict, filename='synapse_layer_distribution_log.png'):
    """
    Plots the distribution of incoming synapses for each post-synaptic layer,
    categorized by their pre-synaptic layer of origin.
    Uses a grouped bar chart with a logarithmic Y-axis.
    UPDATED: Now includes both Thalamic ('th') and Background ('bg') sources.
    """

    ordered_post = ['L1', 'L23', 'L4', 'L5', 'L6']
    ordered_pre = ['L1', 'L23', 'L4', 'L5', 'L6', 'th', 'bg']

    # Initialize a nested dictionary to store synapse counts
    counts = {post: {pre: 0 for pre in ordered_pre} for post in ordered_post}

    # 1. Aggregate the Synapse Data
    for post_id, matrix in synapse_dict.items():
        if post_id >= len(mtypes): continue

        post_mtype = str(mtypes[post_id])

        # Skip Thalamic and Background cells (they don't receive inputs in this plot)
