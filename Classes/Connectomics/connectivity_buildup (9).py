    # Retrieve the fit type
    fit_type = conn_data.get('best_fit', {}).get(pre_mtype, {}).get(post_mtype, None)

    # Calculate probabilities (Y-axis) based on the fit type
    if fit_type == 'exp':
        a0 = float(conn_data['a0mat_exp'][pre_mtype][post_mtype])
        l  = float(conn_data['lmat_exp'][pre_mtype][post_mtype])
        d0 = float(conn_data['d0_exp'][pre_mtype][post_mtype])

        # P(d) = A0 * exp(-(d - d0)/lambda)
        probabilities = a0 * np.exp(-np.maximum(distances - d0, 0) / l)
        title_fit = "Exponential Decay"
        info_text = f"$A_0$ = {a0:.3f}\n$\lambda$ = {l:.1f} $\mu m$\n$d_0$ = {d0:.1f} $\mu m$"

    elif fit_type == 'gauss':
        a0 = float(conn_data['a0mat_gauss'][pre_mtype][post_mtype])
        l  = float(conn_data['lmat_gauss'][pre_mtype][post_mtype])
        x0 = float(conn_data['x0_gauss'][pre_mtype][post_mtype])

        # P(d) = A0 * exp(-((d - x0)^2) / lambda^2)
        probabilities = a0 * np.exp(-((distances - x0)**2) / (l**2))
        title_fit = "Gaussian Decay"
        info_text = f"$A_0$ = {a0:.3f}\n$\lambda$ = {l:.1f} $\mu m$\n$x_0$ = {x0:.1f} $\mu m$"

    else:
        # --- Fallback: Step-function empirical probability bins ---
        probabilities = np.zeros_like(distances)

        dist_bins = [
            (12.5, 'pmat12um'),   (25.0, 'pmat25um'),   (50.0, 'pmat50um'),
            (75.0, 'pmat75um'),   (100.0, 'pmat100um'), (125.0, 'pmat125um'),
            (150.0, 'pmat150um'), (175.0, 'pmat175um'), (200.0, 'pmat200um'),
            (225.0, 'pmat225um'), (250.0, 'pmat250um'), (275.0, 'pmat275um'),
            (300.0, 'pmat300um'), (325.0, 'pmat325um'), (350.0, 'pmat350um'),
            (375.0, 'pmat375um')
        ]

        prev_d = -1.0

        for max_d, bin_key in dist_bins:
            try:
                bin_prob = float(conn_data[bin_key][pre_mtype][post_mtype])
            except (KeyError, ValueError, TypeError):
                bin_prob = 0.0

            mask = (distances > prev_d) & (distances <= max_d)
            probabilities[mask] = bin_prob
            prev_d = max_d

        title_fit = "Empirical Step Function (No Spatial Fit)"

        try:
            mean_p = float(conn_data['pmat'][pre_mtype][post_mtype])
        except:
            mean_p = 0.0
        info_text = f"Binned Empirical Data\nGlobal Mean $P$ = {mean_p:.4f}"

    # --- Plotting ---
    plt.figure(figsize=(8, 5), dpi=100)

    # Plot the curve/steps
    plt.plot(distances, probabilities, color='crimson', linewidth=2.5, label=f'{pre_mtype} $\\rightarrow$ {post_mtype}')

    # Add a text box with the extracted mathematical parameters
    props = dict(boxstyle='round', facecolor='white', alpha=0.8, edgecolor='gray')
    plt.gca().text(0.95, 0.95, info_text, transform=plt.gca().transAxes, fontsize=11,
                   verticalalignment='top', horizontalalignment='right', bbox=props)

    plt.title(f"Connection Probability: {pre_mtype} $\\rightarrow$ {post_mtype}\n({title_fit})", fontsize=14, pad=15)
    plt.xlabel(r"3D Euclidean Distance ($\mu m$)", fontsize=12)
    plt.ylabel("Probability of Connection $P(d)$", fontsize=12)

    # Scale Y axis gracefully
    y_max = max(probabilities) if max(probabilities) > 0 else 0.1
    plt.ylim(0, y_max * 1.2)
    plt.xlim(0, max_dist)

    plt.grid(True, linestyle='--', alpha=0.6)
    plt.legend(loc='lower left')
    plt.tight_layout()
    plt.show()


