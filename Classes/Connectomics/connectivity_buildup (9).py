    if cell_mtypes.size == 0:
        print("Error: The cell_mtypes array is empty.")
        return

    # 1. Extract layers and genetic markers
    # The layer is the prefix before the first underscore in the m-type (column 0)
    layers = np.array([m.split('_')[0] for m in cell_mtypes])
    genetic_types = cells_gtypes

    # Define the ordered categories we want to plot
    unique_layers = ['L1', 'L23', 'L4', 'L5', 'L6']
    target_genetics = ['PY', 'SST', 'PV', 'VIP']

    # 2. Count occurrences for each genetic type per layer
    counts = {g: [] for g in target_genetics}

    for layer in unique_layers:
        layer_mask = (layers == layer)
        layer_genetics = genetic_types[layer_mask]

        for g in target_genetics:
            counts[g].append(np.sum(layer_genetics == g))

    # 3. Build the grouped bar chart
    x = np.arange(len(unique_layers))  # the label locations
    width = 0.2  # the width of the bars

    fig, ax = plt.subplots(figsize=(10, 6), dpi=100)

    # Distinct colors for Excitatory (Blue) and Inhibitory (Warm colors)
    colors = {'PY': '#1f77b4', 'SST': '#ff7f0e', 'PV': '#2ca02c', 'VIP': '#d62728'}

    # Plot bars iteratively
    for i, g in enumerate(target_genetics):
        # Calculate the offset to group bars side-by-side
        offset = (i - 1.5) * width

        # Replace 0s with a tiny number just for the log scale to render properly
        safe_counts = [c if c > 0 else 0.1 for c in counts[g]]

        bars = ax.bar(x + offset, safe_counts, width, label=g, color=colors[g], zorder=3)

        # Optional: Add text labels on top of the bars for exact numbers
        for bar, raw_count in zip(bars, counts[g]):
            if raw_count > 0:
                yval = bar.get_height()
                ax.text(bar.get_x() + bar.get_width()/2, yval * 1.1, int(raw_count),
                        ha='center', va='bottom', fontsize=8, rotation=90)

    # 4. Formatting
    ax.set_ylabel('Number of Cells (Log Scale)', fontsize=12)
    ax.set_xlabel('Cortical Layer', fontsize=12)
    ax.set_title('Distribution of Genetic Subpopulations across Layers', fontsize=14)
    ax.set_xticks(x)
    ax.set_xticklabels(unique_layers)

    # Using a log scale because PY cells vastly outnumber PV/SST/VIP
    ax.set_yscale('log')
    ax.set_ylim(bottom=1) # Set bottom to 1 to avoid log(0) distortion

    ax.legend(title='Genetic Type')
    ax.grid(axis='y', linestyle='--', alpha=0.7, zorder=0)

    plt.tight_layout()
    plt.show()


