def plot_interactive_adjacency_matrix(adj_matrix, cell_mtypes=None, max_cells=3000):
    """
    Plots a highly interactive, WebGL-accelerated microcircuit adjacency matrix using Plotly.
    Supports zooming, panning, and hover-tooltips to identify specific synaptic pathways.
    """

    N = adj_matrix.shape[0]

    # 1. Subsample if necessary
    if N > max_cells:
        print(f"Notice: Matrix too large ({N}x{N}). Subsampling to a {max_cells}x{max_cells} block.")
        plot_matrix = adj_matrix[:max_cells, :max_cells]
        plot_mtypes = cell_mtypes[:max_cells] if cell_mtypes is not None else None
    else:
        plot_matrix = adj_matrix
        plot_mtypes = cell_mtypes

    # 2. Sort by m-type to reveal pathway structures
    if plot_mtypes is not None:
        sort_idx = np.argsort(plot_mtypes)
        plot_matrix = plot_matrix[sort_idx, :][:, sort_idx]
        plot_mtypes = plot_mtypes[sort_idx]

    # 3. Extract Coordinates for the Scatter Plot
    # We only plot the '1's (connections) to save massive amounts of memory
    if sparse.issparse(plot_matrix):
        rows, cols = plot_matrix.nonzero()
    else:
        rows, cols = np.where(plot_matrix > 0)

    # 4. Generate Hover Text
    if plot_mtypes is not None:
        # Vectorized string concatenation is much faster than a loop
        hover_text = "Pre: " + plot_mtypes[rows].astype(str) + "<br>Post: " + plot_mtypes[cols].astype(str)
    else:
        hover_text = [f"Pre: {r}<br>Post: {c}" for r, c in zip(rows, cols)]

    # 5. Build the WebGL Scatter Plot
    # Scattergl uses the GPU, which is mandatory for >10,000 points
    fig = go.Figure(data=go.Scattergl(
        x=cols,
        y=rows,
        mode='markers',
        marker=dict(size=2, color='#001933', opacity=0.7),
        text=hover_text,
        hoverinfo='text'
    ))

    # 6. Advanced Labeling and Layer Highlighting
    if plot_mtypes is not None:
        unique_mtypes, indices = np.unique(plot_mtypes, return_index=True)
        sort_order = np.argsort(indices)
        boundaries = indices[sort_order]
        ordered_mtypes = unique_mtypes[sort_order]

        layer_bounds = []
        current_layer = None

        # --- NEW: Filtered lists for labels ---
        filtered_centers = []
        filtered_ticktext = []
        N_plot = plot_matrix.shape[0]

        # Threshold: Population must be at least 1.5% of the displayed matrix to get a text label
        min_width_threshold = N_plot * 0.015

        for i in range(len(boundaries)):
            start = boundaries[i]
            end = boundaries[i+1] if i+1 < len(boundaries) else N_plot
            width = end - start
            center = (start + end) / 2.0

            # Track layer boundaries for shading
            layer_prefix = ordered_mtypes[i].split('_')[0]
            if layer_prefix != current_layer:
                layer_bounds.append((start, layer_prefix))
                current_layer = layer_prefix

            # Draw subtle dashed dividers for EVERY population (even the tiny ones)
            if start > 0:
                fig.add_hline(y=start, line_dash="dash", line_color="#2b2b2b", opacity=0.3, line_width=1)
                fig.add_vline(x=start, line_dash="dash", line_color="#2b2b2b", opacity=0.3, line_width=1)

            # ONLY add the text label if the population block is wide enough
            if width >= min_width_threshold:
                filtered_centers.append(center)
                filtered_ticktext.append(ordered_mtypes[i])

        # Pastel Layer Colors
        layer_colors = {
            'L1':  '#FF9999', 'L23': '#FFCC99',
            'L4':  '#99FF99', 'L5':  '#99CCFF', 'L6':  '#CC99FF'
        }

        # Apply the colored shading as background rectangles
        for i in range(len(layer_bounds)):
            start_l = layer_bounds[i][0]
            layer_name = layer_bounds[i][1]
            end_l = layer_bounds[i+1][0] if i+1 < len(layer_bounds) else N_plot

            rect_color = layer_colors.get(layer_name, '#E0E0E0')

            fig.add_vrect(x0=start_l, x1=end_l, fillcolor=rect_color, opacity=0.2, layer="below", line_width=0)
            fig.add_hrect(y0=start_l, y1=end_l, fillcolor=rect_color, opacity=0.2, layer="below", line_width=0)

        # Apply the dynamically filtered Subpopulation Labels to Axes
        fig.update_xaxes(tickvals=filtered_centers, ticktext=filtered_ticktext, tickangle=90, side='bottom', tickfont=dict(size=10))
        fig.update_yaxes(tickvals=filtered_centers, ticktext=filtered_ticktext, tickfont=dict(size=10))

    # 7. Final Layout Adjustments
    fig.update_layout(
        title=dict(text="Interactive Microcircuit Adjacency Matrix", font=dict(size=24), x=0.5),
        xaxis_title="Post-Synaptic Population",
        yaxis_title="Pre-Synaptic Population",
        yaxis=dict(autorange="reversed"), # Reverses Y axis so (0,0) is top-left like Matplotlib spy
        width=1000,
        height=1000,
        plot_bgcolor='white',
        margin=dict(l=150, b=150, t=80, r=40) # Add margin room for the rotated text labels
    )

    fig.show()

