def rebuild_and_verify_matrix(original_matrix, pre_to_post):
    """
    Rebuilds the adjacency matrix from a dictionary and verifies exact equality.

    Parameters:
    - original_matrix: scipy.sparse.csr_matrix (The ground truth matrix)
    - pre_to_post: Dict (Efferent connections generated previously)

    Returns:
    - rebuilt_matrix: scipy.sparse.csr_matrix (The reconstructed matrix)
    - is_identical: Boolean (True if perfectly matched, False otherwise)
    """
    # 1. Get the original dimensions to ensure the rebuilt matrix is exactly the same size.
    # (This prevents dimension mismatches if the very last neurons had 0 connections).
    N = original_matrix.shape[0]

    rows = []
    cols = []

    # 2. Extract the coordinates from the dictionary
    for pre_id, post_ids in pre_to_post.items():
        rows.extend([pre_id] * len(post_ids))
        cols.extend(post_ids)

    # 3. Create the data array (all 1s for our unweighted connections)
    data = np.ones(len(rows), dtype=np.uint8)

    # 4. Reconstruct the sparse CSR matrix
    rebuilt_matrix = sparse.csr_matrix((data, (rows, cols)), shape=(N, N))

    # 5. The Verification Step
    # Subtracting/comparing sparse matrices natively returns another sparse matrix.
    # If they are perfectly identical, the resulting boolean matrix will have zero 'True' values.
    difference = (original_matrix != rebuilt_matrix)
    is_identical = (difference.nnz == 0)

    return rebuilt_matrix, is_identical



