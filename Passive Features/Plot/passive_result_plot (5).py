def plot_bootstrap_trace_diagnostic(
    *,
    fit_result,
    cell_data=None,
    pulse_pool: Optional[List[Dict[str, Any]]] = None,
    n_examples: int = 3,
    n_pulses_per_replicate: int = 9,
    n_avg_groups_bootstrap: int = 3,
    noise_mode: str = "iid",
    out_dir: Optional[str] = None,
    seed: int = 42,
) -> Path:
    """Diagnostic plot: example training traces for both bootstrap modes.

    Produces a figure with two panels (side by side):
      Left:  PARAMETRIC — real trace, MLE trace, n_examples synthetic traces.
      Right: NONPARAMETRIC — original averaged trace, n_examples
             resampled-and-averaged traces.

    Parameters
    ----------
    fit_result
        PassiveFitResult from Phase 2 (must have neuron_cell, opt_inputs).
    cell_data
        CellData with ss_individual_pulses (needed for nonparametric panel).
    pulse_pool
        Direct pulse pool override.  If None, uses cell_data.ss_individual_pulses.
    """
    rng = np.random.default_rng(seed)

    opt_inputs = fit_result.opt_inputs
    mle_phys = (fit_result.cm_uF_per_cm2,
                fit_result.rm_Ohm_cm2,
                fit_result.ra_Ohm_cm)
    v_rest = opt_inputs.v_rest_mV
    train_t0_s, train_t1_s = (
        np.asarray(opt_inputs.train_window_ms) * 1e-3).tolist()

    # ── Resolve pulse pool ──
    if pulse_pool is None and cell_data is not None:
        pulse_pool = getattr(cell_data, "ss_individual_pulses", None)
    has_nonparametric = (pulse_pool is not None and len(pulse_pool) > 0)

    # ── Figure layout ──
    n_panels = 2 if has_nonparametric else 1
    fig, axes = plt.subplots(1, n_panels, figsize=(7.0 * n_panels, 5.0))
    if n_panels == 1:
        axes = [axes]

    # ── Compute MLE traces for the first training bundle ──
    b0 = opt_inputs.train_bundles[0]
    cell = fit_result.neuron_cell
    cm, rm, ra = mle_phys
    cell.set_passive(cm, rm, ra)
    cell.set_e_pas(v_rest)
    t_sim_s, v_sim_raw = _simulate_square_subthreshold(cell, b0, v_rest)
    sim_v = _interp_to_grid(t_sim_s, v_sim_raw, b0.t)

    # ── σ_residual for noise ──
    sigma_res = float(getattr(fit_result, "residuals_sigma_mV", float("nan")))
    if not np.isfinite(sigma_res):
        sigma_res = float(fit_result.noise_sigma_mV)
    rho_res = float(getattr(fit_result, "residuals_rho_lag1", 0.0))
    if not np.isfinite(rho_res):
        rho_res = 0.0

    # ── LEFT PANEL: Parametric ──
    ax = axes[0]
    t_ms = b0.t * 1e3  # convert to ms

    ax.plot(t_ms, b0.v_mV, color="k", lw=1.2, alpha=0.8,
            label="experiment (averaged)")
    ax.plot(t_ms, sim_v, color="tab:red", lw=1.0, alpha=0.9,
            label="MLE model")

    post_mask = (b0.t >= train_t0_s) & (b0.t <= train_t1_s)
    pre_mask  = (b0.t < train_t0_s)
    bd = float(b0.v_mV[pre_mask].mean()) if pre_mask.any() else 0.0
    bs = float(sim_v[pre_mask].mean())   if pre_mask.any() else 0.0

    colors_syn = plt.cm.Set2(np.linspace(0, 1, max(n_examples, 3)))
    for k in range(n_examples):
        n_post = int(post_mask.sum())
        noise = _generate_noise(
            n=n_post, sigma=sigma_res, rho=rho_res,
            mode=noise_mode, block_length=None,
            empirical_residuals=None, rng=rng,
        )
        syn_v = b0.v_mV.copy()
        syn_v[post_mask] = sim_v[post_mask] - bs + bd + noise
        ax.plot(t_ms, syn_v, color=colors_syn[k], lw=0.7, alpha=0.6,
                label=f"synthetic #{k+1}" if k < 3 else None)

    ax.set_xlabel("Time (ms)")
    ax.set_ylabel("V (mV)")
    ax.set_title(f"Parametric bootstrap traces\n"
                 f"(σ_res={sigma_res:.4f} mV, mode={noise_mode})",
                 fontsize=10)
    ax.legend(loc="best", fontsize=8)

    # ── RIGHT PANEL: Nonparametric ──
    if has_nonparametric:
        ax2 = axes[1]
        # Original averaged trace
        ax2.plot(t_ms, b0.v_mV, color="k", lw=1.2, alpha=0.8,
                 label="original average")

        for k in range(n_examples):
            idx = rng.integers(0, len(pulse_pool), size=n_pulses_per_replicate)
            drawn = [pulse_pool[int(j)] for j in idx]
            # Filter by the polarity of the first training bundle for display
            target_pol = b0.polarity
            same_pol = [w for w in drawn if w["polarity"] == target_pol]
            if not same_pol:
                same_pol = drawn  # fallback
            v_stack = np.stack([w["v"] for w in same_pol])
            avg_v = v_stack.mean(axis=0)
            t_ref_ms = same_pol[0]["t"] * 1e3
            ax2.plot(t_ref_ms, avg_v, color=colors_syn[k], lw=0.8,
                     alpha=0.7,
                     label=f"resampled avg #{k+1} "
                           f"(n={len(same_pol)})" if k < 3 else None)

        ax2.set_xlabel("Time (ms)")
        ax2.set_ylabel("V (mV)")
        ax2.set_title(
            f"Nonparametric bootstrap traces\n"
            f"(pool={len(pulse_pool)} pulses, "
            f"draw={n_pulses_per_replicate}, "
            f"groups={n_avg_groups_bootstrap})",
            fontsize=10,
        )
        ax2.legend(loc="best", fontsize=8)

    fig.suptitle(f"Bootstrap trace diagnostic — cell {fit_result.specimen_id}",
                 fontsize=11, fontweight="bold")
    fig.tight_layout(rect=[0, 0, 1, 0.95])

    if out_dir is None:
        out_dir = Path(".")
    out_dir = Path(out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)
    path = out_dir / "bootstrap_trace_diagnostic.png"
    fig.savefig(path, dpi=140)
    plt.close(fig)
    return path

"""
phase3_persistence_v4.py — robust save/load for Phase-3 v3 artefacts
============================================================================

Drop-in replacement for the persistence half of
`phase3_persistence_and_replot.py`.  The four replot functions for
histograms, pairwise scatters, and GP profiles are unchanged (they only
read `bundle.bootstrap_result` and `bundle.gp_diagnostic_result`, both
preserved verbatim).  `replot_trace_diagnostic` is rewritten to read from
the flat bundle structure.

Why this rewrite
----------------
The v3 bundle stored ``fit_result_stripped`` and ``cell_data_stripped``.
The former still carried ``gp_result``, a ``scipy.optimize.OptimizeResult``
that references the wrapped loss function — which closes over the live
NEURON cell.  Pickling that closure either silently truncates or fails
midway, depending on the protocol and the size, which is exactly the
"43.7 MB and got cut off" symptom we observed on one cell.

Fixes
-----
1. **No live skopt / sklearn / NEURON objects ever enter the bundle.**
   We extract numpy arrays, primitives, and lite dataclasses defined in
   this module (so unpickling never depends on classes in ``__main__``
   from Phase 1/2 — except for the two ``BootstrapCIResult`` /
   ``GpDiagnosticResult`` dataclasses, which were already safe).

2. **Atomic writes** via ``tempfile.mkstemp`` + ``os.replace``.  A
   half-written file can never overwrite a good one; on failure the
   tempfile is removed.

3. **Upfront validation**: every field of the bundle is ``pickle.dumps``-ed
   in isolation *before* the bundle is written.  Unpicklable fields
   surface with a precise error name instead of a corrupted blob.

4. **Multi-file layout** under ``<root>/cell_<id>/replot/``:
       core.pkl        — bootstrap + GP diag + lite fit + MLE traces (~few MB)
       pulse_pool.pkl  — ss_individual_pulses (optional, ~10–20 MB)
       meta.json       — tiny human-readable index

5. **Graceful lazy loading**: ``load_replot_bundle`` works even if
   ``pulse_pool.pkl`` is missing or corrupt; only the nonparametric
   panel of the trace diagnostic is then unavailable.

The call signature of ``save_replot_bundle`` is preserved exactly so the
existing main script needs no changes.
"""

