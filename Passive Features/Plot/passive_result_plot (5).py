#  RENDER ALL — orchestration wrapper, unchanged orchestration order
# ═══════════════════════════════════════════════════════════════════════════════

def render_all_publication_figures(
    bundle, *,
    root_dir: Optional[str] = None,
    themes: Sequence[str] = ("light",),
    show_ci: Sequence[str] = ("percentile",),
    projection_mode: str = "c",
    verbose: bool = True,
) -> Path:
    """Render the full v4-compatible publication suite for one cell.

    Outputs under  <root_dir>/cell_<id>/publication/<theme>/

    For each theme directory:
      • corner.[pdf|png]
      • 3d_bootstrap_cloud.html
      • gp_mean_heatmap_{Cm_Rm,Cm_Ra,Rm_Ra}.[pdf|png]   ← bootstrap-RMSD
      • gp_sigma_heatmap_{Cm_Rm,Cm_Ra,Rm_Ra}.[pdf|png]  ← bootstrap-density
      • profile_{Cm,Rm,Ra}.[pdf|png]                    ← 1D GP profile
                                                          (one per param;
                                                          family_param dropped)
      • 3d_loss_surface_{Cm_Rm,Cm_Ra,Rm_Ra}.html        ← RMSD scatter
    """
    base = _output_dir(bundle, root_dir, subdir="publication")
    pairs = [("Cm", "Rm"), ("Cm", "Ra"), ("Rm", "Ra")]

    for tname in themes:
        out_dir = _ensure_dir(base / tname)
        if verbose:
            print(f"\n[publication suite — theme={tname}] → {out_dir}")

        # 1. Corner
        fig, _ = plot_corner(bundle, show_ci=show_ci, theme=tname,
                             out_dir=out_dir, filename_base="corner")
        plt.close(fig)

        # 2. 3D bootstrap cloud
        plot_3d_bootstrap_cloud(
            bundle, projection_mode=projection_mode,
            ci_kind=show_ci[0] if show_ci else "percentile",
            theme=tname, out_dir=out_dir,
            filename="3d_bootstrap_cloud.html", return_fig=False,
        )

        # 3 & 4. RMSD + density heatmaps per pair
        for p_x, p_y in pairs:
            for kind, fn in [("mean", plot_gp_heatmap_pair),
                             ("sigma", plot_gp_sigma_heatmap_pair)]:
                fig, _ = fn(bundle, p_x, p_y, show_ci=show_ci, theme=tname)
                fname = f"gp_{kind}_heatmap_{p_x}_{p_y}"
                fig.savefig(out_dir / f"{fname}.pdf",
                            facecolor=THEMES[tname].bg, bbox_inches="tight")
                fig.savefig(out_dir / f"{fname}.png",
                            facecolor=THEMES[tname].bg, dpi=200,
                            bbox_inches="tight")
                plt.close(fig)

        # 5. 1D GP profile per parameter (family collapsed)
        for p in PARAM_NAMES:
            fig, _ = plot_gp_profile_family(
                bundle, parameter=p, family_param=None,
                show_ci=show_ci, theme=tname,
            )
            fname = f"profile_{p}"
            fig.savefig(out_dir / f"{fname}.pdf",
                        facecolor=THEMES[tname].bg, bbox_inches="tight")
            fig.savefig(out_dir / f"{fname}.png",
                        facecolor=THEMES[tname].bg, dpi=200,
                        bbox_inches="tight")
            plt.close(fig)

        # 6. 3D RMSD scatter per pair
        for p_x, p_y in pairs:
            plot_3d_loss_surface(
                bundle, p_x, p_y, theme=tname,
                out_dir=out_dir,
                filename=f"3d_loss_surface_{p_x}_{p_y}.html",
                save_html=True,
            )

        if verbose:
            print(f"[publication suite — theme={tname}] done.")
    return base

