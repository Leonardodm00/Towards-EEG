# ═══════════════════════════════════════════════════════════════════════════════
#  CI computation — percentile, BCa, normal  (scipy/numpy)
# ═══════════════════════════════════════════════════════════════════════════════

def _ci_percentile(samples_1d: np.ndarray, alpha: float
                   ) -> Tuple[float, float]:
    """Basic percentile CI: α/2 and 1−α/2 quantiles."""
    tail = (1.0 - alpha) / 2.0
    lo = float(np.quantile(samples_1d, tail))
    hi = float(np.quantile(samples_1d, 1.0 - tail))
    return lo, hi


def _ci_normal(samples_1d: np.ndarray, alpha: float
               ) -> Tuple[float, float]:
    """Normal (Wald) bootstrap CI: mean ± z_{α/2} × SE.

    Uses ``scipy.stats.norm.ppf`` for the critical value.
    """
    mean = float(np.mean(samples_1d))
    se   = float(np.std(samples_1d, ddof=1))
    z    = float(sp_stats.norm.ppf(1.0 - (1.0 - alpha) / 2.0))
    return mean - z * se, mean + z * se


def _ci_bca(
    samples_1d: np.ndarray,
    theta_hat: float,
    alpha: float,
) -> Tuple[float, float]:
    """Bias-corrected and accelerated (BCa) bootstrap CI.

    Implements Efron & Tibshirani (1993) §14.3:
        z₀ = Φ⁻¹(fraction of θ* < θ̂)
        a  = jackknife-on-bootstrap acceleration
        α₁ = Φ(z₀ + (z₀ + z_{α/2}) / (1 − a·(z₀ + z_{α/2})))
        α₂ = Φ(z₀ + (z₀ + z_{1−α/2}) / (1 − a·(z₀ + z_{1−α/2})))

    Uses ``scipy.stats.norm.ppf`` / ``.cdf`` for Φ / Φ⁻¹.
    """
    n = len(samples_1d)
    if n < 3:
        return float("nan"), float("nan")

    # ── Bias correction z₀ ──
    frac_below = np.mean(samples_1d < theta_hat)
    frac_below = np.clip(frac_below, 1e-8, 1.0 - 1e-8)
    z0 = float(sp_stats.norm.ppf(frac_below))

    # ── Acceleration a (jackknife on the bootstrap samples) ──
    # Delete-one mean estimator applied to the bootstrap samples themselves.
    # This is the standard shortcut when the original data is not available
    # (Efron & Tibshirani 1993 §14.3, DiCiccio & Efron 1996 §5).
    jk_means = np.empty(n)
    total = float(np.sum(samples_1d))
    for i in range(n):
        jk_means[i] = (total - samples_1d[i]) / (n - 1)
    jk_bar = float(np.mean(jk_means))
    d = jk_bar - jk_means
    a_num = float(np.sum(d ** 3))
    a_den = float(6.0 * (np.sum(d ** 2)) ** 1.5)
    a = a_num / a_den if abs(a_den) > 1e-15 else 0.0

    # ── Adjusted quantiles ──
    z_lo = float(sp_stats.norm.ppf((1.0 - alpha) / 2.0))
    z_hi = float(sp_stats.norm.ppf(1.0 - (1.0 - alpha) / 2.0))

    def _adj(z):
        numer = z0 + z
        denom = 1.0 - a * numer
        if abs(denom) < 1e-12:
            return 0.5
        return float(sp_stats.norm.cdf(z0 + numer / denom))

    alpha1 = _adj(z_lo)
    alpha2 = _adj(z_hi)

    # Clip to valid quantile range
    alpha1 = np.clip(alpha1, 1.0 / (2.0 * n), 1.0 - 1.0 / (2.0 * n))
    alpha2 = np.clip(alpha2, 1.0 / (2.0 * n), 1.0 - 1.0 / (2.0 * n))

    lo = float(np.quantile(samples_1d, alpha1))
    hi = float(np.quantile(samples_1d, alpha2))
    return lo, hi


# ═══════════════════════════════════════════════════════════════════════════════
#  Noise generation (parametric mode only — unchanged from v2)
# ═══════════════════════════════════════════════════════════════════════════════

def _generate_noise(
    n: int, sigma: float, rho: float,
    mode: str, block_length: Optional[int],
    empirical_residuals: Optional[np.ndarray],
    rng: np.random.Generator,
) -> np.ndarray:
    """Generate n residual-noise samples (iid | ar1 | block)."""
    n = int(n)
    if mode == "iid":
        return rng.normal(0.0, sigma, size=n)
    if mode == "ar1":
        if not np.isfinite(rho):
            rho = 0.0
        rho = float(np.clip(rho, -0.99, 0.99))
        eps = np.empty(n)
        eps[0] = rng.normal(0.0, sigma)
        innov_scale = sigma * float(np.sqrt(max(1 - rho**2, 1e-12)))
        for t in range(1, n):
            eps[t] = rho * eps[t-1] + rng.normal(0.0, innov_scale)
        return eps
    if mode == "block":
        if empirical_residuals is None or empirical_residuals.size < 4:
            raise ValueError("block mode requires empirical_residuals")
        N = empirical_residuals.size
        if block_length is None:
            block_length = max(int(np.floor(np.sqrt(N))), 2)
        n_blocks = int(np.ceil(n / block_length))
        starts = rng.integers(0, N, size=n_blocks)
        out = np.empty(n_blocks * block_length)
        for k, s in enumerate(starts):
            idx = (s + np.arange(block_length)) % N
            out[k * block_length:(k + 1) * block_length] = empirical_residuals[idx]
        return out[:n]
    raise ValueError(f"Unknown noise mode: {mode!r}")


# ═══════════════════════════════════════════════════════════════════════════════
#  MLE traces cache (parametric mode)
# ═══════════════════════════════════════════════════════════════════════════════

def _compute_mle_traces(
    cell, train_bundles, v_rest_mV: float,
    train_window_ms: Sequence[float],
    mle_physical: Tuple[float, float, float],
) -> Dict[int, np.ndarray]:
    """Simulate each training bundle at MLE; return interpolated traces."""
    cm, rm, ra = mle_physical
    cell.set_passive(cm, rm, ra)
    cell.set_e_pas(v_rest_mV)
    traces = {}
    for i, b in enumerate(train_bundles):
        t_sim_s, v_sim_raw = _simulate_square_subthreshold(cell, b, v_rest_mV)
        traces[i] = _interp_to_grid(t_sim_s, v_sim_raw, b.t)
    return traces


# ═══════════════════════════════════════════════════════════════════════════════
#  Single bootstrap iteration — PARAMETRIC
# ═══════════════════════════════════════════════════════════════════════════════

def _run_one_parametric_iteration(
    *,
    cell, opt_inputs,
    mle_traces: Dict[int, np.ndarray],
    mle_log: Tuple[float, float, float],
    sigma: float, rho: float,
    empirical_residuals: Optional[np.ndarray],
    noise_mode: str, block_length: Optional[int],
    fit_mode: str, n_calls: int, n_initial: int, ball_radius: float,
    v_rest_mV: float, train_window_ms: Sequence[float],
    seed: int,
) -> Dict[str, float]:
    """One parametric residual bootstrap refit.

    Synthesises noise → adds to MLE trace → builds synthetic bundles → refits.
    """
    rng = np.random.default_rng(int(seed))
    train_t0_s, train_t1_s = (np.asarray(train_window_ms) * 1e-3).tolist()

    # ── Build synthetic bundles ──
    synthetic_bundles = []
    for i, b in enumerate(opt_inputs.train_bundles):
        sim_v = mle_traces[i]
        post_mask = (b.t >= train_t0_s) & (b.t <= train_t1_s)
        pre_mask  = (b.t < train_t0_s)
        n_post = int(post_mask.sum())
        noise = _generate_noise(
            n=n_post, sigma=sigma, rho=rho,
            mode=noise_mode, block_length=block_length,
            empirical_residuals=empirical_residuals, rng=rng,
        )
        synthetic_v = b.v_mV.copy()
        baseline_data = float(b.v_mV[pre_mask].mean()) if pre_mask.any() else 0.0
        baseline_sim  = float(sim_v[pre_mask].mean())   if pre_mask.any() else 0.0
        synthetic_v[post_mask] = (
            sim_v[post_mask] - baseline_sim + baseline_data + noise
        )
        synthetic_bundles.append(dc_replace(b, v_mV=synthetic_v))

    return _refit_from_bundles(
        cell=cell, bundles=synthetic_bundles,
        v_rest_mV=v_rest_mV, train_window_ms=train_window_ms,
        mle_log=mle_log, dims=opt_inputs.skopt_dimensions,
        fit_mode=fit_mode, n_calls=n_calls, n_initial=n_initial,
        ball_radius=ball_radius, seed=seed, rng=rng,
    )


# ═══════════════════════════════════════════════════════════════════════════════
#  Single bootstrap iteration — NONPARAMETRIC
# ═══════════════════════════════════════════════════════════════════════════════

def _run_one_nonparametric_iteration(
    *,
    cell, opt_inputs,
    pulse_pool: List[Dict[str, Any]],
    n_pulses: int,
    n_avg_groups: int,
    mle_log: Tuple[float, float, float],
    fit_mode: str, n_calls: int, n_initial: int, ball_radius: float,
    v_rest_mV: float, train_window_ms: Sequence[float],
    seed: int,
) -> Dict[str, float]:
    """One nonparametric bootstrap refit.

    Draws ``n_pulses`` individual pulse windows with replacement from
    ``pulse_pool``, groups them by polarity, averages within groups of
    size ~ n_pulses / n_avg_groups, and refits.
    """
    rng = np.random.default_rng(int(seed))

    # ── Draw with replacement ──
    indices = rng.integers(0, len(pulse_pool), size=n_pulses)
    drawn = [pulse_pool[int(j)] for j in indices]

    # ── Separate by polarity and build SweepBundles ──
    by_pol: Dict[str, List[Dict]] = {"dep": [], "hyp": []}
    for w in drawn:
        by_pol[w["polarity"]].append(w)

    resampled_bundles: List[SweepBundle] = []
    for pol_label, group in by_pol.items():
        if not group:
            continue
        # Subdivide into n_avg_groups (proportional to how many fell here)
        n_g = max(1, min(n_avg_groups, len(group)))
        partitions = np.array_split(np.arange(len(group)), n_g)
        for part in partitions:
            if len(part) == 0:
                continue
            v_stack = np.stack([group[int(k)]["v"] for k in part])
            i_stack = np.stack([group[int(k)]["i"] for k in part])
            t_ref   = group[int(part[0])]["t"]
            stim_dur = float(np.mean(
                [group[int(k)]["stim_duration_s"] for k in part]))
            amp = float(np.mean(
                [group[int(k)]["peak_pA"] for k in part]))
            sr = float(group[int(part[0])].get("sampling_rate_Hz", 50000.0))

            resampled_bundles.append(SweepBundle(
                polarity=pol_label,
                amplitude_pA=amp,
                t=t_ref,
                v_mV=v_stack.mean(axis=0),
                i_pA=i_stack.mean(axis=0),
                stim_onset_s=0.0,
                stim_duration_s=stim_dur,
                n_repeats_averaged=int(len(part)),
                sweep_numbers=[],
                sampling_rate_Hz=sr,
            ))

    if not resampled_bundles:
        raise RuntimeError("No bundles produced from resampled pulses.")

    return _refit_from_bundles(
        cell=cell, bundles=resampled_bundles,
        v_rest_mV=v_rest_mV, train_window_ms=train_window_ms,
        mle_log=mle_log, dims=opt_inputs.skopt_dimensions,
        fit_mode=fit_mode, n_calls=n_calls, n_initial=n_initial,
        ball_radius=ball_radius, seed=seed, rng=rng,
    )


# ═══════════════════════════════════════════════════════════════════════════════
#  Shared refit logic
# ═══════════════════════════════════════════════════════════════════════════════

def _refit_from_bundles(
    *, cell, bundles, v_rest_mV, train_window_ms,
    mle_log, dims, fit_mode, n_calls, n_initial, ball_radius,
    seed, rng,
) -> Dict[str, float]:
    """Build loss from bundles → run gp_minimize → return fitted params."""
    fn = _locate_build_loss_function()
    loss_fn = fn(
        cell=cell, train_bundles=bundles,
        v_rest_mV=v_rest_mV, train_window_ms=train_window_ms,
    )

    def wrapped(x):
        return float(loss_fn(x[0], x[1], x[2]))

    if fit_mode == "fast":
        mle_arr = np.asarray(mle_log)
        lows  = np.array([float(d.low)  for d in dims])
        highs = np.array([float(d.high) for d in dims])
        x0 = [mle_arr.tolist()]
        for _ in range(max(n_initial - 1, 0)):
            cand = mle_arr + rng.normal(0.0, ball_radius, size=3)
            cand = np.clip(cand, lows, highs)
            x0.append(cand.tolist())
        res = gp_minimize(
            func=wrapped, dimensions=dims,
            x0=x0, n_initial_points=0,
            n_calls=int(n_calls), random_state=int(seed), acq_func="EI",
        )
    elif fit_mode == "rigorous":
        res = gp_minimize(
            func=wrapped, dimensions=dims,
            n_initial_points=int(n_initial),
            n_calls=int(n_calls), random_state=int(seed), acq_func="EI",
        )
    else:
        raise ValueError(f"Unknown fit_mode: {fit_mode!r}")

    cm_log, rm_log, ra_log = (float(res.x[0]), float(res.x[1]),
                               float(res.x[2]))
    return {
        "cm_log": cm_log, "rm_log": rm_log, "ra_log": ra_log,
        "cm": float(np.exp(cm_log)),
        "rm": float(np.exp(rm_log)),
        "ra": float(np.exp(ra_log)),
        "rmsd": float(res.fun),
    }


def _locate_build_loss_function():
    """Find Phase 2's _build_loss_function in the current session."""
    import inspect
    for frame_info in inspect.stack():
        g = frame_info.frame.f_globals
        if "_build_loss_function" in g:
            return g["_build_loss_function"]
    import sys as _sys
    fn = getattr(_sys.modules.get("__main__"), "_build_loss_function", None)
    if fn is not None:
        return fn
    raise RuntimeError(
        "Could not locate _build_loss_function.  Run the Phase-2 "
        "definition cell in the current notebook session first."
    )


# ═══════════════════════════════════════════════════════════════════════════════
#  Worker wrapper (sequential and parallel)
# ═══════════════════════════════════════════════════════════════════════════════

_WORKER_STATE: Dict[str, Any] = {}


def _bootstrap_worker_run_v3(task):
    """Dispatch one bootstrap iteration (parametric or nonparametric)."""
    (
        idx, mode,
        opt_inputs_bytes, mle_traces_bytes,
        mle_log, sigma, rho,
        residuals_bytes,
        noise_mode, block_length,
        pulse_pool_bytes, n_pulses, n_avg_groups_boot,
        fit_mode, n_calls, n_initial, ball_radius,
        v_rest_mV, train_window_ms, seed,
    ) = task

    try:
        cell = _WORKER_STATE["cell"]
        opt_inputs = pickle.loads(opt_inputs_bytes)

        if mode == "parametric":
            mle_traces = pickle.loads(mle_traces_bytes)
            empirical_residuals = (pickle.loads(residuals_bytes)
                                   if residuals_bytes else None)
            result = _run_one_parametric_iteration(
                cell=cell, opt_inputs=opt_inputs,
                mle_traces=mle_traces, mle_log=mle_log,
                sigma=sigma, rho=rho,
                empirical_residuals=empirical_residuals,
                noise_mode=noise_mode, block_length=block_length,
                fit_mode=fit_mode, n_calls=n_calls, n_initial=n_initial,
                ball_radius=ball_radius,
                v_rest_mV=v_rest_mV, train_window_ms=train_window_ms,
                seed=seed,
            )
        else:  # nonparametric
            pulse_pool = pickle.loads(pulse_pool_bytes)
            result = _run_one_nonparametric_iteration(
                cell=cell, opt_inputs=opt_inputs,
                pulse_pool=pulse_pool,
                n_pulses=n_pulses, n_avg_groups=n_avg_groups_boot,
                mle_log=mle_log,
                fit_mode=fit_mode, n_calls=n_calls, n_initial=n_initial,
                ball_radius=ball_radius,
                v_rest_mV=v_rest_mV, train_window_ms=train_window_ms,
                seed=seed,
            )
        return (idx, result, "")
    except Exception as exc:
        return (idx, None, f"{type(exc).__name__}: {exc}")


