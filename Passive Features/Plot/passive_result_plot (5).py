#  GP diagnostic (updated for v3 — no joint CIs)
# ═══════════════════════════════════════════════════════════════════════════════

def gp_diagnostic_for_cell(
    *,
    fit_result,
    bootstrap_result: Optional[BootstrapCIResult] = None,
    root_dir: str,
    output_dir: Optional[str] = None,
    n_grid: int               = DEFAULT_GP_N_GRID,
    inner_grid_per_axis: int  = DEFAULT_GP_INNER_GRID,
    envelope_k: float         = DEFAULT_GP_K,
    n_validation_per_bound: int      = DEFAULT_GP_N_VALIDATION,
    validation_ball_logradius: float = DEFAULT_GP_BALL_LOGRADIUS,
    trust_abs_mv: float              = DEFAULT_TRUST_ABS_MV,
    trust_zscore: float              = DEFAULT_TRUST_ZSCORE,
    save_plots:  bool = True,
    save_pickle: bool = True,
    seed: int         = 0,
    verbose: bool     = True,
) -> GpDiagnosticResult:
    """GP-surrogate profile diagnostic with σ-envelope (v3).

    Overlays whatever CI types are present in ``bootstrap_result``.
    Validates each CI boundary point with real NEURON calls.
    """
    if output_dir is not None:
        out_dir = Path(output_dir)
    else:
        out_dir = (Path(root_dir)
                   / f"cell_{int(fit_result.specimen_id)}"
                   / "gp_diagnostic")
    out_dir.mkdir(parents=True, exist_ok=True)

    rng = np.random.default_rng(int(seed))
    notes: List[str] = []
    per_param: Dict[str, GpDiagnosticPerParameter] = {}

    if verbose:
        ci_tag = "attached" if bootstrap_result is not None else "NOT attached"
        print(f"[gp_diagnostic] cell {fit_result.specimen_id}  k={envelope_k}"
              f"  bootstrap CIs {ci_tag}")

    # ── Collect all CI types present in bootstrap_result ──
    ci_dict_map: Dict[str, Dict[str, Tuple[float, float]]] = {}
    if bootstrap_result is not None:
        ci_dict_map["percentile"] = bootstrap_result.ci_percentile
        ci_dict_map["bca"]        = bootstrap_result.ci_bca
        ci_dict_map["normal"]     = bootstrap_result.ci_normal

    for p in PARAM_NAMES:
        grid_log = _build_log_grid(fit_result, fit_result.opt_inputs, p, n_grid)
        grid_phy = np.exp(grid_log)
        mu, sig = _build_gp_profile(
            gp_result=fit_result.gp_result,
            opt_inputs=fit_result.opt_inputs,
            param_name=p, grid_log=grid_log,
            inner_grid_per_axis=inner_grid_per_axis,
        )
        mle_val = {"Cm": fit_result.cm_uF_per_cm2,
                   "Rm": fit_result.rm_Ohm_cm2,
                   "Ra": fit_result.ra_Ohm_cm}[p]

        # ── Validate at each CI boundary ──
        validation_pts: Dict[str, List[Tuple[float, float, float, float]]] = {}
        for ci_name, ci_table in ci_dict_map.items():
            lo_val, hi_val = ci_table[p]
            pts_lo = _validate_at_param_value(
                theta_phys=lo_val, param_name=p, fit_result=fit_result,
                n_validation=n_validation_per_bound,
                ball_log_radius=validation_ball_logradius, rng=rng,
            ) if np.isfinite(lo_val) else []
            pts_hi = _validate_at_param_value(
                theta_phys=hi_val, param_name=p, fit_result=fit_result,
                n_validation=n_validation_per_bound,
                ball_log_radius=validation_ball_logradius, rng=rng,
            ) if np.isfinite(hi_val) else []
            validation_pts[f"{ci_name}_lo"] = pts_lo
            validation_pts[f"{ci_name}_hi"] = pts_hi

        all_pts = [pt for v_list in validation_pts.values() for pt in v_list]
        trustworthy = (
            _gp_trust_combined(all_pts, trust_abs_mv, trust_zscore)
            if all_pts else True
        )
        if not trustworthy:
            msg = (f"[gp_diagnostic]   {p}: GP may under-resolve the loss "
                   f"near a CI boundary.")
            if verbose:
                print(msg)
            notes.append(msg)

        per_param[p] = GpDiagnosticPerParameter(
            name=p, mle_value=float(mle_val),
            grid_physical=grid_phy, grid_log=grid_log,
            profile_mean=mu, profile_std=sig,
            validation_points=validation_pts,
            gp_trustworthy=trustworthy,
        )

    result = GpDiagnosticResult(
        specimen_id=int(fit_result.specimen_id),
        gp_envelope_k=float(envelope_k),
        per_parameter=per_param,
        notes=notes,
    )

    if save_pickle:
        with open(out_dir / "gp_diagnostic_result.pkl", "wb") as f:
            pickle.dump(result, f)
    if save_plots:
        plot_gp_diagnostic(result, bootstrap_result, out_dir, envelope_k)

    return result


# ═══════════════════════════════════════════════════════════════════════════════
#  Top-level wrapper
# ═══════════════════════════════════════════════════════════════════════════════

def phase3_full_for_cell(
    *,
    fit_result,
    root_dir: str,
    bootstrap_kwargs: Optional[Dict[str, Any]] = None,
    gp_kwargs: Optional[Dict[str, Any]] = None,
    verbose: bool = True,
) -> Phase3Result:
    """Run bootstrap + GP diagnostic for one cell (v3).

    Output structure:
        <root_dir>/cell_<id>/
          ├─ bootstrap/        *.pkl, *.npy, *.csv, histogram_*.png, pairwise_*.png
          └─ gp_diagnostic/    *.pkl, profile_*.png
    """
    if verbose:
        print(f"\n=== Phase 3 v3 — cell {fit_result.specimen_id} ===")

    boot = bootstrap_ci_for_cell(
        fit_result=fit_result, root_dir=root_dir,
        verbose=verbose, **(bootstrap_kwargs or {}),
    )
    gp = gp_diagnostic_for_cell(
        fit_result=fit_result, bootstrap_result=boot,
        root_dir=root_dir, verbose=verbose, **(gp_kwargs or {}),
    )
    return Phase3Result(
        specimen_id=int(fit_result.specimen_id),
        bootstrap=boot, gp_diagnostic=gp,
    )


