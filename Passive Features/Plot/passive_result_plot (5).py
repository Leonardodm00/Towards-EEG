def _locate_global(name: str):
    """Find a function or class by name in any frame's globals or __main__.

    Used to locate Phase-1/Phase-2 helpers that the user paste-defines in
    Colab (``_simulate_square_subthreshold``, ``_interp_to_grid``,
    ``_generate_noise``).
    """
    for frame_info in inspect.stack():
        g = frame_info.frame.f_globals
        if name in g:
            return g[name]
    fn = getattr(sys.modules.get("__main__"), name, None)
    if fn is not None:
        return fn
    raise RuntimeError(
        f"Could not locate {name!r} in current session. "
        f"Make sure the defining cell has been executed.")


def _precompute_mle_traces(fit_result) -> Dict[int, np.ndarray]:
    """Simulate each training bundle at MLE; return interpolated traces.

    Must be called while ``fit_result.neuron_cell`` is still alive.
    Reuses ``_simulate_square_subthreshold`` and ``_interp_to_grid``
    from the running session (Phase 1 / Phase 3 module).
    """
    simulate = _locate_global("_simulate_square_subthreshold")
    interp   = _locate_global("_interp_to_grid")

    cell       = fit_result.neuron_cell
    opt_inputs = fit_result.opt_inputs
    mle_phys = (float(fit_result.cm_uF_per_cm2),
                float(fit_result.rm_Ohm_cm2),
                float(fit_result.ra_Ohm_cm))
    cell.set_passive(*mle_phys)
    cell.set_e_pas(opt_inputs.v_rest_mV)

    traces: Dict[int, np.ndarray] = {}
    for i, b in enumerate(opt_inputs.train_bundles):
        t_sim_s, v_sim_raw = simulate(cell, b, opt_inputs.v_rest_mV)
        traces[i] = np.asarray(interp(t_sim_s, v_sim_raw, b.t), dtype=float)
    return traces


