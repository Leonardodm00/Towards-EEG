import io
import pickle
from pathlib import Path


class _AttrBag:
    """Bulletproof placeholder accepting every pickle reconstruction path."""

    def __new__(cls, *args, **kwargs):
        # Pickle may call cls.__new__(cls, *args) — accept anything.
        inst = object.__new__(cls)
        if args:
            inst.__dict__["_init_args"] = args
        if kwargs:
            inst.__dict__["_init_kwargs"] = kwargs
        return inst

    def __init__(self, *args, **kwargs):
        # Pickle may also call cls(*args) — accept anything.
        if args and "_init_args" not in self.__dict__:
            self.__dict__["_init_args"] = args
        if kwargs and "_init_kwargs" not in self.__dict__:
            self.__dict__["_init_kwargs"] = kwargs

    def __setstate__(self, state):
        if isinstance(state, dict):
            self.__dict__.update(state)
        elif isinstance(state, tuple) and len(state) == 2:
            sd, slots = state
            if isinstance(sd, dict):
                self.__dict__.update(sd)
            if isinstance(slots, dict):
                self.__dict__.update(slots)
        else:
            self.__dict__["_state"] = state

    # Some pickle protocols call __reduce__ on the placeholder during
    # cycle resolution — give a sensible default.
    def __reduce__(self):
        return (_AttrBag, (), self.__dict__.copy())

    def __repr__(self):
        keys = ", ".join(sorted(self.__dict__.keys())[:6])
        return f"<_AttrBag {{{keys}{'...' if len(self.__dict__) > 6 else ''}}}>"


_PROTECTED_NAMES = {
    "ReplotBundle", "BootstrapCIResult", "GpDiagnosticResult",
    "GpDiagnosticPerParameter", "Phase3Result",
}


class _ResilientUnpickler(pickle.Unpickler):
    """Tolerant unpickler: substitute _AttrBag for unresolvable classes."""

    def __init__(self, fileobj, missing_log):
        super().__init__(fileobj)
        self._missing_log = missing_log

    def find_class(self, module, name):
        # 1. Standard resolution first
        try:
            return super().find_class(module, name)
        except (ModuleNotFoundError, AttributeError, ImportError):
            pass

        # 2. Cross-module fallback for our own dataclasses
        if name in _PROTECTED_NAMES:
            for mod_name in ("__main__", "phase3_bootstrap_v3_clean",
                             "phase3_bootstrap_v3"):
                try:
                    mod = __import__(mod_name)
                    cls = getattr(mod, name, None)
                    if cls is not None:
                        return cls
                except ImportError:
                    pass

        # 3. Last resort: bulletproof placeholder
        if (module, name) not in self._missing_log:
            self._missing_log.append((module, name))
        return _AttrBag


def _load_replot_bundle_v3_resilient(path, *, verbose: bool = True):
    """RENAMED FROM load_replot_bundle (v3-style, single-file, resilient).

    Reason for rename: this function used to shadow the v4 loader defined
    earlier in the file at line ~2380 because Python's "last definition
    wins" rule applied when the whole module was pasted into one Colab
    cell. The v4 loader is directory-aware (it resolves core.pkl); this
    one only handles a single pickle file. Keep this version available as
    a private fallback for legacy v3 bundles whose unpickling needs the
    _AttrBag substitution logic. The public name `load_replot_bundle`
    below now dispatches: v4 directory layout if present, this resilient
    loader otherwise.

    Two-pass loader: standard pickle, then resilient fallback.
    Whichever succeeds, you get a bundle.  The plotting code only reads
    attributes by name and doesn't care whether the objects are real
    dataclass instances or _AttrBag placeholders.
    """
    path = Path(path)
    if not path.exists():
        raise FileNotFoundError(
            f"replot_bundle not found at {path}.")
    size = path.stat().st_size
    if size == 0:
        raise RuntimeError(
            f"replot_bundle at {path} is 0 bytes — the previous save aborted.")

    # ── PASS 1: standard pickle.load ─────────────────────────────────────
    try:
        with open(path, "rb") as f:
            bundle = pickle.load(f)
        if verbose:
            print(f"[load_replot_bundle] {path.name} ({size/1024:.1f} kB) "
                  f"loaded via standard pickle.")
        return bundle
    except (AttributeError, ModuleNotFoundError, ImportError) as e:
        if verbose:
            print(f"[load_replot_bundle] standard pickle failed "
                  f"({type(e).__name__}: {e}); falling back to resilient "
                  f"unpickler.")
    except Exception as e:
        if verbose:
            print(f"[load_replot_bundle] standard pickle raised "
                  f"{type(e).__name__}: {e}; trying resilient fallback.")

    # ── PASS 2: resilient unpickler ──────────────────────────────────────
    missing: list = []
    try:
        with open(path, "rb") as f:
            bundle = _ResilientUnpickler(f, missing).load()
    except EOFError:
        raise RuntimeError(
            f"\nReplot bundle at {path} could not be loaded.\n"
            f"\n"
            f"The standard pickle failed because some class is missing\n"
            f"from your current session, and the resilient fallback then\n"
            f"hit an EOFError — meaning the substitution corrupted the\n"
            f"reconstruction protocol for one of the objects in the\n"
            f"bundle.\n"
            f"\n"
            f"Easiest fix:  in this fresh kernel, re-run the Phase 1 and\n"
            f"Phase 2 cells that DEFINE the dataclasses (SweepBundle,\n"
            f"CellData, PassiveSearchSpace, OptimiserInputs,\n"
            f"PassiveFitResult).  You do NOT need to refetch any AllenSDK\n"
            f"data or rerun the fitting — only the class definitions need\n"
            f"to be present.  Then call load_replot_bundle again.\n"
        )

    if verbose:
        unique = sorted(set(missing))
        print(f"[load_replot_bundle] {path.name} ({size/1024:.1f} kB) "
              f"loaded with {len(unique)} class(es) replaced by _AttrBag:")
        for mod_name, cls_name in unique:
            print(f"   • {mod_name}.{cls_name}")
    return bundle




