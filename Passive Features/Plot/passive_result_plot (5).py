def _dim_bounds_log(bundle, p: str) -> Tuple[float, float]:
    """Return (low, high) in LOG space for parameter ``p``, from v4 bundle.

    v4 stores ``skopt_dimensions`` as a list of ``_SkoptDimLite`` carrying
    ``.name``, ``.low``, ``.high``. We index by name rather than position
    so the order in the list doesn't matter.
    """
    name_to_dim = {d.name: d for d in bundle.skopt_dimensions}
    if p not in name_to_dim:
        raise KeyError(f"parameter {p!r} not in bundle.skopt_dimensions; "
                       f"available: {list(name_to_dim)}")
    d = name_to_dim[p]
    return float(d.low), float(d.high)


def _kde_2d_on_grid(samples_2d_log, n_grid=120, pad_frac=0.10,
                    extra_bounds_log=None):
    """Evaluate a 2D Gaussian KDE on a regular grid in log-space.

    Parameters
    ----------
    extra_bounds_log : tuple(x0, x1, y0, y1) or None
        Optional extra bounds (in log-space, same units as ``samples_2d_log``)
        that the grid MUST cover.  Useful when an overlay (e.g. a Gaussian
        confidence ellipse) extends beyond the bootstrap convex hull and
        we want the KDE colour to extend with it rather than cut off at
        the data range.  The final grid bounds are the union of the
        padded data range and ``extra_bounds_log``.
    """
    x, y = samples_2d_log[:, 0], samples_2d_log[:, 1]
    xr = (x.max() - x.min()) or 1.0
    yr = (y.max() - y.min()) or 1.0
    x0, x1 = x.min() - pad_frac*xr, x.max() + pad_frac*xr
    y0, y1 = y.min() - pad_frac*yr, y.max() + pad_frac*yr
    if extra_bounds_log is not None:
        ex0, ex1, ey0, ey1 = extra_bounds_log
        x0, x1 = min(x0, ex0), max(x1, ex1)
        y0, y1 = min(y0, ey0), max(y1, ey1)
    X, Y = np.meshgrid(np.linspace(x0, x1, n_grid),
                       np.linspace(y0, y1, n_grid))
    kde = gaussian_kde(np.vstack([x, y]))
    Z = kde(np.vstack([X.ravel(), Y.ravel()])).reshape(X.shape)
    return X, Y, Z


def _gaussian_contour_log(cov_log_2x2, mu_log_2, level=0.95, n_points=200):
    radius = float(np.sqrt(chi2_dist.ppf(level, df=2)))
    thetas = np.linspace(0, 2*np.pi, n_points)
    unit_circle = np.stack([np.cos(thetas), np.sin(thetas)], axis=0)
    L = np.linalg.cholesky(cov_log_2x2 + 1e-12*np.eye(2))
    return mu_log_2[:, None] + radius * (L @ unit_circle)


