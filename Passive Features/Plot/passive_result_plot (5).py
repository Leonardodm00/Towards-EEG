def _bootstrap_rmsd_slice(
    bundle, p_x: str, p_y: str, *,
    n_grid: int = 120,
    method: Literal["linear", "cubic", "nearest"] = "linear",
) -> Tuple[np.ndarray, np.ndarray, np.ndarray]:
    """Interpolate bootstrap-refit RMSDs onto a 2D log-space grid.

    This is NOT a GP slice. Each bootstrap sample has a stored RMSD
    (``boot.rmsds[i]``) reflecting the value of the loss function at
    that re-optimised triplet. We treat the (samples_log[:, [ix, iy]],
    rmsds) point cloud as scattered observations of the loss surface
    and interpolate them onto a regular grid.

    The third parameter is NOT pinned at MLE — every bootstrap sample
    contributes regardless of where its third coordinate lies. The
    resulting surface is therefore the marginalised loss-along-the-
    sampled-manifold, not a true slice. Outside the bootstrap convex
    hull the interpolation is NaN.

    Returns
    -------
    Xp, Yp : meshgrid in physical units (n_grid, n_grid)
    Z      : interpolated RMSD (mV), NaN outside the bootstrap convex hull
    """
    boot = bundle.bootstrap_result
    ix, iy = _NC[p_x], _NC[p_y]
    pts = boot.samples_log[:, [ix, iy]]
    vals = boot.rmsds

    bx = _dim_bounds_log(bundle, p_x)
    by = _dim_bounds_log(bundle, p_y)
    X, Y = np.meshgrid(np.linspace(*bx, n_grid),
                       np.linspace(*by, n_grid))
    Z = griddata(pts, vals, (X, Y), method=method, fill_value=np.nan)
    return np.exp(X), np.exp(Y), Z


def _bootstrap_density_slice(
    bundle, p_x: str, p_y: str, *, n_grid: int = 120,
) -> Tuple[np.ndarray, np.ndarray, np.ndarray]:
    """2D Gaussian KDE of the bootstrap samples in (p_x, p_y) log-space.

    This shows where the bootstrap distribution concentrates — i.e. the
    posterior-like uncertainty over the two parameters after
    marginalising over the third. It is the visual analogue of the v1
    σ_GP slice, but it is not the same quantity. σ_GP measured GP
    uncertainty about the loss surface; this measures the spread of the
    fitted estimator.

    Returns Xp, Yp (physical-unit meshgrid) and the density Z evaluated
    on the grid.
    """
    boot = bundle.bootstrap_result
    ix, iy = _NC[p_x], _NC[p_y]
    bx = _dim_bounds_log(bundle, p_x)
    by = _dim_bounds_log(bundle, p_y)
    X, Y = np.meshgrid(np.linspace(*bx, n_grid),
                       np.linspace(*by, n_grid))
    kde = gaussian_kde(boot.samples_log[:, [ix, iy]].T)
    Z = kde(np.vstack([X.ravel(), Y.ravel()])).reshape(X.shape)
    return np.exp(X), np.exp(Y), Z


# ═══════════════════════════════════════════════════════════════════════════════
#  1.  3D BOOTSTRAP CLOUD — unchanged from v1 (no GP access)
# ═══════════════════════════════════════════════════════════════════════════════

def plot_3d_bootstrap_cloud(
    bundle, *,
    projection_mode: Literal["a", "b", "c", "none"] = "c",
    show_ci_rectangles: bool = True,
    ci_kind: str = "percentile",
    contour_level: float = 0.95,
    n_contour_points: int = 200,
    theme: Union[str, Theme] = "light",
    point_size: float = 3.0,
    shadow_size: float = 2.0,
    shadow_opacity: float = 0.22,
    title: Optional[str] = None,
    out_dir: Optional[str] = None,
    filename: str = "3d_bootstrap_cloud.html",
    width: int = 900, height: int = 800,
    save_html: bool = True, return_fig: bool = True,
):
    """Interactive Plotly 3D scatter of bootstrap samples in log-space.

    Identical to the v1 implementation — this function never touched
    the live GP. Reads only `bundle.bootstrap_result` (samples, mle_log,
    cov_log, CIs).
    """
    if not _HAS_PLOTLY:
        raise RuntimeError("plotly not installed — `pip install plotly`.")
    th = _resolve_theme(theme)
    boot = bundle.bootstrap_result
    samples = boot.samples_log
    mle_log = np.asarray(boot.mle_log)
    cov_log = boot.cov_log
    n = samples.shape[0]

    pad = 0.08
    ranges = []
    for i in range(3):
        lo = float(min(samples[:, i].min(), mle_log[i]))
        hi = float(max(samples[:, i].max(), mle_log[i]))
        sp = (hi - lo) or 1.0
        ranges.append((lo - pad*sp, hi + pad*sp))

    fig = go.Figure()
    fig.add_trace(go.Scatter3d(
        x=samples[:, 0], y=samples[:, 1], z=samples[:, 2],
        mode="markers",
        marker=dict(size=point_size, color=th.scatter,
                    opacity=th.scatter_alpha, line=dict(width=0)),
        name=f"bootstrap (n={n})",
        hovertemplate=("Cm = %{customdata[0]:.4g}<br>"
                       "Rm = %{customdata[1]:.4g}<br>"
                       "Ra = %{customdata[2]:.4g}<extra></extra>"),
        customdata=np.exp(samples),
    ))
    fig.add_trace(go.Scatter3d(
        x=[mle_log[0]], y=[mle_log[1]], z=[mle_log[2]],
        mode="markers",
        marker=dict(size=8, color=th.mle, symbol="diamond",
                    line=dict(color=th.fg, width=1)),
        name="MLE",
        hovertemplate=(f"MLE<br>Cm = {boot.mle_physical[0]:.4g}<br>"
                       f"Rm = {boot.mle_physical[1]:.4g}<br>"
                       f"Ra = {boot.mle_physical[2]:.4g}<extra></extra>"),
    ))

    if projection_mode in ("a", "c"):
        wall_specs = [(2, ranges[2][0]), (1, ranges[1][1]), (0, ranges[0][0])]
        for (skip_dim, wval) in wall_specs:
            xs = samples[:, 0].copy(); ys = samples[:, 1].copy(); zs = samples[:, 2].copy()
            if skip_dim == 0: xs[:] = wval
            if skip_dim == 1: ys[:] = wval
            if skip_dim == 2: zs[:] = wval
            fig.add_trace(go.Scatter3d(
                x=xs, y=ys, z=zs, mode="markers",
                marker=dict(size=shadow_size, color=th.scatter,
                            opacity=shadow_opacity, line=dict(width=0)),
                showlegend=False, hoverinfo="skip",
            ))

    if projection_mode in ("b", "c"):
        wall_specs = [
            (0, 1, 2, ranges[2][0], "Cm-Rm"),
            (0, 2, 1, ranges[1][1], "Cm-Ra"),
            (1, 2, 0, ranges[0][0], "Rm-Ra"),
        ]
        for (i, j, skip_dim, wval, wname) in wall_specs:
            sub_cov = np.array([[cov_log[i, i], cov_log[i, j]],
                                [cov_log[j, i], cov_log[j, j]]])
            sub_mu = mle_log[[i, j]]
            ell_log = _gaussian_contour_log(sub_cov, sub_mu,
                                             level=contour_level,
                                             n_points=n_contour_points)
            xs = np.full(n_contour_points, wval if skip_dim == 0 else None)
            ys = np.full(n_contour_points, wval if skip_dim == 1 else None)
            zs = np.full(n_contour_points, wval if skip_dim == 2 else None)
            if skip_dim == 0:   ys = ell_log[0]; zs = ell_log[1]
            elif skip_dim == 1: xs = ell_log[0]; zs = ell_log[1]
            else:               xs = ell_log[0]; ys = ell_log[1]
            fig.add_trace(go.Scatter3d(
                x=xs, y=ys, z=zs, mode="lines",
                line=dict(color=th.ellipse_color, width=3),
                name=f"{int(100*contour_level)}% Gaussian ({wname})",
                hoverinfo="skip", showlegend=False,
            ))
            if show_ci_rectangles:
                ci_i = _gather_ci(boot, [ci_kind], PARAM_NAMES[i]).get(ci_kind)
                ci_j = _gather_ci(boot, [ci_kind], PARAM_NAMES[j]).get(ci_kind)
                if ci_i and ci_j:
                    lx, hx = np.log(ci_i[0]), np.log(ci_i[1])
                    ly, hy = np.log(ci_j[0]), np.log(ci_j[1])
                    rect_i = np.array([lx, hx, hx, lx, lx])
                    rect_j = np.array([ly, ly, hy, hy, ly])
                    xs2 = np.full(5, wval if skip_dim == 0 else None)
                    ys2 = np.full(5, wval if skip_dim == 1 else None)
                    zs2 = np.full(5, wval if skip_dim == 2 else None)
                    if skip_dim == 0:   ys2 = rect_i; zs2 = rect_j
                    elif skip_dim == 1: xs2 = rect_i; zs2 = rect_j
                    else:               xs2 = rect_i; ys2 = rect_j
                    fig.add_trace(go.Scatter3d(
                        x=xs2, y=ys2, z=zs2, mode="lines",
                        line=dict(color=th.ci_colors.get(ci_kind, th.fg),
                                  width=2, dash="dash"),
                        hoverinfo="skip", showlegend=False,
                    ))

    if title is None:
        title = (f"Cell {boot.specimen_id} — bootstrap cloud (log-space)<br>"
                 f"<sub>B={boot.B_requested}, kept={boot.n_kept}, "
                 f"mode={boot.bootstrap_mode}</sub>")

    def _axis(label_phys, rng):
        ticks = np.arange(np.floor(rng[0]), np.ceil(rng[1])+0.5, 0.5)
        ticklabels = [f"{np.exp(t):.3g}" for t in ticks]
        return dict(
            title=dict(text=label_phys, font=dict(color=th.fg)),
            range=list(rng), tickvals=ticks.tolist(), ticktext=ticklabels,
            gridcolor=th.grid, zerolinecolor=th.grid,
            color=th.fg, backgroundcolor=th.bg, showbackground=True,
        )

    fig.update_layout(
        title=dict(text=title, font=dict(color=th.fg, size=th.title_size+4)),
        paper_bgcolor=th.bg, plot_bgcolor=th.bg,
        font=dict(color=th.fg, size=th.text_size),
        width=width, height=height,
        margin=dict(l=10, r=10, b=10, t=70),
        legend=dict(font=dict(color=th.fg)),
        scene=dict(
            xaxis=_axis(PARAM_LABELS_PLAIN["Cm"], ranges[0]),
            yaxis=_axis(PARAM_LABELS_PLAIN["Rm"], ranges[1]),
            zaxis=_axis(PARAM_LABELS_PLAIN["Ra"], ranges[2]),
            bgcolor=th.bg,
            camera=dict(eye=dict(x=1.6, y=1.6, z=1.2)),
            aspectmode="cube",
        ),
    )

    if save_html and out_dir is not None:
        out_path = _ensure_dir(out_dir) / filename
        fig.write_html(str(out_path), include_plotlyjs="cdn")
        print(f"[plot_3d_bootstrap_cloud] saved → {out_path}")
    return fig if return_fig else None


# ═══════════════════════════════════════════════════════════════════════════════
#  2.  CORNER PLOT — unchanged from v1 (no GP access)
# ═══════════════════════════════════════════════════════════════════════════════

def plot_corner(
    bundle, *,
    show_ci: Sequence[str] = ("percentile",),
    n_kde_grid: int = 120, kde_levels: int = 12,
    show_scatter: bool = True,
    scatter_alpha: float = 0.35, scatter_size: float = 8.0,
    show_gaussian_ellipse: bool = True, ellipse_level: float = 0.95,
    log_axes: bool = True,
    theme: Union[str, Theme] = "light",
    figsize: Tuple[float, float] = (10.5, 10.5),
    title: Optional[str] = None,
    out_dir: Optional[str] = None,
    filename_base: str = "corner",
    save_pdf: bool = True, save_png: bool = True,
) -> Tuple[Figure, Dict[str, Axes]]:
    """Corner plot — diagonal 1D KDEs, lower-triangle 2D KDE heatmaps.

    Unchanged from v1; reads only `bundle.bootstrap_result`.
    """
    th = _resolve_theme(theme)
    boot = bundle.bootstrap_result
    samples_log = boot.samples_log
    samples_phys = boot.samples
    mle_log = np.asarray(boot.mle_log)
    mle_phys = boot.mle_physical
    cov_log = boot.cov_log

    fig = plt.figure(figsize=figsize, facecolor=th.bg)
    # Slightly larger spacing prevents tick labels from spilling into
    # the neighbouring panel on logarithmic axes.
    gs = GridSpec(3, 3, figure=fig, hspace=0.14, wspace=0.14)
    axes: Dict[str, Axes] = {}

    for i, pi in enumerate(PARAM_NAMES):
        for j, pj in enumerate(PARAM_NAMES):
            if j > i:
                continue
            ax = fig.add_subplot(gs[i, j])
            axes[f"{pi}_{pj}"] = ax

            if i == j:
                x_log = samples_log[:, i]
                grid_log = np.linspace(x_log.min(), x_log.max(), 400)
                grid_phys = np.exp(grid_log)
                kde = gaussian_kde(x_log)
                d = kde(grid_log)
                ax.plot(grid_phys, d, color=th.gp_mean, lw=1.6)
                ax.fill_between(grid_phys, 0, d, color=th.gp_mean, alpha=0.18)
                # MLE and CIs are drawn as lines but no in-panel legend:
                # a single figure-level legend at the end captures them.
                ax.axvline(mle_phys[i], color=th.mle, lw=1.2)
                for ci_kind in show_ci:
                    ci = _gather_ci(boot, [ci_kind], pi).get(ci_kind)
                    if ci is None: continue
                    c = th.ci_colors[ci_kind]
                    ax.axvline(ci[0], color=c, ls="--", lw=1.1)
                    ax.axvline(ci[1], color=c, ls="--", lw=1.1)
                if log_axes: ax.set_xscale("log")
                ax.set_yticks([])
                if i == len(PARAM_NAMES) - 1:
                    ax.set_xlabel(PARAM_LABELS[pi], color=th.fg)
                else:
                    plt.setp(ax.get_xticklabels(), visible=False)
            else:
                # 1. Compute the Gaussian confidence ellipse FIRST, so we
                #    can expand the KDE grid bounds to cover its extent.
                #    Otherwise, the ellipse can extend beyond the bootstrap
                #    convex hull and the KDE colour terminates inside the
                #    ellipse — visually misleading.
                if show_gaussian_ellipse:
                    sub_cov = np.array([[cov_log[j, j], cov_log[j, i]],
                                        [cov_log[i, j], cov_log[i, i]]])
                    sub_mu = mle_log[[j, i]]
                    ell_log = _gaussian_contour_log(sub_cov, sub_mu,
                                                    level=ellipse_level)
                    extra_bounds_log = (
                        float(ell_log[0].min()), float(ell_log[0].max()),
                        float(ell_log[1].min()), float(ell_log[1].max()),
                    )
                else:
                    ell_log = None
                    extra_bounds_log = None

                # 2. KDE on a grid that covers BOTH the data and the ellipse.
                X, Y, Z = _kde_2d_on_grid(
                    samples_log[:, [j, i]], n_grid=n_kde_grid,
                    extra_bounds_log=extra_bounds_log,
                )
                Xp, Yp = np.exp(X), np.exp(Y)
                ax.contourf(Xp, Yp, Z, levels=kde_levels,
                            cmap=th.heatmap_cmap, antialiased=True)
                if show_scatter:
                    ax.scatter(samples_phys[:, j], samples_phys[:, i],
                               s=scatter_size, alpha=scatter_alpha,
                               c=th.scatter, edgecolors="none", zorder=2)
                if ell_log is not None:
                    ell_phys = np.exp(ell_log)
                    ax.plot(ell_phys[0], ell_phys[1],
                            color=th.ellipse_color, lw=1.4, zorder=3)
                ax.plot(mle_phys[j], mle_phys[i],
                        marker="*", markersize=15, color=th.mle,
                        markeredgecolor=th.fg, linestyle="none", zorder=5)
                for ci_kind in show_ci:
                    ci_x = _gather_ci(boot, [ci_kind], pj).get(ci_kind)
                    ci_y = _gather_ci(boot, [ci_kind], pi).get(ci_kind)
                    if not (ci_x and ci_y): continue
                    c = th.ci_colors[ci_kind]
                    for v in ci_x: ax.axvline(v, color=c, ls="--", lw=0.9, alpha=0.8)
                    for v in ci_y: ax.axhline(v, color=c, ls="--", lw=0.9, alpha=0.8)
                if log_axes:
                    ax.set_xscale("log"); ax.set_yscale("log")
                if i == len(PARAM_NAMES) - 1:
                    ax.set_xlabel(PARAM_LABELS[pj], color=th.fg)
                else:
                    plt.setp(ax.get_xticklabels(), visible=False)
                if j == 0:
                    ax.set_ylabel(PARAM_LABELS[pi], color=th.fg)
                else:
                    # plt.setp on each tick-label artist is the most
                    # robust way to hide labels — survives every
                    # downstream tick_params / autoscale call.
                    plt.setp(ax.get_yticklabels(), visible=False)

            ax.tick_params(colors=th.fg, labelsize=8)
            for sp in ax.spines.values():
                sp.set_color(th.fg)
            ax.set_facecolor(th.bg)

    # Single figure-level legend in the empty upper-right triangle.
    # Built from proxy artists so it doesn't repeat per-panel.
    from matplotlib.lines import Line2D
    legend_handles = [
        Line2D([0], [0], marker="*", color="none",
               markerfacecolor=th.mle, markeredgecolor=th.fg,
               markersize=12, linestyle="none", label="MLE"),
        Line2D([0], [0], color=th.gp_mean, lw=1.6,
               label="bootstrap 1D KDE (diagonal)"),
    ]
    if show_gaussian_ellipse:
        legend_handles.append(Line2D(
            [0], [0], color=th.ellipse_color, lw=1.4,
            label=f"{int(ellipse_level*100)}% Gaussian ellipse",
        ))
    for ci_kind in show_ci:
        legend_handles.append(Line2D(
            [0], [0], color=th.ci_colors[ci_kind], ls="--", lw=1.1,
            label=f"{ci_kind} CI",
        ))
    fig.legend(
        handles=legend_handles,
        loc="upper right",
        bbox_to_anchor=(0.975, 0.94),
        frameon=False,
        fontsize=9.5,
        labelcolor=th.fg,
        title="Legend",
        title_fontsize=10,
    )

    if title is None:
        title = (f"Bootstrap distribution of passive parameters "
                 f"— cell {boot.specimen_id}")
    fig.suptitle(title, color=th.fg, fontsize=th.title_size+2, y=0.995)
    # Sub-caption with B / kept / mode goes below the title in smaller
    # type — keeps the main title clean.
    fig.text(
        0.5, 0.965,
        f"n = {boot.n_kept} bootstrap resamples ({boot.bootstrap_mode}; "
        f"B = {boot.B_requested} requested)",
        ha="center", va="top", color=th.fg, fontsize=9, style="italic",
    )
    _apply_mpl_theme(fig, th)
    fig.subplots_adjust(left=0.10, right=0.98, top=0.93, bottom=0.08,
                        hspace=0.14, wspace=0.14)

    # ── FINAL tick-label visibility pass ────────────────────────────────
    # This block MUST run after _apply_mpl_theme and subplots_adjust.
    # On log-scale axes, mpl regenerates tick artists when tick_params is
    # called (e.g. inside _apply_mpl_theme), so any earlier
    # setp(visible=False) call gets silently undone. Running the pass last
    # — when no further mpl calls touch ticks — guarantees the labels
    # stay hidden in the saved PDF/PNG.
    n_par = len(PARAM_NAMES)
    for i, pi in enumerate(PARAM_NAMES):
        for j, pj in enumerate(PARAM_NAMES):
            if j > i:
                continue
            ax_ij = axes[f"{pi}_{pj}"]
            # Hide x-tick labels on every row except the bottom row.
            if i != n_par - 1:
                plt.setp(ax_ij.get_xticklabels(), visible=False)
                plt.setp(ax_ij.get_xticklabels(minor=True), visible=False)
            # Hide y-tick labels on off-diagonal panels that aren't in
            # the leftmost column. Diagonal panels already have
            # set_yticks([]) so they have nothing to hide.
            if i != j and j != 0:
                plt.setp(ax_ij.get_yticklabels(), visible=False)
                plt.setp(ax_ij.get_yticklabels(minor=True), visible=False)

    if out_dir is not None:
        out_path = _ensure_dir(out_dir)
        if save_pdf:
            fig.savefig(out_path / f"{filename_base}.pdf",
                        facecolor=th.bg, bbox_inches="tight")
        if save_png:
            fig.savefig(out_path / f"{filename_base}.png",
                        facecolor=th.bg, dpi=200, bbox_inches="tight")
        print(f"[plot_corner] saved → {out_path}/{filename_base}.[pdf|png]")
    return fig, axes


# ═══════════════════════════════════════════════════════════════════════════════
#  3.  GP-mean heatmap (v1)  →  bootstrap-RMSD interpolation heatmap (v2)
# ═══════════════════════════════════════════════════════════════════════════════

def plot_gp_heatmap_pair(
    bundle, p_x: str, p_y: str, *,
    n_grid: int = 120,
    interp_method: Literal["linear", "cubic", "nearest"] = "linear",
    cbar_label: Optional[str] = None,
    show_bootstrap: bool = True,
    bootstrap_alpha: float = 0.35, bootstrap_size: float = 8.0,
    show_ci: Sequence[str] = ("percentile",),
    show_mle: bool = True,
    log_axes: bool = True,
    theme: Union[str, Theme] = "light",
    figsize: Tuple[float, float] = (7.5, 6.0),
    title: Optional[str] = None,
    ax: Optional[Axes] = None,
) -> Tuple[Figure, Axes]:
    """Heatmap of refit RMSDs interpolated from the bootstrap cloud.

    NOTE — substitution from v1
    ---------------------------
    v1 showed the GP posterior mean μ_GP on a slice through the loss
    surface, with the third parameter pinned at its MLE. v4 bundles do
    not save the live GP, so this function instead interpolates the
    scattered (bootstrap_sample, refit_RMSD) pairs onto a regular 2D
    grid via scipy.interpolate.griddata. The third parameter is NOT
    pinned — it is marginalised over the bootstrap distribution.

    Pixels outside the convex hull of the bootstrap cloud appear as the
    colormap's "bad" colour (typically white/gray); this is honest about
    where the bootstrap provides no information.
    """
    th = _resolve_theme(theme)
    Xp, Yp, Z = _bootstrap_rmsd_slice(
        bundle, p_x, p_y, n_grid=n_grid, method=interp_method)

    if ax is None:
        fig, ax = plt.subplots(figsize=figsize, facecolor=th.bg)
    else:
        fig = ax.figure

    cmap = mpl.colormaps.get_cmap(th.heatmap_cmap).copy()
    cmap.set_bad(th.bg, alpha=0.0)
    Zm = np.ma.masked_invalid(Z)
    pcm = ax.pcolormesh(Xp, Yp, Zm, cmap=cmap, shading="auto", rasterized=True)
    cbar = fig.colorbar(pcm, ax=ax, pad=0.02)
    cbar.set_label(
        cbar_label or "Refit RMSD interpolated from bootstrap (mV)",
        color=th.fg, fontsize=th.text_size,
    )
    cbar.ax.tick_params(colors=th.fg)

    boot = bundle.bootstrap_result
    ix, iy = _NC[p_x], _NC[p_y]

    if show_bootstrap:
        ax.scatter(boot.samples[:, ix], boot.samples[:, iy],
                   s=bootstrap_size, alpha=bootstrap_alpha,
                   c=th.scatter, edgecolors="none", zorder=2,
                   label=f"bootstrap (n={boot.n_kept})")
    if show_mle:
        ax.plot(boot.mle_physical[ix], boot.mle_physical[iy],
                marker="*", markersize=18, color=th.mle,
                markeredgecolor=th.fg, linestyle="none",
                zorder=5, label="MLE")
    for ci_kind in show_ci:
        ci_x = _gather_ci(boot, [ci_kind], p_x).get(ci_kind)
        ci_y = _gather_ci(boot, [ci_kind], p_y).get(ci_kind)
        if not (ci_x and ci_y): continue
        c = th.ci_colors[ci_kind]
        ax.axvline(ci_x[0], color=c, ls="--", lw=1.1)
        ax.axvline(ci_x[1], color=c, ls="--", lw=1.1)
        ax.axhline(ci_y[0], color=c, ls="--", lw=1.1)
        ax.axhline(ci_y[1], color=c, ls="--", lw=1.1,
                   label=f"{ci_kind} CI")

    if log_axes:
        ax.set_xscale("log"); ax.set_yscale("log")
    ax.set_xlabel(PARAM_LABELS[p_x], color=th.fg)
    ax.set_ylabel(PARAM_LABELS[p_y], color=th.fg)
    if title is None:
        third = next(p for p in PARAM_NAMES if p not in (p_x, p_y))
        title = (f"Loss surface on $({p_x},\\ {p_y})$  "
                 f"— cell {boot.specimen_id}\n"
                 f"RMSD interpolated from bootstrap "
                 f"({third} marginalised)")
    ax.set_title(title, color=th.fg, fontsize=th.title_size)
    ax.legend(fontsize=7.5, loc="best", labelcolor=th.fg, frameon=True)
    _apply_mpl_theme(fig, th)
    return fig, ax


# ═══════════════════════════════════════════════════════════════════════════════
#  4.  GP-sigma heatmap (v1)  →  bootstrap-density KDE heatmap (v2)
# ═══════════════════════════════════════════════════════════════════════════════

def plot_gp_sigma_heatmap_pair(
    bundle, p_x: str, p_y: str, *,
    n_grid: int = 120,
    cbar_label: Optional[str] = None,
    show_bootstrap: bool = True,
    bootstrap_alpha: float = 0.35, bootstrap_size: float = 8.0,
    show_ci: Sequence[str] = ("percentile",),
    show_mle: bool = True,
    log_axes: bool = True,
    theme: Union[str, Theme] = "light",
    figsize: Tuple[float, float] = (7.5, 6.0),
    title: Optional[str] = None,
    ax: Optional[Axes] = None,
) -> Tuple[Figure, Axes]:
    """Heatmap of the 2D bootstrap-sample KDE in (p_x, p_y) log-space.

    NOTE — substitution from v1
    ---------------------------
    v1 showed σ_GP — the GP's posterior uncertainty about the loss
    surface. v4 cannot recompute that. The closest visualisation we can
    build from v4 data is the bootstrap-density KDE: where the
    parameter estimates concentrate after marginalising over the third.
    This is a parameter-uncertainty heatmap, NOT a loss-surface-
    uncertainty heatmap. The two were related but not identical in v1.
    """
    th = _resolve_theme(theme)
    Xp, Yp, Z = _bootstrap_density_slice(bundle, p_x, p_y, n_grid=n_grid)

    if ax is None:
        fig, ax = plt.subplots(figsize=figsize, facecolor=th.bg)
    else:
        fig = ax.figure

    pcm = ax.pcolormesh(Xp, Yp, Z, cmap=th.sigma_cmap,
                        shading="auto", rasterized=True)
    cbar = fig.colorbar(pcm, ax=ax, pad=0.02)
    cbar.set_label(
        cbar_label or "Bootstrap density (KDE in log-space)",
        color=th.fg, fontsize=th.text_size,
    )
    cbar.ax.tick_params(colors=th.fg)

    boot = bundle.bootstrap_result
    ix, iy = _NC[p_x], _NC[p_y]
    if show_bootstrap:
        ax.scatter(boot.samples[:, ix], boot.samples[:, iy],
                   s=bootstrap_size, alpha=bootstrap_alpha,
                   c=th.scatter, edgecolors="none", zorder=2,
                   label=f"bootstrap (n={boot.n_kept})")
    if show_mle:
        ax.plot(boot.mle_physical[ix], boot.mle_physical[iy],
                marker="*", markersize=18, color=th.mle,
                markeredgecolor=th.fg, linestyle="none",
                zorder=5, label="MLE")
    for ci_kind in show_ci:
        ci_x = _gather_ci(boot, [ci_kind], p_x).get(ci_kind)
        ci_y = _gather_ci(boot, [ci_kind], p_y).get(ci_kind)
        if not (ci_x and ci_y): continue
        c = th.ci_colors[ci_kind]
        ax.axvline(ci_x[0], color=c, ls="--", lw=1.1)
        ax.axvline(ci_x[1], color=c, ls="--", lw=1.1)
        ax.axhline(ci_y[0], color=c, ls="--", lw=1.1)
        ax.axhline(ci_y[1], color=c, ls="--", lw=1.1,
                   label=f"{ci_kind} CI")

    if log_axes:
        ax.set_xscale("log"); ax.set_yscale("log")
    ax.set_xlabel(PARAM_LABELS[p_x], color=th.fg)
    ax.set_ylabel(PARAM_LABELS[p_y], color=th.fg)
    if title is None:
        third = next(p for p in PARAM_NAMES if p not in (p_x, p_y))
        title = (f"Bootstrap parameter density on $({p_x},\\ {p_y})$  "
                 f"— cell {boot.specimen_id}\n"
                 f"2D Gaussian KDE in log-space ({third} marginalised)")
    ax.set_title(title, color=th.fg, fontsize=th.title_size)
    ax.legend(fontsize=7.5, loc="best", labelcolor=th.fg, frameon=True)
    _apply_mpl_theme(fig, th)
    return fig, ax


