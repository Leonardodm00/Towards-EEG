# ═══════════════════════════════════════════════════════════════════════════════

def load_replot_bundle(
    path,
    *,
    load_pulse_pool: bool = True,
    verbose: bool = False,
) -> ReplotBundle:
    """Load a bundle written by ``save_replot_bundle``.

    Parameters
    ----------
    path
        Either the path to ``core.pkl`` directly, OR the path to the
        ``replot/`` directory (in which case ``core.pkl`` is appended).
    load_pulse_pool
        If True and a sibling ``pulse_pool.pkl`` exists, it is loaded and
        attached to ``bundle.pulse_pool``.  Failures here are warned and
        leave ``pulse_pool = None`` — they never abort the load.

    Notes
    -----
    For unpickling to succeed in a fresh session, the dataclasses
    ``BootstrapCIResult``, ``GpDiagnosticPerParameter``, and
    ``GpDiagnosticResult`` from ``phase3_bootstrap_v3`` must be defined
    in the current Python session.  The lite classes used internally
    (``_TrainBundleLite``, ``_SkoptDimLite``, ``ReplotBundle``) come from
    this module.
    """
    p = Path(path)
    core_path = p / "core.pkl" if p.is_dir() else p
    if not core_path.exists():
        raise FileNotFoundError(f"core.pkl not found at {core_path}")

    # Standard unpickle first. If it fails because a class can't be
    # resolved in this session, fall back to the resilient unpickler
    # defined later in the file (substitutes _AttrBag for missing
    # classes). Note: _ResilientUnpickler is defined below in this
    # module; by the time load_replot_bundle is CALLED, that definition
    # has been executed, so the lookup at call-time succeeds.
    try:
        with open(core_path, "rb") as f:
            bundle = pickle.load(f)
    except (AttributeError, ModuleNotFoundError, ImportError) as e:
        if verbose:
            print(f"[load_replot_bundle] standard pickle failed "
                  f"({type(e).__name__}: {e}); using resilient fallback.")
        missing: list = []
        with open(core_path, "rb") as f:
            bundle = _ResilientUnpickler(f, missing).load()
        if verbose and missing:
            unique = sorted(set(missing))
            print(f"[load_replot_bundle] {len(unique)} class(es) replaced "
                  f"by _AttrBag during resilient load:")
            for mod_name, cls_name in unique:
                print(f"   • {mod_name}.{cls_name}")

    if not isinstance(bundle, ReplotBundle):
        warnings.warn(
            f"[load_replot_bundle] loaded object is "
            f"{type(bundle).__name__}, not ReplotBundle; replot functions "
            f"may fail.")

    if load_pulse_pool:
        pp_path = core_path.parent / "pulse_pool.pkl"
        if pp_path.exists():
            try:
                with open(pp_path, "rb") as f:
                    bundle.pulse_pool = pickle.load(f)
                if verbose:
                    print(f"[load_replot_bundle] attached pulse_pool "
                          f"({len(bundle.pulse_pool)} pulses)")
            except Exception as e:
                warnings.warn(
                    f"[load_replot_bundle] pulse_pool.pkl present but "
                    f"could not be loaded ({type(e).__name__}: {e}); "
                    f"continuing without it.")
                bundle.pulse_pool = None
        elif verbose:
            print("[load_replot_bundle] no pulse_pool.pkl; nonparametric "
                  "trace panel will be skipped.")

    return bundle


# ═══════════════════════════════════════════════════════════════════════════════
#  replot_trace_diagnostic  —  updated to read the flat bundle
#  (the other three replot_* functions are unchanged; keep them from v3)
# ═══════════════════════════════════════════════════════════════════════════════

def replot_trace_diagnostic(
    bundle: ReplotBundle,
    *,
    n_examples: int = 3,
    bundle_index: int = 0,
    figsize: Tuple[float, float] = (14.0, 5.0),
    noise_mode: str = "iid",
    sigma_override: Optional[float] = None,
    rho_override:   Optional[float] = None,
    seed: int = 42,
    n_pulses_per_replicate: int = 9,
    parametric_kwargs:    Optional[Dict[str, Any]] = None,
    nonparametric_kwargs: Optional[Dict[str, Any]] = None,
    show_suptitle: bool = True,
) -> Tuple[Figure, np.ndarray]:
    """Redraw the side-by-side trace diagnostic from a v4 bundle.

    The parametric panel reuses ``bundle.mle_traces_by_bundle`` (precomputed
    at save time) and synthesises fresh noise via the Phase-3 helper
    ``_generate_noise`` (located in the running session).  The nonparametric
    panel resamples from ``bundle.pulse_pool`` if it was loaded; otherwise
    only the parametric panel is drawn.
    """
    rng = np.random.default_rng(int(seed))

    if not (0 <= bundle_index < len(bundle.train_bundles)):
        raise IndexError(
            f"bundle_index {bundle_index} out of range "
            f"(have {len(bundle.train_bundles)} training bundles).")
    if bundle_index not in bundle.mle_traces_by_bundle:
        raise KeyError(
            f"MLE trace for bundle_index={bundle_index} was not saved.")

    b0    = bundle.train_bundles[bundle_index]
    sim_v = bundle.mle_traces_by_bundle[bundle_index]
    t_ms  = b0.t * 1e3

    # σ / ρ for the parametric panel: prefer overrides, then saved values
    sigma_res = sigma_override
    if sigma_res is None or not np.isfinite(sigma_res):
        sigma_res = bundle.residuals_sigma_mV
        if not np.isfinite(sigma_res):
            sigma_res = bundle.noise_sigma_mV
        if not np.isfinite(sigma_res):
            sigma_res = 0.0
    rho_res = rho_override
    if rho_res is None or not np.isfinite(rho_res):
        rho_res = bundle.residuals_rho_lag1
        if not np.isfinite(rho_res):
            rho_res = 0.0

    pulse_pool = bundle.pulse_pool or []
    has_np = len(pulse_pool) > 0
    n_panels = 2 if has_np else 1
    fig, axes = plt.subplots(1, n_panels, figsize=figsize)
    if n_panels == 1:
        axes = np.array([axes])

    p_kw  = dict(lw=0.7, alpha=0.6);  p_kw.update(parametric_kwargs    or {})
    np_kw = dict(lw=0.8, alpha=0.7);  np_kw.update(nonparametric_kwargs or {})

    # ── LEFT: parametric ─────────────────────────────────────────────────
    ax_p = axes[0]
    ax_p.plot(t_ms, b0.v_mV, color="k",        lw=1.2, alpha=0.8,
              label="experiment (averaged)")
    ax_p.plot(t_ms, sim_v,   color="tab:red",  lw=1.0, alpha=0.9,
              label="MLE model")

    train_t0_s, train_t1_s = (np.asarray(bundle.train_window_ms) * 1e-3).tolist()
    post_mask = (b0.t >= train_t0_s) & (b0.t <= train_t1_s)
    pre_mask  = (b0.t <  train_t0_s)
    bd = float(b0.v_mV[pre_mask].mean()) if pre_mask.any() else 0.0
    bs = float(sim_v[pre_mask].mean())   if pre_mask.any() else 0.0

    colors_syn = plt.cm.Set2(np.linspace(0, 1, max(n_examples, 3)))
    generate_noise = _locate_global("_generate_noise")
    for k in range(n_examples):
        n_post = int(post_mask.sum())
        noise = generate_noise(
            n=n_post, sigma=sigma_res, rho=rho_res,
            mode=noise_mode, block_length=None,
            empirical_residuals=None, rng=rng,
        )
        syn_v = b0.v_mV.copy()
        syn_v[post_mask] = sim_v[post_mask] - bs + bd + noise
        ax_p.plot(t_ms, syn_v, color=colors_syn[k],
                  label=(f"synthetic #{k+1}" if k < 3 else None), **p_kw)

    ax_p.set_xlabel("Time (ms)")
    ax_p.set_ylabel("V (mV)")
    ax_p.set_title(
        f"Parametric bootstrap traces\n"
        f"(σ_res={sigma_res:.4f} mV, mode={noise_mode})",
        fontsize=10)
    ax_p.legend(loc="best", fontsize=8)

    # ── RIGHT: nonparametric (only if pulse_pool was loaded) ─────────────
    if has_np:
        ax_n = axes[1]
        ax_n.plot(t_ms, b0.v_mV, color="k", lw=1.2, alpha=0.8,
                  label="original average")
        for k in range(n_examples):
            idx = rng.integers(0, len(pulse_pool), size=n_pulses_per_replicate)
            drawn = [pulse_pool[int(j)] for j in idx]
            target_pol = b0.polarity
            same_pol = [w for w in drawn if w["polarity"] == target_pol]
            if not same_pol:
                same_pol = drawn
            v_stack  = np.stack([w["v"] for w in same_pol])
            avg_v    = v_stack.mean(axis=0)
            t_ref_ms = same_pol[0]["t"] * 1e3
            ax_n.plot(t_ref_ms, avg_v, color=colors_syn[k],
                      label=(f"resampled avg #{k+1} (n={len(same_pol)})"
                             if k < 3 else None),
                      **np_kw)
        ax_n.set_xlabel("Time (ms)")
        ax_n.set_ylabel("V (mV)")
        ax_n.set_title(
            f"Nonparametric bootstrap traces\n"
            f"(pool={len(pulse_pool)} pulses, draw={n_pulses_per_replicate})",
            fontsize=10)
        ax_n.legend(loc="best", fontsize=8)

    if show_suptitle:
        fig.suptitle(
            f"Bootstrap trace diagnostic — cell {bundle.specimen_id}",
            fontsize=11, fontweight="bold")
        fig.tight_layout(rect=[0, 0, 1, 0.95])
    else:
        fig.tight_layout()
    return fig, axes

