def plot_bootstrap_histograms(
    r: BootstrapCIResult,
    out_dir,
) -> List[Path]:
    """One density-histogram per parameter with MLE + percentile/BCa/normal CIs."""
    out_dir = Path(out_dir)
    saved = []

    # Collect available CI dicts
    ci_sources = {
        "percentile": r.ci_percentile,
        "bca":        r.ci_bca,
        "normal":     r.ci_normal,
    }

    for i, p in enumerate(PARAM_NAMES):
        s = r.samples[:, i]
        fig, ax = plt.subplots(figsize=(7.0, 4.6))
        ax.hist(s, bins=40, density=True, color="tab:blue",
                alpha=0.45, edgecolor="white",
                label=f"bootstrap (n={r.n_kept})")
        ax.axvline(r.mle_physical[i], color="k", lw=1.4,
                   label=f"MLE = {r.mle_physical[i]:.4g}")

        for ci_name, ci_table in ci_sources.items():
            lo, hi = ci_table[p]
            sty = CI_STYLES[ci_name]
            if np.isfinite(lo) and np.isfinite(hi):
                lab = sty["label_fmt"].format(lo=lo, hi=hi)
                ax.axvline(lo, color=sty["color"], lw=sty["lw"],
                           linestyle=sty["ls"])
                ax.axvline(hi, color=sty["color"], lw=sty["lw"],
                           linestyle=sty["ls"], label=lab)

        ax.set_xscale("log")
        ax.set_xlabel(PARAM_LABELS[p])
        ax.set_ylabel("density")
        mode_lbl = (f"noise={r.noise_mode}" if r.bootstrap_mode == "parametric"
                    else f"nonparam (n_pulses={r.n_pulses_per_replicate})")
        ax.set_title(
            f"Bootstrap distribution — {p}  |  cell {r.specimen_id}\n"
            f"B={r.B_requested} (n_kept={r.n_kept}), α={r.threshold_alpha}, "
            f"fit_mode={r.fit_mode}, {mode_lbl}",
            fontsize=9,
        )
        ax.legend(loc="best", fontsize=7.5)
        fig.tight_layout()
        path = out_dir / f"histogram_{p}.png"
        fig.savefig(path, dpi=140)
        saved.append(path)
        plt.close(fig)
    return saved


