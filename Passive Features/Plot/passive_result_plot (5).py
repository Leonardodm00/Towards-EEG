def _validate_picklable(obj: Any, label: str) -> int:
    """Pickle ``obj`` to bytes; raise a clear error if it fails.

    Returns the size in bytes of the pickled representation.  Used as an
    upfront check so unpicklable fields are localised before the final
    dump, instead of leaving a partly-written file.
    """
    try:
        buf = pickle.dumps(obj, protocol=pickle.HIGHEST_PROTOCOL)
    except Exception as e:
        raise RuntimeError(
            f"Field {label!r} is not picklable "
            f"({type(e).__name__}: {e}). "
            f"This is the field that would have corrupted the bundle."
        ) from e
    return len(buf)


def _atomic_pickle_dump(
    obj: Any, target: Path,
    protocol: int = pickle.HIGHEST_PROTOCOL,
) -> int:
    """Pickle ``obj`` to ``target`` atomically.

    Writes to a same-directory tempfile, ``fsync``s, then ``os.replace``s
    into place.  On both POSIX and Windows this is atomic at the
    filesystem level.  On failure, the tempfile is cleaned up and the
    pre-existing ``target`` (if any) is untouched.

    Returns the final file size in bytes.
    """
    target = Path(target)
    target.parent.mkdir(parents=True, exist_ok=True)

    fd, tmp_name = tempfile.mkstemp(
        prefix=target.name + ".", suffix=".tmp", dir=str(target.parent))
    tmp_path = Path(tmp_name)
    try:
        with os.fdopen(fd, "wb") as f:
            pickle.dump(obj, f, protocol=protocol)
            f.flush()
            os.fsync(f.fileno())
        os.replace(tmp_path, target)
    except Exception:
        try:
            if tmp_path.exists():
                tmp_path.unlink()
        except Exception:
            pass
        raise
    return target.stat().st_size


def _atomic_json_dump(obj: Any, target: Path) -> int:
    """JSON-dump ``obj`` atomically.  Same scheme as ``_atomic_pickle_dump``."""
    target = Path(target)
    target.parent.mkdir(parents=True, exist_ok=True)
    fd, tmp_name = tempfile.mkstemp(
        prefix=target.name + ".", suffix=".tmp", dir=str(target.parent))
    tmp_path = Path(tmp_name)
    try:
        with os.fdopen(fd, "w") as f:
            json.dump(obj, f, indent=2, default=str)
            f.flush()
            os.fsync(f.fileno())
        os.replace(tmp_path, target)
    except Exception:
        try:
            if tmp_path.exists():
                tmp_path.unlink()
        except Exception:
            pass
        raise
    return target.stat().st_size


