    # Phase 1 — discover and load cells
    # =========================================================================
    candidates = list_human_cells_with_morphology(
        layer="6", dendrite_type="spiny", n_cells=1)
    cells_data = load_complete_cells(candidates, n_avg_groups=1)

    F_layer6_spiny = 1.9
    opt_inputs = [prepare_optimiser_inputs(cd, fit_target="hyp")
                  for cd in cells_data]

    # =========================================================================
    # Phase 2 — Bayesian optimisation (parallelisable)
    # =========================================================================
    df, results = fit_cells(
        cells_data, opt_inputs,
        F=F_layer6_spiny,
        n_workers=1,          # ← set to number of available cores on HPC
        n_calls=150, n_initial=20,
    )
    print(df)

    # Diagnostic plots
    for r, cd, oi in zip(results, cells_data, opt_inputs):
        if r.validation_status != "failed":
            pc = build_neuron_model(cd.swc_path, F=F_layer6_spiny)
            plot_fit_diagnostic(pc, cd, oi, r)

    # Population aggregation
    pop = aggregate_population(df, accept_statuses=("good", "to_refine"))
    print(pop)

    # Save Phase 2 results
    df.to_parquet("phase2_results_layer6_spiny.parquet")

    # =========================================================================
    # Phase 3 — profile-likelihood post-processing
    # =========================================================================
    # Step A: rebuild NEURON cells in the parent process.
    rebuild_neuron_cells_for_phase3(results, cells_data, F=F_layer6_spiny)

    # Step B: run Phase 3 for each qualifying cell.
    PHASE3_ROOT   = input_dir
    PHASE3_KWARGS = dict(
        n_grid=80,
        inner_grid_per_axis=30,
        window_log_halfwidth=None,
        threshold_alpha=0.95,
        # N_eff from membrane time constant τ_m = Cm × Rm (recommended).
        # n_tau_factor=2 (Nyquist): ~2 independent observations per τ_m.
        # Increase to widen CIs (conservative); decrease to tighten them.
        # Use N_eff_override=<float> to force a specific value instead.
        n_eff_mode="tau",
        n_tau_factor=1.0,
        n_validation_per_boundary=5,
        validation_ball_logradius=0.01,
        trust_abs_mv=0.05,
        trust_zscore=3.0,
        save_plots=True,
        save_pickle=True,
    )

    pl_results = []
    for i, fr in enumerate(results):
        if fr.neuron_cell is None:    # failed or rebuild skipped
            continue
        try:
            pl = profile_likelihood_for_cell(
                fit_result=fr,
                root_dir=PHASE3_ROOT,
                seed=i,
                verbose=True,
                **PHASE3_KWARGS,
            )
            pl_results.append(pl)
        except Exception as exc:
            print(f"[Phase 3] specimen {fr.specimen_id} failed: {exc}")

    # Step C: population CI summary
    import pandas as pd

    ci_rows = []
    for pl in pl_results:
        for pname, prof in pl.profiles.items():
            ci_rows.append(dict(
                specimen_id     = pl.specimen_id,
                tau_m_ms        = pl.tau_m_ms,     # Cm × Rm at MLE [ms]
                N_eff           = pl.N_eff_used,   # effective sample size used
                N_eff_method    = pl.N_eff_method, # "tau" | "lag1" | ...
                parameter       = pname,
                mle             = prof.mle_value,
                ci_lower        = prof.ci_lower,
                ci_upper        = prof.ci_upper,
                identifiability = prof.identifiability,
                gp_ok           = prof.gp_validation_ok,
            ))

    ci_df = pd.DataFrame(ci_rows)
    print(ci_df.to_string(index=False))
    ci_df.to_csv("phase3_ci_summary_layer6_spiny.csv", index=False)

    # Flag any GP-trust warnings
    suspicious = ci_df[~ci_df["gp_ok"]]
    if not suspicious.empty:
        print("\nParameters with GP-trust warnings (consider GP retraining):")
        print(suspicious[["specimen_id", "parameter",
                           "ci_lower", "ci_upper", "identifiability"]]
              .to_string(index=False))