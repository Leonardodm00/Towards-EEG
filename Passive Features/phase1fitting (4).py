"""
phase3_profile_likelihood.py
============================
Phase 3 of the human-cortex passive-property pipeline: profile-likelihood
post-processing on top of the Phase-2 Gaussian-process surrogate.

This script consumes the live objects produced by ``fit_one_cell`` in
Phase 2 (already in scope) and produces, for one cell:

  1. A profile-likelihood curve for each parameter (Cm, Rm, Ra), computed
     entirely by *querying* the Phase-2 GP surrogate — no NEURON runs,
     no refit. For each pinned value of one parameter, the inner
     "minimise over the other two parameters" step is a dense vectorised
     batch query of the GP (no nested optimiser).
  2. A χ²-based 1-D pointwise confidence interval per parameter,
     computed with the per-cell observation noise σ stored in
     ``fit_result.noise_sigma_mV``.  The effective sample size N_eff used
     in the χ² threshold is estimated from the membrane time constant
     τ_m = Cm × Rm (see Section 4b below), which correctly accounts for
     the strong temporal autocorrelation of a passive voltage transient.
  3. A small-ball validation set around each CI boundary, where the
     *real* NEURON simulator is evaluated (typically 3–5 calls per
     bound, so ≲ 30 NEURON runs per cell). The real RMSD is compared
     against the GP prediction and a per-parameter "GP trustworthy"
     boolean is set using a combined absolute + z-score criterion
     (Q5.3 option (d)).
  4. One PNG plot per parameter (three figures total), saved into a
     per-cell folder under the user-chosen root directory, together
     with a pickle of the full ``ProfileLikelihoodResult`` for later
     analysis.

Assumed already in scope (produced by Phase 2):
    fit_result   : PassiveFitResult  -- with the patched live-object fields
                                        (gp_result, neuron_cell, opt_inputs)
    np, pd, plt, ...                  -- standard scientific stack

Phase 2 helpers reused (not redefined):
    _build_loss_function   -- to recompute RMSD on the real simulator
    PassiveCell.simulate    -- via the loss function

--- N_eff estimation from τ_m (key design decision) ---------------------------

The χ² CI threshold requires an effective sample size N_eff.  Using the
raw sample count N_raw (≈ 5 000 for a 100 ms window at 50 kHz) produces
a threshold indistinguishable from the MLE RMSD — even for identifiable
parameters.  This is not a failure of identifiability; it is a failure of
the independence assumption in the Wilks theorem.

A passive voltage transient decays with time constant τ_m = Cm × Rm.
Two adjacent time samples at 50 kHz (Δt = 0.02 ms) are separated by
Δt/τ_m ≈ 0.0003 — they carry almost identical information about the
model parameters.  The number of *statistically independent* observations
in a fitting window of length T_window is therefore proportional to
T_window/τ_m, not to T_window × f_sample.

The default estimator implemented here is:

    N_eff = max(n_tau_factor × T_window / τ_m ,  N_EFF_MIN)

where:
  τ_m          = Cm_MLE × Rm_MLE × 1e-3   [ms]
  T_window     = train_window_ms[1] − train_window_ms[0]   [ms]
  n_tau_factor = 2   (Nyquist: ~2 independent estimates per τ_m)
  N_EFF_MIN    = 1   (floor to avoid degenerate thresholds)

For the example cell (Cm=0.996 µF/cm², Rm=63994 Ω·cm²  →  τ_m=63.7 ms,
T_window=99 ms):
    N_eff = 2 × 99/63.7 ≈ 3.1
    threshold ≈ RMSD_MLE × 1.13   (13 % headroom — enough for real CIs)

Three alternative modes are available via the ``n_eff_mode`` parameter:
  "tau"      (default): formula above — physically motivated
  "lag1"     : legacy lag-1 autocorrelation correction on baseline noise
  "raw"      : use N_raw without correction (not recommended — too tight)
  "override" : use the explicit ``N_eff_override`` value

References
----------
Raue A et al. (2009) Bioinformatics 25:1923–1929.
Wieland F-G et al. (2021) On structural and practical identifiability.
Statistical Invariance report (project upload), Section 5.
"""

