# %% Cell 4a — Legacy lag-1 N_eff correction ===================================
def _effective_N_lag1(N: int, rho: float, apply_correction: bool) -> float:
    """N_eff from the lag-1 AR(1) formula.

    N_eff = N × (1 − ρ) / (1 + ρ)

    Appropriate when the pre-stimulus baseline autocorrelation is a good
    proxy for the signal autocorrelation.  For smooth decaying transients
    this under-estimates correlation; prefer n_eff_mode='tau'.
    """
    if not apply_correction:
        return float(N)
    if not np.isfinite(rho):
        return float(N)
    rho_c = float(np.clip(rho, -0.999, 0.999))
    return float(N) * (1.0 - rho_c) / (1.0 + rho_c)


# %% Cell 4b — τ_m-based N_eff (primary method) ================================
def _tau_m_from_fit(fit_result) -> float:
    """Membrane time constant from MLE parameters, in milliseconds.

    τ_m = Cm [µF/cm²] × Rm [Ω·cm²]
        = 10⁻⁶ F·Ω = 10⁻⁶ s   →  × 10⁻³  gives ms.
    """
    return float(fit_result.cm_uF_per_cm2) * float(fit_result.rm_Ohm_cm2) * 1e-3


def _effective_N_tau(
    tau_m_ms: float,
    window_ms: float,
    n_tau_factor: float = DEFAULT_N_TAU_FACTOR,
    n_eff_min: float    = DEFAULT_N_EFF_MIN,
) -> float:
    """N_eff estimated from the membrane time constant τ_m.

    Physical justification
    ----------------------
    A passive transient decays with characteristic time τ_m.  Adjacent
    samples at Δt ≪ τ_m are nearly identical copies and contribute almost
    no independent information.  The number of truly independent epochs
    in a window of length T_window is ≈ T_window / τ_m.

    A multiplicative factor n_tau_factor ≈ 2 (Nyquist) accounts for the
    two-slope structure of the transient (fast capacitive charging at
    early times and slow exponential decay at late times) — loosely
    equivalent to the bandwidth–time product of a first-order low-pass
    filter with cut-off 1/(2π τ_m).

    Formula
    -------
        N_eff = max( n_tau_factor × T_window / τ_m,  n_eff_min )

    Worked examples  (n_tau_factor=2, T_window=99 ms)
    --------------------------------------------------
    τ_m =  64 ms  →  N_eff ≈  3.1   (typical L6 spiny in this dataset)
    τ_m =  20 ms  →  N_eff ≈  9.9
    τ_m =  10 ms  →  N_eff ≈ 19.8
    τ_m = 200 ms  →  N_eff =  1.0   (floor)
    """
    if not (np.isfinite(tau_m_ms) and tau_m_ms > 0):
        warnings.warn(
            f"[phase3] τ_m = {tau_m_ms:.3f} ms is not a positive finite "
            f"number.  Falling back to n_eff_min = {n_eff_min}."
        )
        return float(n_eff_min)
    return float(max(n_tau_factor * window_ms / tau_m_ms, n_eff_min))


# %% Cell 4c — χ²/RMSD threshold ==============================================
def rmsd_threshold_constant(
    rmsd_mle: float,
    sigma_mV: float,
    N_eff: float,
    chi2_delta: float,
) -> float:
    """CI threshold in RMSD units.

    χ²(θ) = N·RMSD(θ)²/σ² ≤ χ²(θ̂) + Δ_α
    ⟹  RMSD(θ) ≤ √( RMSD(θ̂)² + σ²·Δ_α / N_eff )
    """
    if not (np.isfinite(rmsd_mle) and np.isfinite(sigma_mV) and N_eff > 0):
        return float("nan")
    return float(np.sqrt(rmsd_mle**2 + sigma_mV**2 * chi2_delta / N_eff))


# %% Cell 5 — Grid helpers =====================================================
def _build_log_grid(
    fit_result,
    opt_inputs,
    param_name: str,
    n_grid: int,
    window_log_halfwidth: Optional[float],
) -> np.ndarray:
    """Log-space sweep grid for one parameter.

    None → full Phase-1 search-space bounds.
    Float → ±half-width in log-space around the MLE.
    """
    name_to_dim = {d.name: d for d in opt_inputs.skopt_dimensions}
    dim = name_to_dim[param_name]
    lo_full, hi_full = float(dim.low), float(dim.high)

    mle_phys = {"Cm": fit_result.cm_uF_per_cm2,
                "Rm": fit_result.rm_Ohm_cm2,
                "Ra": fit_result.ra_Ohm_cm}[param_name]
    mle_log = float(np.log(mle_phys))

    if window_log_halfwidth is None:
        lo, hi = lo_full, hi_full
    else:
        lo = max(lo_full, mle_log - float(window_log_halfwidth))
        hi = min(hi_full, mle_log + float(window_log_halfwidth))
        if hi - lo < 1e-6:
            lo, hi = lo_full, hi_full

    return np.linspace(lo, hi, int(n_grid))


def _other_axes_log_grid(
    opt_inputs,
    pinned_name: str,
    inner_grid_per_axis: int,
) -> Tuple[np.ndarray, np.ndarray, Tuple[str, str]]:
    """Dense grid over the two non-pinned parameters in log-space."""
    other_names = tuple(n for n in PARAM_NAMES if n != pinned_name)
    name_to_dim = {d.name: d for d in opt_inputs.skopt_dimensions}
    bounds = [(float(name_to_dim[n].low), float(name_to_dim[n].high))
              for n in other_names]
    axA = np.linspace(bounds[0][0], bounds[0][1], inner_grid_per_axis)
    axB = np.linspace(bounds[1][0], bounds[1][1], inner_grid_per_axis)
    A, B = np.meshgrid(axA, axB, indexing="ij")
    return A.ravel(), B.ravel(), other_names


# %% Cell 6 — GP profile query =================================================
def _profile_one_parameter_gp(
    *,
    gp_result,
    opt_inputs,
    param_name: str,
    grid_log: np.ndarray,
    inner_grid_per_axis: int,
) -> Dict[str, np.ndarray]:
    """Compute the GP-surrogate profile for one parameter.

    Builds the full (n_grid × M²) × 3 candidate matrix in a single
    vectorised gp.predict() call.  No scipy optimiser, no NEURON, no GP
    refit — purely querying the already-fitted surrogate from Phase 2.
    """
    gp    = gp_result.models[-1]
    space = gp_result.space
    n_grid = len(grid_log)
    M2     = int(inner_grid_per_axis) ** 2

    other_A_log, other_B_log, (name_A, name_B) = _other_axes_log_grid(
        opt_inputs, param_name, inner_grid_per_axis,
    )
    name_to_col = {"Cm": 0, "Rm": 1, "Ra": 2}
    pc = name_to_col[param_name]
    cA = name_to_col[name_A]
    cB = name_to_col[name_B]

    cands = np.empty((n_grid * M2, 3), dtype=float)
    for i, pv in enumerate(grid_log):
        sl = slice(i * M2, (i + 1) * M2)
        cands[sl, pc] = pv
        cands[sl, cA] = other_A_log
        cands[sl, cB] = other_B_log

    # Phase-1 space IS log-space, so cands is already in "original space".
    mu_flat, sig_flat = gp.predict(space.transform(cands.tolist()), return_std=True)
    mu  = mu_flat.reshape(n_grid, M2)
    sig = sig_flat.reshape(n_grid, M2)

    idx  = np.argmin(mu, axis=1)
    rows = np.arange(n_grid)
    return {
        "profile_mean_rmsd":       mu[rows, idx],
        "profile_std_rmsd":        sig[rows, idx],
        "inner_argmin_others_log": np.column_stack([other_A_log[idx],
                                                    other_B_log[idx]]),
        "other_names":             (name_A, name_B),
    }


# %% Cell 7 — Threshold crossings & identifiability ===========================
def _find_threshold_crossings(
    grid_log: np.ndarray,
    profile_mean: np.ndarray,
    threshold: float,
    mle_log: float,
) -> Tuple[float, float]:
    """Lower/upper log-space crossings via linear interpolation."""
    lo_list, hi_list = [], []
    for i in range(len(grid_log) - 1):
        y0, y1 = profile_mean[i], profile_mean[i + 1]
        if not (np.isfinite(y0) and np.isfinite(y1)):
            continue
        if (y0 - threshold) * (y1 - threshold) > 0:
            continue
        frac  = 0.0 if y0 == y1 else (threshold - y0) / (y1 - y0)
        xc    = grid_log[i] + frac * (grid_log[i + 1] - grid_log[i])
        (lo_list if xc < mle_log else hi_list).append(xc)

    return (max(lo_list) if lo_list else float("nan"),
            min(hi_list) if hi_list else float("nan"))


def _classify_identifiability(
    ci_lo: float, ci_hi: float,
    grid_log: np.ndarray, profile_mean: np.ndarray,
    threshold: float,
) -> str:
    """Raue 2009 three-way classification."""
    has_lo, has_hi = np.isfinite(ci_lo), np.isfinite(ci_hi)
    if has_lo and has_hi:
        return "identifiable"
    if not np.isfinite(np.nanmax(profile_mean)) or np.nanmax(profile_mean) < threshold:
        return "structurally_non_identifiable"
    if has_lo:
        return "practically_non_identifiable_above"
    if has_hi:
        return "practically_non_identifiable_below"
    return "structurally_non_identifiable"


# %% Cell 8 — Real-NEURON validation at CI bounds ==============================
def _validate_at_bound(
    *,
    bound_log: float,
    pinned_name: str,
    other_names: Tuple[str, str],
    other_argmin_log: np.ndarray,
    n_validation: int,
    ball_log_radius: float,
    loss_fn,
    gp_result,
    rng: np.random.Generator,
    opt_inputs,
) -> List[Tuple[float, float, float, float]]:
    """Sample a small Gaussian ball around the CI boundary and call NEURON."""
    if not np.isfinite(bound_log):
        return []

    nc = {"Cm": 0, "Rm": 1, "Ra": 2}
    pc = nc[pinned_name]; cA = nc[other_names[0]]; cB = nc[other_names[1]]

    name_to_dim = {d.name: d for d in opt_inputs.skopt_dimensions}
    lows  = np.array([float(name_to_dim[n].low)  for n in PARAM_NAMES])
    highs = np.array([float(name_to_dim[n].high) for n in PARAM_NAMES])

    centre = np.empty(3)
    centre[pc] = bound_log
    centre[cA] = float(other_argmin_log[0])
    centre[cB] = float(other_argmin_log[1])

    samples    = np.empty((int(n_validation), 3))
    samples[0] = centre
    if n_validation > 1:
        samples[1:] = centre + rng.normal(0.0, ball_log_radius,
                                          size=(n_validation - 1, 3))
    samples = np.clip(samples, lows, highs)

    gp = gp_result.models[-1]; space = gp_result.space
    mu_p, sig_p = gp.predict(space.transform(samples.tolist()), return_std=True)

    out = []
    bound_phys = float(np.exp(bound_log))
    for k, (s, mu_s, sig_s) in enumerate(zip(samples, mu_p, sig_p)):
        try:
            rmsd_real = float(loss_fn(s[0], s[1], s[2]))
        except Exception as e:
            warnings.warn(f"NEURON validation crashed at {s}: {e}")
            rmsd_real = float("nan")
        theta_phys = bound_phys if k == 0 else float(np.exp(s[pc]))
        out.append((theta_phys, rmsd_real, float(mu_s), float(sig_s)))
    return out


def _gp_trust_flag(
    pts: List[Tuple[float, float, float, float]],
    abs_mv: float,
    z_thresh: float,
) -> bool:
    """True iff every point passes both the absolute and z-score checks."""
    if not pts:
        return False
    for (_, rmsd_real, mu, sigma) in pts:
        if not np.isfinite(rmsd_real):
            return False
        d = abs(rmsd_real - mu)
        if d > abs_mv:
            return False
        if sigma > 1e-9 and d > z_thresh * sigma:
            return False
    return True


# %% Cell 9 — Top-level driver =================================================
def profile_likelihood_for_cell(
    *,
    fit_result,
    root_dir: os.PathLike | str,
    # Profile sweep
    n_grid: int = DEFAULT_N_GRID,
    inner_grid_per_axis: int = DEFAULT_INNER_GRID_PER_AXIS,
    window_log_halfwidth: Optional[float] = None,
    threshold_alpha: float = DEFAULT_THRESHOLD_ALPHA,
    # N_eff estimation
    n_eff_mode: str          = DEFAULT_N_EFF_MODE,
    n_tau_factor: float      = DEFAULT_N_TAU_FACTOR,
    N_eff_override: Optional[float] = None,
    # Boundary validation
    n_validation_per_boundary: int   = DEFAULT_N_VALIDATION_PER_BOUNDARY,
    validation_ball_logradius: float = DEFAULT_VALIDATION_BALL_LOGRADIUS,
    trust_abs_mv: float = DEFAULT_TRUST_ABS_MV,
    trust_zscore: float = DEFAULT_TRUST_ZSCORE,
    # I/O
    save_plots: bool  = True,
    save_pickle: bool = True,
    seed: int         = 0,
    verbose: bool     = True,
) -> ProfileLikelihoodResult:
    """Run Phase-3 post-processing for one cell.

    Parameters
    ----------
    fit_result
        ``PassiveFitResult`` with live-object fields populated.
    root_dir
        Root output directory; ``cell_<specimen_id>/`` is created inside.
    n_grid
        Profile resolution: number of pinned-parameter values per parameter.
    inner_grid_per_axis
        GP-query resolution for the inner minimisation. At each pinned value,
        the other two parameters are sampled at inner_grid_per_axis²  points
        and the GP is queried once on the full batch.
    window_log_halfwidth
        None → full Phase-1 search-space sweep.
        Positive float → ±half-width in log-space around the MLE.
    threshold_alpha
        Pointwise CI level, default 0.95.
    n_eff_mode
        How N_eff is computed for the χ² threshold:

        ``"tau"`` (default)
            N_eff = max(n_tau_factor × T_window / τ_m, N_EFF_MIN).
            τ_m = Cm_MLE × Rm_MLE × 1e-3 ms. Physically motivated:
            accounts for the temporal correlation of the passive transient.
        ``"lag1"``
            Legacy AR(1) correction from baseline noise lag-1 autocorrelation.
            Under-estimates correlation for smooth decaying signals.
        ``"raw"``
            N_eff = N_raw. Produces uselessly tight thresholds for
            oversampled traces — only for debugging/comparison.
        ``"override"``
            N_eff = N_eff_override (must supply a positive float).

    n_tau_factor
        Multiplier for the tau-based estimator (default 2). Increase to
        widen the CI (conservative); decrease to tighten it.
    N_eff_override
        Explicit N_eff override, takes precedence over n_eff_mode when set.
    n_validation_per_boundary
        Real-NEURON evaluations per CI bound.
    validation_ball_logradius
        Gaussian ball radius in log-space around each bound (≈ ±5 % at 0.05).
    trust_abs_mv, trust_zscore
        Combined GP-trust criterion thresholds (Q5.3 option d).
    """
    # ---- Guard rails --------------------------------------------------------
    if fit_result.gp_result is None:
        raise RuntimeError(
            "fit_result.gp_result is None.  Run fit_one_cell() in a single "
            "process (or call rebuild_neuron_cells_for_phase3() after "
            "fit_cells()) before Phase 3."
        )
    if fit_result.neuron_cell is None or fit_result.opt_inputs is None:
        raise RuntimeError(
            "fit_result.neuron_cell / opt_inputs is None.  Phase 3 requires "
            "the live PassiveCell and OptimiserInputs."
        )

    root     = Path(root_dir)
    cell_dir = root / f"cell_{int(fit_result.specimen_id)}"
    cell_dir.mkdir(parents=True, exist_ok=True)

    opt_inputs = fit_result.opt_inputs
    notes: List[str] = []

    # ---- σ_noise and ρ ------------------------------------------------------
    sigma_mV = float(fit_result.noise_sigma_mV)
    rho      = float(fit_result.noise_rho_lag1)

    # ---- N_raw: samples in the training window ------------------------------
    t0_s = float(opt_inputs.train_window_ms[0]) * 1e-3
    t1_s = float(opt_inputs.train_window_ms[1]) * 1e-3
    N_raw = sum(int((b.t >= t0_s).sum() & (b.t <= t1_s).sum() > 0 and True)
                for b in opt_inputs.train_bundles)
    # More precise: count samples in-window across all bundles
    N_raw = 0
    for b in opt_inputs.train_bundles:
        N_raw += int(((b.t >= t0_s) & (b.t <= t1_s)).sum())
    if N_raw <= 0:
        raise RuntimeError("Could not infer N_raw from training bundles.")

    window_ms = float(opt_inputs.train_window_ms[1] - opt_inputs.train_window_ms[0])

    # ---- τ_m (always computed — used in logging even when not the N_eff mode)
    tau_m_ms = _tau_m_from_fit(fit_result)

    # ---- N_eff selection ----------------------------------------------------
    if N_eff_override is not None and float(N_eff_override) > 0:
        N_eff        = float(N_eff_override)
        n_eff_method = "override"
        tau_n_eff    = _effective_N_tau(tau_m_ms, window_ms, n_tau_factor)
        msg = (f"[phase3] N_eff MANUALLY overridden to {N_eff:.2f} "
               f"(N_raw={N_raw}, tau-based would be {tau_n_eff:.2f})")
        if verbose:
            print(msg)
        notes.append(msg)

    elif n_eff_mode == "tau":
        N_eff        = _effective_N_tau(tau_m_ms, window_ms, n_tau_factor)
        n_eff_method = "tau"

    elif n_eff_mode == "lag1":
        apply_corr   = bool(fit_result.noise_rho_lag1_high)
        N_eff        = _effective_N_lag1(N_raw, rho, apply_corr)
        n_eff_method = "lag1"
        if apply_corr:
            msg = (f"[phase3] lag-1 correction: ρ={rho:.3f} "
                   f"→ N_eff/N = {N_eff/N_raw:.3f}  (N_raw={N_raw})")
            if verbose:
                print(msg)
            notes.append(msg)

    elif n_eff_mode == "raw":
        N_eff        = float(N_raw)
        n_eff_method = "raw"
        msg = ("[phase3] WARNING: n_eff_mode='raw' uses the full raw sample "
               "count.  Threshold will be uselessly tight for oversampled "
               "traces.  Consider n_eff_mode='tau'.")
        if verbose:
            print(msg)
        notes.append(msg)

    else:
        raise ValueError(
            f"n_eff_mode must be 'tau', 'lag1', 'raw', or 'override'; "
            f"got '{n_eff_mode}'."
        )

    # ---- χ²/RMSD threshold --------------------------------------------------
    chi2_delta  = float(chi2_dist.ppf(threshold_alpha, df=1))
    rmsd_thresh = rmsd_threshold_constant(
        fit_result.train_rmsd_mV, sigma_mV, N_eff, chi2_delta,
    )

    if verbose:
        pct_above = (rmsd_thresh / fit_result.train_rmsd_mV - 1.0) * 100
        print(
            f"[phase3] cell {fit_result.specimen_id}\n"
            f"[phase3]   τ_m = {tau_m_ms:.1f} ms  "
            f"n_eff_mode = '{n_eff_method}'  "
            f"N_raw = {N_raw}  N_eff = {N_eff:.2f}\n"
            f"[phase3]   σ = {sigma_mV:.4f} mV  "
            f"χ²_Δ(α={threshold_alpha:.2f}) = {chi2_delta:.3f}\n"
            f"[phase3]   RMSD_MLE = {fit_result.train_rmsd_mV:.4f} mV  "
            f"→  RMSD_thresh = {rmsd_thresh:.4f} mV  "
            f"(+{pct_above:.1f}% above MLE)"
        )

    # ---- Real-simulator loss function (Phase-2 helper, no rewrite) ----------
    # from phase2_optimiser import _build_loss_function  # type: ignore
    loss_fn = _build_loss_function(
        cell=fit_result.neuron_cell,
        train_bundles=opt_inputs.train_bundles,
        v_rest_mV=opt_inputs.v_rest_mV,
        train_window_ms=opt_inputs.train_window_ms,
    )

    # ---- Per-parameter loop -------------------------------------------------
    rng = np.random.default_rng(int(seed))
    profiles: Dict[str, ParameterProfile] = {}
    mle_log  = {"Cm": np.log(fit_result.cm_uF_per_cm2),
                "Rm": np.log(fit_result.rm_Ohm_cm2),
                "Ra": np.log(fit_result.ra_Ohm_cm)}
    mle_phys = {"Cm": fit_result.cm_uF_per_cm2,
                "Rm": fit_result.rm_Ohm_cm2,
                "Ra": fit_result.ra_Ohm_cm}

    for pname in PARAM_NAMES:
        if verbose:
            print(f"[phase3]   profiling {pname} ...")

        grid_log = _build_log_grid(
            fit_result, opt_inputs, pname, n_grid, window_log_halfwidth,
        )
        grid_phys = np.exp(grid_log)

        prof = _profile_one_parameter_gp(
            gp_result=fit_result.gp_result,
            opt_inputs=opt_inputs,
            param_name=pname,
            grid_log=grid_log,
            inner_grid_per_axis=inner_grid_per_axis,
        )

        ci_lo_log, ci_hi_log = _find_threshold_crossings(
            grid_log, prof["profile_mean_rmsd"],
            rmsd_thresh, mle_log[pname],
        )
        ci_lo = float(np.exp(ci_lo_log)) if np.isfinite(ci_lo_log) else float("nan")
        ci_hi = float(np.exp(ci_hi_log)) if np.isfinite(ci_hi_log) else float("nan")
        iclass = _classify_identifiability(
            ci_lo_log, ci_hi_log,
            grid_log, prof["profile_mean_rmsd"], rmsd_thresh,
        )
        if verbose:
            print(f"[phase3]     {pname}: CI = [{ci_lo:.4g}, {ci_hi:.4g}]"
                  f"  ({iclass})")

        def _argmin_at(tlog: float) -> np.ndarray:
            if not np.isfinite(tlog):
                return np.array([float("nan"), float("nan")])
            return prof["inner_argmin_others_log"][
                int(np.argmin(np.abs(grid_log - tlog)))
            ]

        val_lo = _validate_at_bound(
            bound_log=ci_lo_log, pinned_name=pname,
            other_names=prof["other_names"],
            other_argmin_log=_argmin_at(ci_lo_log),
            n_validation=n_validation_per_boundary,
            ball_log_radius=validation_ball_logradius,
            loss_fn=loss_fn, gp_result=fit_result.gp_result,
            rng=rng, opt_inputs=opt_inputs,
        )
        val_hi = _validate_at_bound(
            bound_log=ci_hi_log, pinned_name=pname,
            other_names=prof["other_names"],
            other_argmin_log=_argmin_at(ci_hi_log),
            n_validation=n_validation_per_boundary,
            ball_log_radius=validation_ball_logradius,
            loss_fn=loss_fn, gp_result=fit_result.gp_result,
            rng=rng, opt_inputs=opt_inputs,
        )

        gp_ok = _gp_trust_flag(val_lo + val_hi, trust_abs_mv, trust_zscore)
        if not gp_ok:
            msg = (f"[phase3]     WARNING: GP mismatch at CI boundary for "
                   f"{pname}.  TODO: refine GP with these evaluations.")
            if verbose:
                print(msg)
            notes.append(msg)

        profiles[pname] = ParameterProfile(
            name=pname,
            mle_value=float(mle_phys[pname]),
            grid_physical=grid_phys,
            grid_log=grid_log,
            profile_mean_rmsd=prof["profile_mean_rmsd"],
            profile_std_rmsd=prof["profile_std_rmsd"],
            inner_argmin_others_log=prof["inner_argmin_others_log"],
            ci_lower=ci_lo, ci_upper=ci_hi,
            identifiability=iclass,
            validation_lower=val_lo, validation_upper=val_hi,
            gp_validation_ok=gp_ok,
        )

    # ---- Assemble and persist -----------------------------------------------
    pl_result = ProfileLikelihoodResult(
        specimen_id=int(fit_result.specimen_id),
        sigma_mV=sigma_mV,
        N_raw=int(N_raw),
        N_eff_used=float(N_eff),
        N_eff_method=n_eff_method,
        tau_m_ms=float(tau_m_ms),
        n_tau_factor=float(n_tau_factor),
        rho_lag1=float(rho),
        threshold_alpha=float(threshold_alpha),
        chi2_delta=float(chi2_delta),
        rmsd_threshold=float(rmsd_thresh),
        rmsd_mle=float(fit_result.train_rmsd_mV),
        profiles=profiles,
        notes=notes,
    )

    if save_pickle:
        with open(cell_dir / "profile_likelihood_result.pkl", "wb") as fh:
            pickle.dump(pl_result, fh)
        summary = {
            k: getattr(pl_result, k)
            for k in ("specimen_id", "sigma_mV", "N_raw", "N_eff_used",
                      "N_eff_method", "tau_m_ms", "n_tau_factor", "rho_lag1",
                      "threshold_alpha", "chi2_delta", "rmsd_threshold",
                      "rmsd_mle", "notes")
        }
        summary["per_parameter"] = {
            p.name: {
                "mle": p.mle_value, "ci_lower": p.ci_lower,
                "ci_upper": p.ci_upper, "identifiability": p.identifiability,
                "gp_ok": p.gp_validation_ok,
            }
            for p in profiles.values()
        }
        with open(cell_dir / "profile_likelihood_summary.json", "w") as fh:
            json.dump(summary, fh, indent=2, default=float)

    if save_plots:
        plot_profile_likelihood(pl_result, cell_dir, show=False)

    return pl_result


# %% Cell 10 — Plotting ========================================================
def plot_profile_likelihood(
    pl_result: ProfileLikelihoodResult,
    out_dir: os.PathLike | str,
    *,
    show: bool = False,
) -> List[Path]:
    """Three separate PNG figures, one per parameter.

    Each panel shows:
      · GP-profile mean (solid blue)
      · ±1 σ_GP shaded band
      · CI threshold curve (drawn via ax.plot — horizontal in the
        constant-N case but not forced to be, so future θ-dependent
        N_eff would curve naturally)
      · Vertical lines: MLE (black), CI bounds (orange dashed)
      · Red circles: real-NEURON RMSD at validation ball points
      · Blue crosses: GP-predicted RMSD at the same points
      · Title: τ_m, N_eff, method — so the threshold can be audited
        without opening a log file
    """
    out_dir = Path(out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)
    saved: List[Path] = []

    for pname, prof in pl_result.profiles.items():
        fig, ax = plt.subplots(figsize=(7.0, 4.8))

        ax.plot(prof.grid_physical, prof.profile_mean_rmsd,
                lw=1.8, color="tab:blue",
                label=r"GP profile mean $\mu_{GP}$")
        ax.fill_between(
            prof.grid_physical,
            prof.profile_mean_rmsd - prof.profile_std_rmsd,
            prof.profile_mean_rmsd + prof.profile_std_rmsd,
            color="tab:blue", alpha=0.18,
            label=r"$\mu_{GP} \pm \sigma_{GP}$",
        )

        # Threshold — drawn as a curve (flat in the constant-N case)
        thresh_curve = np.full_like(prof.grid_physical, pl_result.rmsd_threshold)
        ax.plot(prof.grid_physical, thresh_curve,
                lw=1.0, linestyle="--", color="0.3",
                label=(fr"CI threshold  $\alpha$={pl_result.threshold_alpha:.2f}, "
                       fr"$N_{{eff}}$={pl_result.N_eff_used:.1f} "
                       fr"[{pl_result.N_eff_method}]"))

        ax.axvline(prof.mle_value, color="k", lw=1.0, alpha=0.4,
                   linestyle="-", label="MLE")
        if np.isfinite(prof.ci_lower):
            ax.axvline(prof.ci_lower, color="tab:orange", lw=1.2,
                       linestyle=":", label="CI lower")
        if np.isfinite(prof.ci_upper):
            ax.axvline(prof.ci_upper, color="tab:orange", lw=1.2,
                       linestyle=":", label="CI upper")

        for pts in (prof.validation_lower, prof.validation_upper):
            for (th, rr, mg, _) in pts:
                if np.isfinite(rr):
                    ax.plot(th, rr, marker="o", markersize=6,
                            markerfacecolor="tab:red",
                            markeredgecolor="darkred",
                            linestyle="none", zorder=5)
                ax.plot(th, mg, marker="x", markersize=7,
                        color="tab:blue", linestyle="none", zorder=5)

        from matplotlib.lines import Line2D
        extra = [
            Line2D([0],[0], marker="o", linestyle="none",
                   markerfacecolor="tab:red", markeredgecolor="darkred",
                   markersize=6, label="real NEURON RMSD"),
            Line2D([0],[0], marker="x", linestyle="none",
                   color="tab:blue", markersize=7, label="GP-predicted RMSD"),
        ]
        h, l = ax.get_legend_handles_labels()
        ax.legend(h + extra, l + [e.get_label() for e in extra],
                  loc="best", fontsize=8, frameon=True)

        ax.set_xscale("log")
        ax.set_xlabel(PARAM_LABELS[pname])
        ax.set_ylabel("Training RMSD (mV)")

        ci_str = (f"[{prof.ci_lower:.4g}, {prof.ci_upper:.4g}]"
                  if (np.isfinite(prof.ci_lower) and np.isfinite(prof.ci_upper))
                  else f"[{prof.ci_lower:.4g}, {prof.ci_upper:.4g}]  ⚠ non-ident.")
        trust = "GP ✓" if prof.gp_validation_ok else "GP ✗ MISMATCH"
        ax.set_title(
            f"Profile likelihood — {pname}  |  cell {pl_result.specimen_id}\n"
            f"MLE = {prof.mle_value:.4g}   CI: {ci_str}\n"
            f"{prof.identifiability}   {trust}   "
            f"(τ_m = {pl_result.tau_m_ms:.1f} ms, "
            f"N_eff = {pl_result.N_eff_used:.1f} [{pl_result.N_eff_method}])",
            fontsize=9,
        )
        fig.tight_layout()
        p = out_dir / f"profile_{pname}.png"
        fig.savefig(p, dpi=140)
        saved.append(p)
        if show:
            plt.show()
        else:
            plt.close(fig)

    return saved


