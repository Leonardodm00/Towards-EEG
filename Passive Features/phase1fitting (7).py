def plot_gp_diagnostic(
    gp_result: GpDiagnosticResult,
    bootstrap_result: Optional[BootstrapCIResult],
    out_dir,
    envelope_k: float,
) -> List[Path]:
    """Per-parameter GP profile with σ envelope and all available CI overlays."""
    out_dir = Path(out_dir)
    saved = []

    # Collect all CI tables from bootstrap_result
    ci_sources: Dict[str, Dict[str, Tuple[float, float]]] = {}
    if bootstrap_result is not None:
        ci_sources["percentile"] = bootstrap_result.ci_percentile
        ci_sources["bca"]        = bootstrap_result.ci_bca
        ci_sources["normal"]     = bootstrap_result.ci_normal

    for p, pp in gp_result.per_parameter.items():
        fig, ax = plt.subplots(figsize=(7.0, 4.8))

        # GP mean + envelope
        ax.plot(pp.grid_physical, pp.profile_mean, lw=1.6, color="tab:blue",
                label=r"GP mean $\mu_{GP}$")
        ax.fill_between(
            pp.grid_physical,
            pp.profile_mean - envelope_k * pp.profile_std,
            pp.profile_mean + envelope_k * pp.profile_std,
            color="tab:blue", alpha=0.18,
            label=fr"$\mu_{{GP}} \pm {envelope_k:.0f}\sigma_{{GP}}$",
        )

        # MLE
        ax.axvline(pp.mle_value, color="k", lw=1.0, alpha=0.5, label="MLE")

        # ── Overlay all CI types ──
        for ci_name, ci_table in ci_sources.items():
            lo, hi = ci_table[p]
            sty = CI_STYLES[ci_name]
            if np.isfinite(lo):
                ax.axvline(lo, color=sty["color"], lw=sty["lw"],
                           linestyle=sty["ls"])
            if np.isfinite(hi):
                ax.axvline(hi, color=sty["color"], lw=sty["lw"],
                           linestyle=sty["ls"], label=f"{ci_name} CI")

        # ── Real-NEURON validation dots ──
        for key, pts in pp.validation_points.items():
            for (th, rr, mg, _) in pts:
                if np.isfinite(rr):
                    ax.plot(th, rr, marker="o", markersize=6,
                            markerfacecolor="tab:red",
                            markeredgecolor="darkred",
                            linestyle="none", zorder=5)
                ax.plot(th, mg, marker="x", markersize=7,
                        color="tab:blue", linestyle="none", zorder=5)

        extra = [
            Line2D([0],[0], marker="o", linestyle="none",
                   markerfacecolor="tab:red", markeredgecolor="darkred",
                   markersize=6, label="real NEURON RMSD"),
            Line2D([0],[0], marker="x", linestyle="none",
                   color="tab:blue", markersize=7, label="GP-predicted RMSD"),
        ]
        h, l = ax.get_legend_handles_labels()
        ax.legend(h + extra, l + [e.get_label() for e in extra],
                  loc="best", fontsize=8, frameon=True)

        ax.set_xscale("log")
        ax.set_xlabel(PARAM_LABELS[p])
        ax.set_ylabel("Training RMSD (mV)")

        trust = "GP ✓" if pp.gp_trustworthy else "GP ✗ MISMATCH"
        ax.set_title(
            f"GP diagnostic — {p}  |  cell {gp_result.specimen_id}\n"
            f"MLE = {pp.mle_value:.4g}   {trust}  "
            f"(envelope k = {envelope_k:.0f})",
            fontsize=9,
        )
        fig.tight_layout()
        path = out_dir / f"profile_{p}.png"
        fig.savefig(path, dpi=140)
        saved.append(path)
        plt.close(fig)
    return saved


