# %% Cell 4 — Helper: list_human_cells_with_morphology =========================
def list_human_cells_with_morphology(
    layer: Optional[str] = None,
    dendrite_type: Optional[Literal["spiny", "aspiny", "sparsely spiny"]] = None,
    require_square_subthreshold: bool = True,
    require_long_square: bool = True,
    patchseq_ttype_csv: Optional[str] = None,
    interneuron_subtype: Optional[Literal["SST", "PV", "VIP"]] = None,
    n_cells: Optional[int] = None,
    ctc: Optional[CellTypesCache] = None,
    verbose: bool = True,
) -> pd.DataFrame:
    """
    Discover human Allen cells matching the requested filters.

    Parameters
    ----------
    layer
        e.g. ``"2/3"``, ``"4"``, ``"5"``, ``"6a"`` — matched against
        ``structure_layer_name``.
    dendrite_type
        ``"spiny"`` / ``"aspiny"`` / ``"sparsely spiny"``.
    require_square_subthreshold, require_long_square
        Whether to drop cells that lack the named Allen protocols.
    patchseq_ttype_csv
        Optional path to a CSV with at least columns ``specimen_id`` and
        ``t_type`` mapping each cell to its Patch-seq transcriptomic type.
        If absent, all aspiny cells are returned with subtype ``"unknown"``.
    interneuron_subtype
        ``"SST"`` / ``"PV"`` / ``"VIP"``.  Honoured only when
        ``patchseq_ttype_csv`` is supplied; otherwise a warning is raised
        and the filter is ignored.
    n_cells
        If set, truncate the returned DataFrame to the first ``n_cells`` rows.
    ctc
        Optional pre-built ``CellTypesCache``.  If ``None`` one is created
        with manifest at ``cell_types/manifest.json`` (Colab-friendly default).

    Returns
    -------
    DataFrame with columns including ``specimen_id``, ``structure_layer_name``,
    ``dendrite_type``, ``donor_id``, ``has_square_subthreshold``,
    ``has_long_square``, and ``subtype``.
    """
    if ctc is None:
        ctc = CellTypesCache(manifest_file="cell_types/manifest.json")

    cells = ctc.get_cells(
        species=[CellTypesApi.HUMAN],
        require_morphology=False,
        require_reconstruction=True,
    )
    df = pd.DataFrame(cells)
    if verbose:
        print(f"[list_human_cells] Found {len(df)} human cells with reconstructions")

    if layer is not None:
        df = df[df["structure_layer_name"] == layer]
        if verbose:
            print(f"[list_human_cells]   after layer={layer!r}: {len(df)} cells")

    if dendrite_type is not None:
        df = df[df["dendrite_type"] == dendrite_type]
        if verbose:
            print(f"[list_human_cells]   after dendrite_type={dendrite_type!r}: "
                  f"{len(df)} cells")

    # Protocol availability — needs one network round-trip per cell, so done last
    if require_square_subthreshold or require_long_square:
        ss_flags, ls_flags = [], []
        for sid in df["id"]:
            try:
                sweeps = ctc.get_ephys_sweeps(int(sid))
                names = {s.get("stimulus_name", "") for s in sweeps}
                # DEFENSIVE: Allen has used "Short Square - Subthreshold",
                # "Square - 0.5ms Subthreshold", "Square Subthreshold" across
                # releases.  Match anything that is BOTH "Square" AND
                # "Subthreshold" but NOT "Long Square".
                ss_flags.append(any(
                    ("Square" in n) and ("Subthreshold" in n) and ("Long" not in n)
                    for n in names
                ))
                ls_flags.append(any("Long Square" in n for n in names))
            except Exception as e:
                if verbose:
                    print(f"[list_human_cells]   sweep query failed for "
                          f"specimen {sid}: {e}")
                ss_flags.append(False)
                ls_flags.append(False)
        df = df.assign(has_square_subthreshold=ss_flags, has_long_square=ls_flags)
        if require_square_subthreshold:
            df = df[df["has_square_subthreshold"]]
        if require_long_square:
            df = df[df["has_long_square"]]
        if verbose:
            print(f"[list_human_cells]   after protocol filters: {len(df)} cells")

    # Patch-seq subtype overlay (optional)
    df = df.assign(subtype="unknown")
    if patchseq_ttype_csv is not None:
        ttype_df = pd.read_csv(patchseq_ttype_csv)
        ttype_df.columns = [c.lower() for c in ttype_df.columns]
        if not {"specimen_id", "t_type"} <= set(ttype_df.columns):
            warnings.warn(
                "patchseq_ttype_csv must contain 'specimen_id' and 't_type' "
                "columns; subtype assignment skipped.")
        else:
            sub_map: Dict[int, str] = {}
            for _, row in ttype_df.iterrows():
                ttype = str(row["t_type"]).upper()
                if "SST" in ttype:
                    sub_map[int(row["specimen_id"])] = "SST"
                elif "PVALB" in ttype or " PV " in f" {ttype} ":
                    sub_map[int(row["specimen_id"])] = "PV"
                elif "VIP" in ttype:
                    sub_map[int(row["specimen_id"])] = "VIP"
                else:
                    sub_map[int(row["specimen_id"])] = "other"
            df["subtype"] = df["id"].map(sub_map).fillna("unknown")

    if interneuron_subtype is not None:
        if patchseq_ttype_csv is None:
            warnings.warn(
                f"interneuron_subtype={interneuron_subtype!r} was requested but "
                f"no patchseq_ttype_csv was supplied — filter ignored.")
        else:
            df = df[df["subtype"] == interneuron_subtype]
            if verbose:
                print(f"[list_human_cells]   after subtype="
                      f"{interneuron_subtype!r}: {len(df)} cells")

    keep = ["id", "structure_layer_name", "dendrite_type", "donor_id",
            "structure_area_abbrev", "subtype",
            "has_square_subthreshold", "has_long_square"]
    out = df[[c for c in keep if c in df.columns]].copy()
    out = out.rename(columns={"id": "specimen_id"}).reset_index(drop=True)

    if n_cells is not None and len(out) > n_cells:
        out = out.iloc[:n_cells].copy()
        if verbose:
            print(f"[list_human_cells]   truncated to first {n_cells} cells")

    return out


