def _other_axes_log_grid(opt_inputs, pinned_name, inner_grid_per_axis):
    """Uniform log-space grid over the two parameters that are NOT pinned.

    Returns
    -------
    A, B : ndarray (inner_grid_per_axis²,)
        Ravelled meshgrid coordinates for the two free axes.
    other : tuple[str, str]
        Names of the two free parameters, in the order (A, B).
    """
    other = tuple(n for n in ("Cm", "Rm", "Ra") if n != pinned_name)
    name_to_dim = {d.name: d for d in opt_inputs.skopt_dimensions}
    bounds = [(float(name_to_dim[n].low), float(name_to_dim[n].high))
              for n in other]
    a = np.linspace(*bounds[0], inner_grid_per_axis)
    b = np.linspace(*bounds[1], inner_grid_per_axis)
    A, B = np.meshgrid(a, b, indexing="ij")
    return A.ravel(), B.ravel(), other


def _build_log_grid(fit_result, opt_inputs, param_name, n_grid):
    """Full search-space sweep for ``param_name`` in log-space.

    Returns a 1-D array of ``n_grid`` evenly spaced values between the
    lower and upper bounds of the parameter's skopt dimension.
    """
    name_to_dim = {d.name: d for d in opt_inputs.skopt_dimensions}
    dim = name_to_dim[param_name]
    return np.linspace(float(dim.low), float(dim.high), int(n_grid))


def _build_gp_profile(
    *, gp_result, opt_inputs, param_name: str,
    grid_log: np.ndarray, inner_grid_per_axis: int,
):
    """Compute (μ_GP, σ_GP) along the 1-D profile for one parameter.

    For each pinned value of ``param_name``, the other two parameters are
    swept over a dense inner grid (``inner_grid_per_axis`` × ``inner_grid_per_axis``
    points).  The GP is queried on the full batch in one call; we record
    μ and σ at the inner argmin — i.e. the point that minimises the GP
    mean for that pinned value.

    Parameters
    ----------
    gp_result : OptimizeResult
        The result object returned by ``gp_minimize``.  Uses
        ``gp_result.models[-1]`` (the final GP model) and ``gp_result.space``
        for the skopt space-transform.
    opt_inputs : OptimiserInputs
        Carries the skopt dimensions (search space bounds).
    param_name : str
        One of ``"Cm"``, ``"Rm"``, ``"Ra"``.
    grid_log : ndarray
        1-D array of log-space values for ``param_name`` (from
        ``_build_log_grid``).
    inner_grid_per_axis : int
        Number of grid points along each of the two free parameter axes.

    Returns
    -------
    mu : ndarray, shape (n_grid,)
    sigma : ndarray, shape (n_grid,)
    """
    gp    = gp_result.models[-1]
    space = gp_result.space
    n_grid = len(grid_log)
    M2     = inner_grid_per_axis ** 2

    other_A, other_B, (nA, nB) = _other_axes_log_grid(
        opt_inputs, param_name, inner_grid_per_axis)

    nc = {"Cm": 0, "Rm": 1, "Ra": 2}
    pc, cA, cB = nc[param_name], nc[nA], nc[nB]

    cands = np.empty((n_grid * M2, 3))
    for i, pv in enumerate(grid_log):
        sl = slice(i * M2, (i + 1) * M2)
        cands[sl, pc] = pv
        cands[sl, cA] = other_A
        cands[sl, cB] = other_B

    mu_flat, sig_flat = gp.predict(
        space.transform(cands.tolist()), return_std=True)

    mu  = mu_flat.reshape(n_grid, M2)
    sig = sig_flat.reshape(n_grid, M2)
    idx = np.argmin(mu, axis=1)
    rows = np.arange(n_grid)
    return mu[rows, idx], sig[rows, idx]


def _validate_at_param_value(
    *, theta_phys: float, param_name: str,
    fit_result, n_validation: int, ball_log_radius: float,
    rng,
):
    """Run real NEURON simulations near a CI boundary point.

    Draws ``n_validation`` points in a small log-space ball around
    (param = theta_phys, others = MLE) and returns both the real NEURON
    RMSD and the GP-predicted μ/σ at each point.

    Parameters
    ----------
    theta_phys : float
        Physical-unit value of ``param_name`` at which to validate (e.g.
        the lower bound of a CI in Ω·cm²).
    param_name : str
        One of ``"Cm"``, ``"Rm"``, ``"Ra"``.
    fit_result : PassiveFitResult
        Must have ``.neuron_cell``, ``.gp_result``, and ``.opt_inputs`` set.
    n_validation : int
        Number of NEURON simulations to run at this boundary.
    ball_log_radius : float
        Std-dev of the Gaussian perturbation added to the other two parameters
        (in log-space).  The first point is always the exact centre.
    rng : numpy.random.Generator
        Seeded RNG for reproducibility.

    Returns
    -------
    list of (theta_phys_k, rmsd_real, mu_gp, sigma_gp) tuples.
    """
    import warnings as _warnings

    if not np.isfinite(theta_phys):
        return []

    nc = {"Cm": 0, "Rm": 1, "Ra": 2}
    pc = nc[param_name]

    mle_log = np.array([
        np.log(fit_result.cm_uF_per_cm2),
        np.log(fit_result.rm_Ohm_cm2),
        np.log(fit_result.ra_Ohm_cm),
    ])
    centre = mle_log.copy()
    centre[pc] = float(np.log(theta_phys))

    opt_inputs = fit_result.opt_inputs
    lows  = np.array([float(d.low)  for d in opt_inputs.skopt_dimensions])
    highs = np.array([float(d.high) for d in opt_inputs.skopt_dimensions])

    n_validation = int(n_validation)
    samples = np.empty((n_validation, 3))
    samples[0] = centre
    if n_validation > 1:
        samples[1:] = centre + rng.normal(
            0.0, ball_log_radius, size=(n_validation - 1, 3))
    samples = np.clip(samples, lows, highs)

    gp    = fit_result.gp_result.models[-1]
    space = fit_result.gp_result.space
    mu_p, sig_p = gp.predict(space.transform(samples.tolist()), return_std=True)

    fn   = _locate_build_loss_function()
    loss = fn(
        cell=fit_result.neuron_cell,
        train_bundles=opt_inputs.train_bundles,
        v_rest_mV=opt_inputs.v_rest_mV,
        train_window_ms=opt_inputs.train_window_ms,
    )

    out = []
    for k, (s, mu_s, sig_s) in enumerate(zip(samples, mu_p, sig_p)):
        try:
            rmsd_real = float(loss(s[0], s[1], s[2]))
        except Exception as e:
            _warnings.warn(f"NEURON validation crashed at {s}: {e}")
            rmsd_real = float("nan")
        phys_k = theta_phys if k == 0 else float(np.exp(s[pc]))
        out.append((phys_k, rmsd_real, float(mu_s), float(sig_s)))
    return out


def _gp_trust_combined(pts, abs_mv: float, z_thresh: float) -> bool:
    """Return True iff ALL validation points agree with the GP within tolerance.

    A point fails if:
      - the real RMSD is not finite, or
      - |RMSD_real − μ_GP| > abs_mv (absolute mismatch in mV), or
      - |RMSD_real − μ_GP| > z_thresh × σ_GP (z-score mismatch).

    Parameters
    ----------
    pts : list of (theta, rmsd_real, mu_gp, sigma_gp)
    abs_mv : float   absolute tolerance in mV
    z_thresh : float z-score tolerance
    """
    if not pts:
        return False
    for (_, rr, mg, sg) in pts:
        if not np.isfinite(rr):
            return False
        d = abs(rr - mg)
        if d > abs_mv:
            return False
        if sg > 1e-9 and d > z_thresh * sg:
            return False
    return True


def _interp_to_grid(
    t_src: np.ndarray, v_src: np.ndarray, t_dst: np.ndarray,
) -> np.ndarray:
    """Linear interpolation of v_src onto t_dst.

    NEURON's variable timestep can produce a slightly different time axis
    than the experimental sweep; this resamples onto the experimental grid
    before computing point-wise RMSD.
    """
    return np.interp(t_dst, t_src, v_src)


def _simulate_square_subthreshold(
    cell, bundle, v_rest_mV: float,
    pre_pad_ms: float = 10.0,
    post_pad_ms: float = 100.0,
    dt_ms: float = 0.025,
):
    """Replay a Square-Subthreshold pulse on the NEURON model.

    The bundle's ``t = 0`` is the pulse onset.  We start ``pre_pad_ms``
    before so the model has a settled baseline matching the experimental
    pre-pulse window.

    Returns
    -------
    t_sim_s : ndarray   — seconds, t=0 at pulse onset
    v_sim   : ndarray   — mV
    """
    delay_ms = pre_pad_ms
    dur_ms   = bundle.stim_duration_s * 1e3
    tstop_ms = pre_pad_ms + dur_ms + post_pad_ms

    t_sim_ms, v_sim = cell.simulate(
        stim_amp_pA  = bundle.amplitude_pA,
        stim_delay_ms = delay_ms,
        stim_dur_ms   = dur_ms,
        tstop_ms      = tstop_ms,
        v_init_mV     = v_rest_mV,
        dt_ms         = dt_ms,
    )
    # Shift so t = 0 is the pulse onset, matching the bundle's convention
    t_sim_s = t_sim_ms * 1e-3 - pre_pad_ms * 1e-3
    return t_sim_s, v_sim


