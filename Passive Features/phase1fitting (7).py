def phase3_full_for_cell(
    *,
    fit_result,
    root_dir: str,
    bootstrap_kwargs: Optional[Dict[str, Any]] = None,
    gp_kwargs: Optional[Dict[str, Any]] = None,
    verbose: bool = True,
) -> Phase3Result:
    """Run bootstrap + GP diagnostic for one cell (v3).

    Output structure:
        <root_dir>/cell_<id>/
          ├─ bootstrap/        *.pkl, *.npy, *.csv, histogram_*.png, pairwise_*.png
          └─ gp_diagnostic/    *.pkl, profile_*.png
    """
    if verbose:
        print(f"\n=== Phase 3 v3 — cell {fit_result.specimen_id} ===")

    boot = bootstrap_ci_for_cell(
        fit_result=fit_result, root_dir=root_dir,
        verbose=verbose, **(bootstrap_kwargs or {}),
    )
