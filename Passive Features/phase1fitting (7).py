def plot_bootstrap_pairwise(
    r: BootstrapCIResult,
    out_dir,
) -> List[Path]:
    """Three pairwise scatter plots with covariance ellipse."""
    out_dir = Path(out_dir)
    saved = []
    nc = {"Cm": 0, "Rm": 1, "Ra": 2}
    pairs = [("Cm", "Rm"), ("Cm", "Ra"), ("Rm", "Ra")]

    for (px, py) in pairs:
        ix, iy = nc[px], nc[py]
        fig, ax = plt.subplots(figsize=(6.4, 5.6))
        ax.scatter(r.samples[:, ix], r.samples[:, iy],
                   s=8, alpha=0.4, color="tab:blue",
                   label=f"bootstrap (n={r.n_kept})")
        ax.plot(r.mle_physical[ix], r.mle_physical[iy],
                marker="*", markersize=18,
                color="k", linestyle="none", label="MLE")

        # 95% covariance ellipse in log-space → exponentiated
        mu_log = np.array(r.mle_log)
        sub = np.array([[r.cov_log[ix, ix], r.cov_log[ix, iy]],
                        [r.cov_log[iy, ix], r.cov_log[iy, iy]]])
        try:
            from scipy.stats import chi2 as chi2_dist
            radius = float(np.sqrt(chi2_dist.ppf(r.threshold_alpha, df=2)))
            thetas = np.linspace(0, 2 * np.pi, 200)
            unit_circle = np.stack([np.cos(thetas), np.sin(thetas)], axis=0)
            L = np.linalg.cholesky(sub + 1e-12 * np.eye(2))
            ell_log = mu_log[[ix, iy]][:, None] + radius * (L @ unit_circle)
            ell_phys = np.exp(ell_log)
            ax.plot(ell_phys[0], ell_phys[1], color="tab:red", lw=1.3,
                    label=fr"cov ellipse (χ²₂, {r.threshold_alpha:.0%})")
        except Exception:
            pass

        corr = (r.cov_log[ix, iy]
                / np.sqrt(r.cov_log[ix, ix] * r.cov_log[iy, iy] + 1e-20))
        ax.set_xscale("log")
        ax.set_yscale("log")
        ax.set_xlabel(PARAM_LABELS[px])
        ax.set_ylabel(PARAM_LABELS[py])
        ax.set_title(
            f"Bootstrap joint distribution — {px} vs {py}  |  "
            f"cell {r.specimen_id}\n"
            f"ρ(log)={corr:+.3f}",
            fontsize=10,
        )
        ax.legend(loc="best", fontsize=9)
        fig.tight_layout()
        path = out_dir / f"pairwise_{px}_{py}.png"
        fig.savefig(path, dpi=140)
        saved.append(path)
        plt.close(fig)
    return saved


