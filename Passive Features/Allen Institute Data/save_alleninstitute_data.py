@dataclass
class PassiveSearchSpace:
    cm_bounds: Tuple[float, float] = DEFAULT_CM_BOUNDS
    rm_bounds: Tuple[float, float] = DEFAULT_RM_BOUNDS
    ra_bounds: Tuple[float, float] = DEFAULT_RA_BOUNDS

    def as_skopt_dimensions(self) -> List[Real]:
        """Return skopt dimensions in LOG-SPACE.

        Log-transform convention
        ------------------------
        Let p denote a biological parameter (Cm, Rm, or Ra) in its natural
        physical units.  The variable the optimiser sees is:

            q = log(p)      (natural logarithm)

        so that p is recovered as:

            p = exp(q)

        Advantages of working in log-space:
        * All three parameters span at least one order of magnitude.
          In log-space their ranges become comparable scalars (-1 to +1 for
          Cm, ~7 to ~12 for Rm, ~4 to ~7 for Ra), so the GP kernel treats
          each axis consistently without manual length-scale tuning.
        * A uniform prior in q = log(p) is equivalent to a log-uniform prior
          in p, which is the correct non-informative prior for scale
          parameters — it assigns equal probability to each decade.
        * The optimisation landscape is smoother in log-space for passive
          cable parameters because the somatic transient depends on
          log-linear combinations of these parameters (e.g. τm = Cm × Rm
          is additive in log-space).
        * Positive-definiteness is guaranteed: exp(q) > 0 for all finite q,
          so the optimiser can never propose a non-physical negative value.

        Phase 2's loss function is responsible for converting q → p = exp(q)
        before passing the parameters to NEURON.  result.x from gp_minimize
        contains the log-space optima and must likewise be exponentiated.
        """
        return [
            Real(np.log(self.cm_bounds[0]), np.log(self.cm_bounds[1]),
                 prior="uniform", name="Cm"),
            Real(np.log(self.rm_bounds[0]), np.log(self.rm_bounds[1]),
                 prior="uniform", name="Rm"),
            Real(np.log(self.ra_bounds[0]), np.log(self.ra_bounds[1]),
                 prior="uniform", name="Ra"),
        ]


