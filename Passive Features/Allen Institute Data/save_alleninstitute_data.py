def download_allen_archive(
    layer: str = "3",
    dendrite_type: str = "spiny",
    n_cells: Optional[int] = 1,
    archive_dir: str = "allen_archive",
    *,
    # ── SS pulse-extraction parameters (match phase1 defaults) ──
    pre_ms: float = 10.0,
    post_ms: float = 200.0,
    pulse_threshold_pA: float = 50.0,
    expected_amplitude_pA: float = SQ_SUB_AMPLITUDE_PA,    # 200.0
    amplitude_tol_pA: float = SQ_SUB_AMPLITUDE_TOL_PA,     # 30.0
    expected_duration_s: float = SQ_SUB_DURATION_S,         # 5e-4
    duration_tol_s: float = SQ_SUB_DURATION_TOL_S,          # 5e-4
    # ── Smoke-test parameters ──
    F: float = 1.9,
    fit_target: str = "hyp",
    # ── General ──
    ljp_correction_mV: float = LJP_CORRECTION_MV,           # 14.0
    cache_dir: str = "cell_types",
    skip_existing: bool = True,
    verbose: bool = True,
    # ── Pass-through to list_human_cells_with_morphology ──
    require_square_subthreshold: bool = True,
    require_long_square: bool = True,
    patchseq_ttype_csv: Optional[str] = None,
    interneuron_subtype: Optional[str] = None,
) -> Tuple[pd.DataFrame, List[Dict[str, Any]]]:
    """Download Allen Cell Types data and archive for offline HPC use.

    Parameters
    ----------
    layer, dendrite_type, n_cells
        Forwarded to ``list_human_cells_with_morphology``.
    archive_dir
        Root directory for the archive.  Created if absent.
    pre_ms, post_ms
        Window around each Square Subthreshold pulse onset (ms before and
        after).  Baked into the saved pulse windows — choose generously.
        Default 10/200 ms matches the phase1 extraction.
    pulse_threshold_pA … duration_tol_s
        Pulse-detection QC parameters.  Match the phase1 defaults.
    F
        Spine-area correction factor for the smoke test only (not baked
        into the archive — HPC code sets F independently).
    fit_target
        Polarity for the smoke test's ``prepare_optimiser_inputs`` call.
    skip_existing
        If True, skip cells whose ``metadata.json`` already exists.
    cache_dir
        AllenSDK cache directory for NWB / SWC downloads.

    Returns
    -------
    (candidates_df, results_list)
        ``results_list`` is a list of ``{"specimen_id": int, "status": str}``
        dicts.  Status is ``"ok"``, ``"partial"`` (smoke test incomplete),
        ``"skipped"``, or ``"failed: <message>"``.
    """
    archive_root = Path(archive_dir)
    archive_root.mkdir(parents=True, exist_ok=True)

    # ── 1. Discover candidates ───────────────────────────────────────────────
    ctc = CellTypesCache(manifest_file=f"{cache_dir}/manifest.json")
    candidates = list_human_cells_with_morphology(
        layer=layer,
        dendrite_type=dendrite_type,
        require_square_subthreshold=require_square_subthreshold,
        require_long_square=require_long_square,
        patchseq_ttype_csv=patchseq_ttype_csv,
        interneuron_subtype=interneuron_subtype,
        n_cells=n_cells,
        ctc=ctc,
        verbose=verbose,
    )
    candidates.to_csv(archive_root / "candidates.csv", index=False)

    if verbose:
        print(f"\n{'='*60}")
        print(f"[archive] {len(candidates)} candidate(s) → {archive_root}")
        print(f"{'='*60}\n")

    # ── 2. Archive each cell ─────────────────────────────────────────────────
    results: List[Dict[str, Any]] = []
    for _, row in candidates.iterrows():
        sid = int(row["specimen_id"])
        cell_dir = archive_root / f"specimen_{sid}"

        if skip_existing and (cell_dir / "metadata.json").exists():
            if verbose:
                print(f"[archive] specimen {sid}: already archived, skipping\n")
            results.append({"specimen_id": sid, "status": "skipped"})
            continue

        cell_dir.mkdir(parents=True, exist_ok=True)
        if verbose:
            print(f"[archive] ═══ specimen {sid} ═══")

        try:
            status = _archive_one_cell(
                sid=sid,
                cell_dir=cell_dir,
                ctc=ctc,
                cache_dir=cache_dir,
                ljp_correction_mV=ljp_correction_mV,
                pre_ms=pre_ms,
                post_ms=post_ms,
                pulse_threshold_pA=pulse_threshold_pA,
                expected_amplitude_pA=expected_amplitude_pA,
                amplitude_tol_pA=amplitude_tol_pA,
                expected_duration_s=expected_duration_s,
                duration_tol_s=duration_tol_s,
                F=F,
                fit_target=fit_target,
                verbose=verbose,
            )
            results.append({"specimen_id": sid, "status": status})
        except Exception as e:
            if verbose:
                print(f"[archive]   FAILED: {type(e).__name__}: {e}\n")
            results.append({
                "specimen_id": sid,
                "status": f"failed: {type(e).__name__}: {e}",
            })

    # ── 3. Save manifest ─────────────────────────────────────────────────────
    manifest = {
        "created": datetime.now().isoformat(),
        "archive_format_version": "1.0",
        "parameters": {
            "layer": layer,
            "dendrite_type": dendrite_type,
            "n_cells": n_cells,
            "pre_ms": pre_ms,
            "post_ms": post_ms,
            "pulse_threshold_pA": pulse_threshold_pA,
            "expected_amplitude_pA": expected_amplitude_pA,
            "amplitude_tol_pA": amplitude_tol_pA,
            "expected_duration_s": expected_duration_s,
            "duration_tol_s": duration_tol_s,
            "F_smoke_test": F,
            "fit_target_smoke_test": fit_target,
            "ljp_correction_mV": ljp_correction_mV,
        },
        "cells": results,
    }
    with open(archive_root / "manifest.json", "w") as f:
        json.dump(manifest, f, indent=2, cls=_NumpyEncoder)

    if verbose:
        n_ok = sum(1 for r in results if r["status"] == "ok")
        n_partial = sum(1 for r in results if r["status"] == "partial")
        n_fail = sum(1 for r in results if r["status"].startswith("failed"))
        n_skip = sum(1 for r in results if r["status"] == "skipped")
        print(f"{'='*60}")
        print(f"[archive] DONE — ok={n_ok}  partial={n_partial}  "
              f"failed={n_fail}  skipped={n_skip}")
        print(f"{'='*60}")

    return candidates, results


# ═══════════════════════════════════════════════════════════════════════════════
#  PER-CELL ARCHIVAL WORKER
# ═══════════════════════════════════════════════════════════════════════════════

def _archive_one_cell(
    sid: int,
    cell_dir: Path,
    ctc,               # CellTypesCache
    cache_dir: str,
    *,
    ljp_correction_mV: float,
    pre_ms: float,
    post_ms: float,
    pulse_threshold_pA: float,
    expected_amplitude_pA: float,
    amplitude_tol_pA: float,
    expected_duration_s: float,
    duration_tol_s: float,
    F: float,
    fit_target: str,
    verbose: bool,
) -> str:
    """Download, extract, smoke-test, and save all data for one cell.

    Returns
    -------
    ``"ok"`` if all smoke tests pass, ``"partial"`` otherwise.
    """

    # ══════════════════════════════════════════════════════════════════════
    # A.  CELL METADATA
    # ══════════════════════════════════════════════════════════════════════
    cells = ctc.get_cells(species=[CellTypesApi.HUMAN])
    meta = next((c for c in cells if c["id"] == sid), None)
    if meta is None:
        raise ValueError(f"Specimen {sid} not found among human cells")

    # ══════════════════════════════════════════════════════════════════════
    # B.  MORPHOLOGY  —  download SWC and copy to archive
    # ══════════════════════════════════════════════════════════════════════
    swc_cache = Path(cache_dir) / f"specimen_{sid}" / "reconstruction.swc"
    swc_cache.parent.mkdir(parents=True, exist_ok=True)
    ctc.get_reconstruction(sid, file_name=str(swc_cache))
    swc_dst = cell_dir / "reconstruction.swc"
    shutil.copy2(swc_cache, swc_dst)
    if verbose:
        print(f"[archive]   SWC → {swc_dst.name}")

    # ══════════════════════════════════════════════════════════════════════
    # C.  ELECTROPHYSIOLOGY  —  raw waveform extraction
    # ══════════════════════════════════════════════════════════════════════
    data_set = ctc.get_ephys_data(sid)
    sweeps_meta = ctc.get_ephys_sweeps(sid)

    # ── C1.  Square Subthreshold: individual pulse windows ───────────────
    #
    # Each detected pulse becomes one entry in all_pulses, carrying its own
    # (t, v, i) window, polarity tag, and provenance fields.  These are the
    # ATOMIC units for downstream averaging, splitting, and bootstrap.

    ss_meta = _select_square_subthreshold(sweeps_meta)
    all_pulses: List[Dict[str, Any]] = []
    ss_sweep_numbers: List[int] = []
    ss_n_raw = 0

    for sm in ss_meta:
        sn = int(sm["sweep_number"])
        sw = data_set.get_sweep(sn)
        idx = sw.get("index_range", (0, len(sw["response"]) - 1))

        v_full = (
            np.asarray(sw["response"][idx[0] : idx[1] + 1], dtype=np.float64)
            * 1e3
            + ljp_correction_mV
        )
        i_full = (
            np.asarray(sw["stimulus"][idx[0] : idx[1] + 1], dtype=np.float64)
            * 1e12
        )
        sr = float(sw["sampling_rate"])

        # Detect all pulses in the current waveform
        pulses_raw = _detect_pulses_in_current(
            i_full, sr, threshold_pA=pulse_threshold_pA
        )
        ss_n_raw += len(pulses_raw)

        # Amplitude / duration QC  — identical logic to _build_subthreshold_bundles
        pulses_qc: List[Tuple[int, int, float]] = []
        for s_i, e_i, peak in pulses_raw:
            dur = (e_i - s_i) / sr
            if abs(abs(peak) - expected_amplitude_pA) > amplitude_tol_pA:
                continue
            if abs(dur - expected_duration_s) > duration_tol_s:
                continue
            pulses_qc.append((s_i, e_i, peak))

        # Extract voltage/current windows
        windows = _extract_windows_around_pulses(
            v_full, i_full, sr, pulses_qc,
            pre_ms=pre_ms, post_ms=post_ms,
        )
        for w in windows:
            w["sampling_rate_Hz"] = sr
            w["sweep_number"] = sn

        all_pulses.extend(windows)
        ss_sweep_numbers.append(sn)

        if verbose:
            nd = sum(1 for w in windows if w["polarity"] == "dep")
            nh = sum(1 for w in windows if w["polarity"] == "hyp")
            print(
                f"[archive]   SS sweep {sn}: "
                f"{len(pulses_raw)} detected → {len(pulses_qc)} QC'd "
                f"(dep={nd}, hyp={nh})"
            )

    n_dep = sum(1 for w in all_pulses if w["polarity"] == "dep")
    n_hyp = len(all_pulses) - n_dep

    # ── Save ss_pulses.npz ──────────────────────────────────────────────
    #
    # Format A (normal case — all windows same length):
    #   t:               (n_samples,)           shared time vector, t=0 at onset
    #   v:               (n_pulses, n_samples)  mV, LJP-corrected
    #   i_pA:            (n_pulses, n_samples)  pA
    #   polarity_is_dep: (n_pulses,)            bool
    #   peak_pA:         (n_pulses,)            float64
    #   stim_duration_s: (n_pulses,)            float64
    #   sampling_rate_Hz:(n_pulses,)            float64
    #   sweep_number:    (n_pulses,)            int64
    #
    # Format B (rare — variable window lengths across sampling rates):
    #   same 1-D metadata arrays, plus per-pulse t_k / v_k / i_k arrays
    #   and variable_length = True flag.

    ss_stacked = False
    if all_pulses:
        lengths = set(len(w["t"]) for w in all_pulses)
        if len(lengths) == 1:
            # ── Format A: stack into 2-D arrays ──
            ss_stacked = True
            ss_data = {
                "t": all_pulses[0]["t"].astype(np.float64),
                "v": np.stack(
                    [w["v"] for w in all_pulses]
                ).astype(np.float64),
                "i_pA": np.stack(
                    [w["i"] for w in all_pulses]
                ).astype(np.float64),
                "polarity_is_dep": np.array(
                    [w["polarity"] == "dep" for w in all_pulses], dtype=bool
                ),
                "peak_pA": np.array(
                    [w["peak_pA"] for w in all_pulses], dtype=np.float64
                ),
                "stim_duration_s": np.array(
                    [w["stim_duration_s"] for w in all_pulses], dtype=np.float64
                ),
                "sampling_rate_Hz": np.array(
                    [w["sampling_rate_Hz"] for w in all_pulses], dtype=np.float64
                ),
                "sweep_number": np.array(
                    [w["sweep_number"] for w in all_pulses], dtype=np.int64
                ),
            }
        else:
            # ── Format B: variable-length windows (index individually) ──
            ss_data: Dict[str, np.ndarray] = {
                "n_pulses": np.array([len(all_pulses)], dtype=np.int64),
                "variable_length": np.array([True], dtype=bool),
                "polarity_is_dep": np.array(
                    [w["polarity"] == "dep" for w in all_pulses], dtype=bool
                ),
                "peak_pA": np.array(
                    [w["peak_pA"] for w in all_pulses], dtype=np.float64
                ),
                "stim_duration_s": np.array(
                    [w["stim_duration_s"] for w in all_pulses], dtype=np.float64
                ),
                "sampling_rate_Hz": np.array(
                    [w["sampling_rate_Hz"] for w in all_pulses], dtype=np.float64
                ),
                "sweep_number": np.array(
                    [w["sweep_number"] for w in all_pulses], dtype=np.int64
                ),
            }
            for k, w in enumerate(all_pulses):
                ss_data[f"t_{k}"] = w["t"].astype(np.float64)
                ss_data[f"v_{k}"] = w["v"].astype(np.float64)
                ss_data[f"i_{k}"] = w["i"].astype(np.float64)

        np.savez_compressed(cell_dir / "ss_pulses.npz", **ss_data)
        if verbose:
            print(
                f"[archive]   SS saved: {len(all_pulses)} pulses "
                f"(dep={n_dep}, hyp={n_hyp}, "
                f"{'stacked' if ss_stacked else 'indexed'})"
            )

    # ── C2.  Long Square: individual sweep traces ────────────────────────
    #
    # Every Long Square sweep is saved individually — including both hyper-
    # and depolarising, and both sub- and suprathreshold.  Downstream code
    # on HPC filters by detected_amplitude_pA.
    #
    # npz layout (variable-length sweeps, indexed by k):
    #   n_sweeps:  (1,)            int
    #   v_k:       (n_samples_k,)  mV, LJP-corrected
    #   i_k:       (n_samples_k,)  pA
    #
    # Per-sweep scalar metadata (sampling_rate_Hz, detected_amplitude_pA,
    # sweep_number, stimulus_name, n_samples) is stored in metadata.json
    # under ls_sweeps[k], NOT in the npz.

    ls_meta_list = _select_long_square_subthreshold(sweeps_meta)
    ls_sweep_info: List[Dict[str, Any]] = []
    ls_arrays: Dict[str, np.ndarray] = {}

    for sm in ls_meta_list:
        sn = int(sm["sweep_number"])
        try:
            sw = data_set.get_sweep(sn)
            idx = sw.get("index_range", (0, len(sw["response"]) - 1))

            v_mV = (
                np.asarray(
                    sw["response"][idx[0] : idx[1] + 1], dtype=np.float64
                )
                * 1e3
                + ljp_correction_mV
            )
            i_pA = (
                np.asarray(
                    sw["stimulus"][idx[0] : idx[1] + 1], dtype=np.float64
                )
                * 1e12
            )
            sr = float(sw["sampling_rate"])
            amp = _detect_step_amplitude(i_pA, sr)

            k = len(ls_sweep_info)
            ls_arrays[f"v_{k}"] = v_mV
            ls_arrays[f"i_{k}"] = i_pA

            ls_sweep_info.append(
                {
                    "index": k,
                    "sweep_number": sn,
                    "detected_amplitude_pA": (
                        float(amp) if np.isfinite(amp) else None
                    ),
                    "sampling_rate_Hz": sr,
                    "stimulus_name": sm.get("stimulus_name", ""),
                    "n_samples": len(v_mV),
                }
            )

            if verbose:
                amp_s = f"{amp:+.0f}" if np.isfinite(amp) else "N/A"
                print(
                    f"[archive]   LS sweep {sn}: amp={amp_s} pA, "
                    f"{len(v_mV)} samples"
                )

        except Exception as e:
            if verbose:
                print(f"[archive]   LS sweep {sn}: FAILED ({e})")

    if ls_sweep_info:
        ls_arrays["n_sweeps"] = np.array([len(ls_sweep_info)], dtype=np.int64)
        np.savez_compressed(cell_dir / "ls_sweeps.npz", **ls_arrays)
        if verbose:
            print(f"[archive]   LS saved: {len(ls_sweep_info)} sweeps")

    # ══════════════════════════════════════════════════════════════════════
    # D.  SCALAR FEATURES  (Rin, τm, Vrest from Allen Cell Feature Summary)
    # ══════════════════════════════════════════════════════════════════════
    all_feats = ctc.get_ephys_features()
    feats = next(
        (f for f in all_feats if f.get("specimen_id") == sid), {}
    )

    rin = float(feats.get("input_resistance_mohm", np.nan))
    tau = float(feats.get("tau", np.nan))
    if 0 < tau < 1:
        tau *= 1e3  # seconds → milliseconds
    vrest = float(feats.get("vrest", np.nan))
    if not np.isnan(vrest):
        vrest += ljp_correction_mV

    if verbose:
        print(
            f"[archive]   scalars: Rin={rin:.1f} MΩ  "
            f"τm={tau:.1f} ms  Vrest={vrest:.1f} mV"
        )

    # ══════════════════════════════════════════════════════════════════════
    # E.  SMOKE TEST
    # ══════════════════════════════════════════════════════════════════════
    #
    # Verify the archived data is sufficient to run Phase 2 by:
    #   1. Building a NEURON model from the saved SWC
    #   2. Reconstructing a CellData (with n_avg_groups=1) from the pulse pool
    #   3. Calling prepare_optimiser_inputs
    #   4. Running one quick NEURON simulation

    smoke: Dict[str, Any] = {}

    try:
        # E1.  NEURON model from the archived SWC ──────────────────────
        cell = build_neuron_model(swc_dst, F=F)
        smoke["build_neuron_model_ok"] = True
        smoke["n_sections"] = {
            "soma": len(cell.soma),
            "dend": len(cell.dend),
            "apic": len(cell.apic),
            "axon": len(cell.axon),
        }

        # E2.  Reconstruct CellData from archived data ────────────────
        cd = _reconstruct_cell_data_for_smoke_test(
            sid=sid,
            meta=meta,
            swc_path=swc_dst,
            all_pulses=all_pulses,
            ls_sweep_info=ls_sweep_info,
            ls_arrays=ls_arrays,
            rin=rin,
            tau=tau,
            vrest=vrest,
            ljp_correction_mV=ljp_correction_mV,
        )
        smoke["reconstruct_cell_data_ok"] = True
        smoke["n_ss_bundles"] = len(cd.square_subthreshold)
        smoke["n_ls_bundles"] = len(cd.long_square_subthreshold)

        # E3.  Prepare optimiser inputs ────────────────────────────────
        oi = prepare_optimiser_inputs(cd, fit_target=fit_target)
        smoke["prepare_optimiser_inputs_ok"] = True
        smoke["n_train_bundles"] = len(oi.train_bundles)
        smoke["n_validation_bundles"] = len(oi.validation_bundles)

        # E4.  Quick NEURON simulation (nominal parameters) ────────────
        if oi.train_bundles and not np.isnan(vrest):
            cell.set_passive(1.0, 20000.0, 150.0)
            cell.set_e_pas(vrest)
            b = oi.train_bundles[0]
            t_sim, v_sim = _simulate_square_subthreshold(cell, b, vrest)
            smoke["simulation_ok"] = True
            smoke["sim_n_points"] = len(t_sim)
        else:
            smoke["simulation_ok"] = False
            smoke["simulation_note"] = "no train bundles or vrest is NaN"

        if verbose:
            secs = smoke["n_sections"]
            print(
                f"[archive]   SMOKE TEST: OK — "
                f"sections={secs['soma']}s/{secs['dend']}d/"
                f"{secs['apic']}a/{secs['axon']}ax  "
                f"train={smoke.get('n_train_bundles', '?')}  "
                f"valid={smoke.get('n_validation_bundles', '?')}"
            )

    except Exception as e:
        smoke["error"] = f"{type(e).__name__}: {e}"
        if verbose:
            print(f"[archive]   SMOKE TEST FAILED: {e}")

    # ══════════════════════════════════════════════════════════════════════
    # F.  SAVE METADATA.JSON
    # ══════════════════════════════════════════════════════════════════════

    # Make the full Allen metadata dict JSON-safe
    meta_json: Dict[str, Any] = {}
    for k, v in meta.items():
        if isinstance(v, (int, float, str, bool, type(None))):
            meta_json[k] = v
        elif isinstance(v, (np.integer,)):
            meta_json[k] = int(v)
        elif isinstance(v, (np.floating,)):
            meta_json[k] = float(v) if np.isfinite(v) else None
        else:
            meta_json[k] = str(v)

    metadata = {
        # ── Top-level identity and scalars ──
        "specimen_id": sid,
        "layer": str(meta.get("structure_layer_name", "")),
        "dendrite_type": str(meta.get("dendrite_type", "")),
        "donor_id": str(meta.get("donor__id", meta.get("donor_id", ""))),
        "structure_area_abbrev": str(meta.get("structure_area_abbrev", "")),
        "rin_MOhm": float(rin) if np.isfinite(rin) else None,
        "tau_ms": float(tau) if np.isfinite(tau) else None,
        "v_rest_mV": float(vrest) if np.isfinite(vrest) else None,
        "ljp_correction_mV": ljp_correction_mV,
        # ── Full Allen metadata (for provenance / debugging) ──
        "full_allen_metadata": meta_json,
        # ── SS extraction provenance ──
        "ss_extraction": {
            "sweep_numbers": ss_sweep_numbers,
            "n_pulses_raw": ss_n_raw,
            "n_pulses_qc": len(all_pulses),
            "n_dep": n_dep,
            "n_hyp": n_hyp,
            "pre_ms": pre_ms,
            "post_ms": post_ms,
            "pulse_threshold_pA": pulse_threshold_pA,
            "expected_amplitude_pA": expected_amplitude_pA,
            "amplitude_tol_pA": amplitude_tol_pA,
            "expected_duration_s": expected_duration_s,
            "duration_tol_s": duration_tol_s,
            "stacked": ss_stacked,
        },
        # ── LS sweep provenance (per-sweep scalar metadata) ──
        "ls_sweeps": ls_sweep_info,
        # ── Smoke test results ──
        "smoke_test": smoke,
    }

    with open(cell_dir / "metadata.json", "w") as f:
        json.dump(metadata, f, indent=2, cls=_NumpyEncoder)

    status = "ok" if smoke.get("simulation_ok") else "partial"
    if verbose:
        print(f"[archive]   → status: {status}\n")

    return status


# ═══════════════════════════════════════════════════════════════════════════════
#  SMOKE-TEST HELPER  —  reconstruct CellData from archived pulse/sweep pools
# ═══════════════════════════════════════════════════════════════════════════════
#
# This is a minimal reconstruction with n_avg_groups=1 (one averaged bundle
# per polarity for SS, one bundle per distinct amplitude for LS).  The HPC
# loader will implement a more flexible version of the same logic with
# user-chosen n_avg_groups and amplitude filtering.


def _reconstruct_cell_data_for_smoke_test(
    sid: int,
    meta: Dict[str, Any],
    swc_path: Path,
    all_pulses: List[Dict[str, Any]],
    ls_sweep_info: List[Dict[str, Any]],
    ls_arrays: Dict[str, np.ndarray],
    rin: float,
    tau: float,
    vrest: float,
    ljp_correction_mV: float,
) -> "CellData":
    """Build a CellData from the raw pulse/sweep pool (n_avg_groups=1).

    This mirrors what the HPC loader will do, but with fixed n_avg_groups=1
    and standard amplitude filtering — just enough to verify the data is
    usable for Phase 2.
    """
    # ── SS bundles: average all pulses of each polarity into one bundle ──
    ss_bundles: List[SweepBundle] = []
    for pol in ("dep", "hyp"):
        group = [w for w in all_pulses if w["polarity"] == pol]
        if not group:
            continue

        v_avg = np.mean([w["v"] for w in group], axis=0)
        i_avg = np.mean([w["i"] for w in group], axis=0)
        t_ref = group[0]["t"]
        sr = group[0]["sampling_rate_Hz"]
        amp = float(np.mean([w["peak_pA"] for w in group]))
        stim_dur = float(np.mean([w["stim_duration_s"] for w in group]))

        ss_bundles.append(
            SweepBundle(
                polarity=pol,
                amplitude_pA=amp,
                t=t_ref,
                v_mV=v_avg,
                i_pA=i_avg,
                stim_onset_s=0.0,
                stim_duration_s=stim_dur,
                n_repeats_averaged=len(group),
                sweep_numbers=sorted(
                    set(w.get("sweep_number", -1) for w in group)
                ),
                sampling_rate_Hz=sr,
                stimulus_name="Square Subthreshold",
            )
        )

    # ── LS bundles: group hyp subthreshold sweeps by amplitude ───────────
    #
    # Replicate the same logic as load_allen_data:
    #   1. Keep only hyperpolarising sweeps with |amp| <= 100 pA
    #      (fallback to 300 pA if none found)
    #   2. Group by amplitude rounded to nearest 10 pA
    #   3. Average within group, detect onset from averaged current

    ls_bundles: List[SweepBundle] = []

    hyp_ls = [
        info
        for info in ls_sweep_info
        if info["detected_amplitude_pA"] is not None
        and info["detected_amplitude_pA"] < -5.0
        and abs(info["detected_amplitude_pA"]) <= 100.0
    ]
    if not hyp_ls:
        # Fallback: accept up to 300 pA
        hyp_ls = [
            info
            for info in ls_sweep_info
            if info["detected_amplitude_pA"] is not None
            and info["detected_amplitude_pA"] < -5.0
            and abs(info["detected_amplitude_pA"]) <= 300.0
        ]

    # Group by amplitude (round to nearest 10 pA)
    amp_groups: Dict[int, List[Dict[str, Any]]] = {}
    for info in hyp_ls:
        key = int(round(info["detected_amplitude_pA"] / 10.0) * 10)
        amp_groups.setdefault(key, []).append(info)

    for amp_key in sorted(amp_groups.keys(), key=abs):
        group = amp_groups[amp_key]

        # Load per-sweep arrays from the ls_arrays dict
        vs = [ls_arrays[f"v_{info['index']}"] for info in group]
        is_ = [ls_arrays[f"i_{info['index']}"] for info in group]
        srs = [info["sampling_rate_Hz"] for info in group]

        # Average (clip to shortest sweep if lengths differ)
        n_min = min(len(v) for v in vs)
        v_avg = np.mean([v[:n_min] for v in vs], axis=0)
        i_avg = np.mean([i[:n_min] for i in is_], axis=0)
        sr = srs[0]
        t = np.arange(n_min) / sr

        # Detect stimulus onset/duration from averaged current
        thr = 0.5 * np.max(np.abs(i_avg))
        active = np.abs(i_avg) > thr
        if active.any():
            on_idx = int(np.argmax(active))
            off_idx = len(active) - int(np.argmax(active[::-1]))
            stim_onset = float(t[on_idx])
            stim_dur = float(t[off_idx - 1] - t[on_idx])
            amp_pA = float(np.sign(i_avg[on_idx]) * np.max(np.abs(i_avg)))
        else:
            stim_onset = stim_dur = amp_pA = 0.0

        ls_bundles.append(
            SweepBundle(
                polarity="hyp",
                amplitude_pA=amp_pA,
                t=t,
                v_mV=v_avg,
                i_pA=i_avg,
                stim_onset_s=stim_onset,
                stim_duration_s=stim_dur,
                n_repeats_averaged=len(group),
                sweep_numbers=[info["sweep_number"] for info in group],
                sampling_rate_Hz=sr,
                stimulus_name="Long Square",
            )
        )

    return CellData(
        specimen_id=sid,
        metadata=meta,
        swc_path=Path(swc_path),
        square_subthreshold=ss_bundles,
        long_square_subthreshold=ls_bundles,
        rin_MOhm=rin,
        tau_ms=tau,
        v_rest_mV=vrest,
        ljp_correction_mV=ljp_correction_mV,
        n_avg_groups=1,
        ss_individual_pulses=all_pulses,
    )

