# %% Cell 5 — RMSD helpers =====================================================
def _estimate_residual_noise_at_mle(
    cell,
    train_bundles,
    v_rest_mV: float,
    cm_mle: float,
    rm_mle: float,
    ra_mle: float,
    train_window_ms: Sequence[float],
    pre_stim_window_ms: Optional[Sequence[float]] = None,
) -> Tuple[float, float]:
    """Pool residuals from (pre-stim baseline + post-stim training window)
    at MLE and return (σ, ρ_lag1).

    The simulate call below MUST mirror whatever `_build_loss_function`
    does internally — for the existing pipeline that means setting the
    passive parameters, running NEURON, and producing a voltage trace on
    the bundle's time grid. Adapt the one annotated line below if your
    PassiveCell exposes a different method name or signature.
    """
    # ── Set MLE state ────────────────────────────────────────────────
    cell.set_passive(cm_mle, rm_mle, ra_mle)
    cell.set_e_pas(v_rest_mV)

    # ── Window definitions in seconds ────────────────────────────────
    train_t0_s, train_t1_s = (np.asarray(train_window_ms) * 1e-3).tolist()

    if pre_stim_window_ms is None:
        # Default: 50 ms ending 1 ms before the training window onset.
        pre_t0_s = (train_window_ms[0] - 50.0) * 1e-3
        pre_t1_s = (train_window_ms[0] - 1.0) * 1e-3
    else:
        pre_t0_s, pre_t1_s = (np.asarray(pre_stim_window_ms) * 1e-3).tolist()

    # ── Loop over bundles, accumulate residuals ──────────────────────
    pooled: List[float] = []
    for b in train_bundles:
        # Mirror exactly what _build_loss_function does internally
        t_sim_s, v_sim_raw = _simulate_square_subthreshold(cell, b, v_rest_mV)
        sim_v = _interp_to_grid(t_sim_s, v_sim_raw, b.t)

        # In the Square-Subthreshold convention, t=0 is the pulse onset,
        # so the pre-stim window is t < 0 (negative times on b.t).
        pre_mask  = (b.t >= pre_t0_s) & (b.t <= pre_t1_s)
        post_mask = (b.t >= train_t0_s) & (b.t <= train_t1_s)
        if (not pre_mask.any()) or (not post_mask.any()):
            continue

        baseline_data = float(b.v_mV[pre_mask].mean())   # → b.v_mV
        baseline_sim  = float(sim_v[pre_mask].mean())

        train_res = ((b.v_mV[post_mask] - baseline_data) -    # → b.v_mV
                    (sim_v[post_mask]    - baseline_sim))
        pre_res   = b.v_mV[pre_mask] - baseline_data     # → b.v_mV
        pooled.extend(pre_res.tolist())
        pooled.extend(train_res.tolist())

    pool = np.asarray(pooled, dtype=float)
    if pool.size < 3:
        return float("nan"), float("nan")

    sigma = float(np.std(pool, ddof=1))
    c     = pool - pool.mean()
    rho   = float(np.sum(c[:-1] * c[1:]) / (np.sum(c**2) + 1e-12))
    return sigma, rho
def _crop_window(t: np.ndarray, v: np.ndarray,
                 t_start_s: float, t_end_s: float) -> np.ndarray:
    """Return v[mask] for t ∈ [t_start_s, t_end_s].  No interpolation."""
    mask = (t >= t_start_s) & (t <= t_end_s)
    return v[mask]


def _interp_to_grid(t_src: np.ndarray, v_src: np.ndarray,
                    t_dst: np.ndarray) -> np.ndarray:
    """Linear interpolate v_src onto t_dst.  NEURON's variable timestep can
    produce a slightly different grid than the experimental sweep, so we
    interpolate before computing point-wise RMSD."""
    return np.interp(t_dst, t_src, v_src)


def _baseline_subtracted_rmsd(
    t_exp_s: np.ndarray, v_exp_mV: np.ndarray,
    t_sim_s: np.ndarray, v_sim_mV: np.ndarray,
    pre_window_s: Tuple[float, float],
    rmsd_window_s: Tuple[float, float],
) -> float:
    """RMSD between experimental and simulated traces in `rmsd_window_s`,
    after subtracting each trace's own pre-window mean.

    The baseline-subtraction step removes any small offset between
    experimental Vrest and simulated Vrest (the latter is set by e_pas).
    This isolates the SHAPE of the transient as the fit target, which is
    the appropriate thing to fit because the absolute baseline is set by an
    independent measurement (Allen Cell Feature Summary's Vrest), not by
    the Square-Subthreshold experiment.
    """
    # Per-trace baselines from their respective pre-windows
    base_exp = float(np.mean(_crop_window(t_exp_s, v_exp_mV, *pre_window_s)))
    base_sim = float(np.mean(_crop_window(t_sim_s, v_sim_mV, *pre_window_s)))
    v_exp_zero = v_exp_mV - base_exp
    v_sim_zero = v_sim_mV - base_sim

    # Crop both to the RMSD window, on the experimental time grid
    mask_exp = (t_exp_s >= rmsd_window_s[0]) & (t_exp_s <= rmsd_window_s[1])
    if mask_exp.sum() < 2:
        return float("inf")
    t_grid = t_exp_s[mask_exp]
    v_exp_window = v_exp_zero[mask_exp]
    v_sim_window = _interp_to_grid(t_sim_s, v_sim_zero, t_grid)

    diff = v_exp_window - v_sim_window
    return float(np.sqrt(np.mean(diff * diff)))


# %% Cell 6 — Stimulus replay helpers ==========================================
def _simulate_square_subthreshold(
    cell: PassiveCell, bundle: SweepBundle, v_rest_mV: float,
    pre_pad_ms: float = 10.0, post_pad_ms: float = 100.0,
    dt_ms: float = 0.025,
) -> Tuple[np.ndarray, np.ndarray]:
    """Replay a Square-Subthreshold pulse on the model.

    The bundle's ``t = 0`` is the pulse onset.  We start the simulation
    `pre_pad_ms` before the pulse so the model has a settled baseline
    matching the experimental pre-pulse window.
    """
    delay_ms = pre_pad_ms
    dur_ms = bundle.stim_duration_s * 1e3
    tstop_ms = pre_pad_ms + dur_ms + post_pad_ms

    t_sim_ms, v_sim = cell.simulate(
        stim_amp_pA=bundle.amplitude_pA,
        stim_delay_ms=delay_ms,
        stim_dur_ms=dur_ms,
        tstop_ms=tstop_ms,
        v_init_mV=v_rest_mV,
        dt_ms=dt_ms,
    )
    # Shift so t = 0 is the pulse onset, matching the bundle's convention
    t_sim_s = t_sim_ms * 1e-3 - pre_pad_ms * 1e-3
    return t_sim_s, v_sim


def _simulate_long_square(
    cell: PassiveCell, bundle: SweepBundle, v_rest_mV: float,
    dt_ms: float = 0.1,
) -> Tuple[np.ndarray, np.ndarray]:
    """Replay a Long-Square step on the model using the bundle's own
    onset/duration metadata.  Returns ``(t_sim_s, v_sim_mV)`` on the
    simulation's time grid (will be interpolated to the bundle grid by
    the RMSD helper)."""
    delay_ms = bundle.stim_onset_s * 1e3
    dur_ms = bundle.stim_duration_s * 1e3
    tstop_ms = float(bundle.t[-1]) * 1e3

    t_sim_ms, v_sim = cell.simulate(
        stim_amp_pA=bundle.amplitude_pA,
        stim_delay_ms=delay_ms,
        stim_dur_ms=dur_ms,
        tstop_ms=tstop_ms,
        v_init_mV=v_rest_mV,
        dt_ms=dt_ms,
    )
    t_sim_s = t_sim_ms * 1e-3
    return t_sim_s, v_sim


# %% Cell 6b — Bundle-protocol detection and labelling =========================
# A "bundle" can be either a Square Subthreshold pulse (~0.5 ms duration) or
# a Long Square step (~1 s duration).  The stim_duration_s field is a clean
# discriminator; we use 0.01 s (10 ms) as the boundary because no real Allen
# protocol falls between 0.5 ms and 1 s.

_BRIEF_PULSE_MAX_DURATION_S = 0.01


def _is_brief_pulse(bundle: SweepBundle) -> bool:
    """True if the bundle is a Square Subthreshold-style brief pulse,
    False if it is a Long Square-style sustained step."""
    return bundle.stim_duration_s < _BRIEF_PULSE_MAX_DURATION_S


def _bundle_label(bundle: SweepBundle) -> str:
    """Short human-readable label used in the per-bundle RMSD breakdown."""
    if _is_brief_pulse(bundle):
        return f"SS {bundle.polarity} ({bundle.amplitude_pA:+.0f} pA)"
    return f"LS ({bundle.amplitude_pA:+.0f} pA)"


# %% Cell 6c — Pre-stimulus noise estimator ====================================
def _estimate_noise(
    bundle: SweepBundle,
    rho_threshold: float = 0.5,
) -> Tuple[float, float, bool]:
    """Estimate background noise from a bundle's pre-stimulus baseline.

    The pre-stimulus segment is the part of the trace where the cell is at
    steady state, before the current pulse / step begins.  For Square
    Subthreshold bundles this is t < 0; for Long Square bundles it is
    t < stim_onset_s.

    Two statistics are returned:

      * ``sigma_mV``  -- standard deviation of the pre-pulse voltage
                         samples.  This is the residual noise on the
                         AVERAGED trace and is the σ that later goes
                         into χ² thresholds for profile-likelihood CIs.
      * ``rho_lag1``  -- lag-1 autocorrelation coefficient of the same
                         samples.  A 50 kHz signal filtered at 10 kHz with
                         a Bessel filter has correlated samples at the
                         original sampling rate; averaging across many
                         pulses tends to whiten this, but residual
                         correlation can survive.  When ρ is high, the
                         effective sample size for any noise-based
                         calculation is reduced by roughly (1-ρ)/(1+ρ).

    The ``is_correlated`` flag (third return value) is True iff
    ``rho_lag1 > rho_threshold`` (default 0.5).  Phase 2 uses this only as
    a warning; downstream post-processing (CI computation in Phase 3)
    should apply the n_eff correction when this flag is set.

    Notes
    -----
    Only the pre-stimulus segment is used because that is where the system
    is genuinely at steady state.  Post-stimulus samples reflect the
    decaying capacitive transient (Square Subthreshold) or the active
    current step plus Ih sag (Long Square), neither of which is noise.
    """
    if _is_brief_pulse(bundle):
        # Square Subthreshold convention: t = 0 is the pulse onset, so
        # everything with t < 0 is pre-stimulus baseline.
        pre_window = (float(bundle.t[0]), 0.0)
    else:
        # Long Square: pre-step is t in [0, stim_onset_s).  We trim a
        # 5 ms guard at the very end to avoid any onset transient bleed.
        end_s = max(float(bundle.t[0]), bundle.stim_onset_s - 5e-3)
        pre_window = (float(bundle.t[0]), end_s)

    mask = (bundle.t >= pre_window[0]) & (bundle.t < pre_window[1])
    samples = np.asarray(bundle.v_mV[mask], dtype=float)

    if len(samples) < 10:
        return float("nan"), float("nan"), False

    sigma_mV = float(np.std(samples, ddof=1))

    # Lag-1 autocorrelation
    s_centered = samples - samples.mean()
    var = float(np.mean(s_centered * s_centered))
    if var <= 0:
        rho_lag1 = float("nan")
    else:
        rho_lag1 = float(np.mean(s_centered[1:] * s_centered[:-1]) / var)

    is_correlated = (not np.isnan(rho_lag1)) and (rho_lag1 > rho_threshold)
    return sigma_mV, rho_lag1, is_correlated


# %% Cell 6d — Per-bundle validation RMSD ======================================
def _rmsd_for_validation_bundle(
    cell: PassiveCell,
    bundle: SweepBundle,
    v_rest_mV: float,
    valid_window_ms_after_onset: float,
) -> float:
    """Compute baseline-subtracted RMSD between simulated and experimental
    voltage on ONE validation bundle, applying the appropriate window for
    the bundle's protocol type.

    Square Subthreshold bundles use the 1-100 ms post-pulse window (same as
    training).  Long Square bundles use an early window of
    ``valid_window_ms_after_onset`` ms after step onset, to minimise Ih
    contamination in the validation signal.
    """
    if _is_brief_pulse(bundle):
        t_sim_s, v_sim = _simulate_square_subthreshold(cell, bundle, v_rest_mV)
        pre_window_s = (-10e-3, 0.0)
        rmsd_window_s = (1e-3, 100e-3)
    else:
        t_sim_s, v_sim = _simulate_long_square(cell, bundle, v_rest_mV)
        pre_window_s = (0.0, bundle.stim_onset_s)
        early_end_s = bundle.stim_onset_s + valid_window_ms_after_onset * 1e-3
        rmsd_window_s = (0.0, early_end_s)

    return _baseline_subtracted_rmsd(
        bundle.t, bundle.v_mV, t_sim_s, v_sim,
        pre_window_s=pre_window_s,
        rmsd_window_s=rmsd_window_s,
    )


# %% Cell 7 — Loss function builder ============================================
def _build_loss_function(
    cell: PassiveCell,
    train_bundles: List[SweepBundle],
    v_rest_mV: float,
    train_window_ms: Tuple[float, float],
    pre_window_ms: Tuple[float, float] = (-10.0, 0.0),
) -> Callable[[float, float, float], float]:
    """Return a function ``loss(Cm, Rm, Ra) -> RMSD_mV`` that gp_minimize
    can call.  The loss is the mean baseline-subtracted RMSD over all
    training bundles (averaging gives equal weight regardless of polarity
    when ``fit_target='both'``)."""
    pre_window_s = (pre_window_ms[0] * 1e-3, pre_window_ms[1] * 1e-3)
    train_window_s = (train_window_ms[0] * 1e-3, train_window_ms[1] * 1e-3)

    def loss(cm_log: float, rm_log: float, ra_log: float) -> float:
        # The optimiser works in log-space (q = log(p)).  Convert back to
        # physical units before calling NEURON: p = exp(q).
        try:
            cm = float(np.exp(cm_log))
            rm = float(np.exp(rm_log))
            ra = float(np.exp(ra_log))
            cell.set_passive(Cm=cm, Rm=rm, Ra=ra)
            cell.set_e_pas(v_rest_mV)
            rmsds = []
            for b in train_bundles:
                t_sim_s, v_sim = _simulate_square_subthreshold(
                    cell, b, v_rest_mV)
                r = _baseline_subtracted_rmsd(
                    b.t, b.v_mV, t_sim_s, v_sim,
                    pre_window_s=pre_window_s,
                    rmsd_window_s=train_window_s,
                )
                if not np.isfinite(r):
                    return 1e6   # large but finite: tells GP the region is bad
                rmsds.append(r)
            return float(np.mean(rmsds))
        except Exception as e:
            # Never let a NEURON exception crash gp_minimize.  Penalise
            # heavily so the surrogate steers away from the offending region.
            warnings.warn(f"Loss evaluation crashed at "
                          f"(log_Cm={cm_log:.3g}, log_Rm={rm_log:.3g}, "
                          f"log_Ra={ra_log:.3g}): {e}")
            return 1e6

    return loss


# %% Cell 8 — GP-posterior uncertainty extraction ==============================
def _gp_parameter_uncertainty(
    optim_result,
    delta_mV: float,
    n_samples: int,
    seed: int,
) -> Dict[str, float]:
    """Extract per-parameter uncertainty from the trained Gaussian-process
    surrogate.

    Method: draw ``n_samples`` from the search space (uniform on the
    transformed scale used by the GP), predict their loss with the GP,
    keep those whose predicted loss is within ``opt_loss + delta_mV`` of
    the global optimum, and return the standard deviation of the kept
    samples along each parameter axis.

    This is essentially a "profile-likelihood" estimate computed on the
    surrogate rather than on the full simulator — extremely cheap
    (no extra NEURON calls) and gives directly interpretable per-parameter
    one-sigma values.

    Parameters
    ----------
    seed
        Plain ``int`` (NOT a numpy ``Generator``).  ``skopt.Space.rvs``
        forwards ``random_state`` to ``sklearn.utils.check_random_state``,
        which only accepts ``int``, ``None``, or the legacy
        ``numpy.random.RandomState`` — passing a new-style ``Generator``
        raises ``ValueError``.

    Returns
    -------
    {"cm_sigma": ..., "rm_sigma": ..., "ra_sigma": ...}
    All values are NaN if the extraction fails for any reason — this
    function never raises, because the optimiser is more important than
    the uncertainty estimate.
    """
    nan_result = {"cm_sigma": float("nan"),
                  "rm_sigma": float("nan"),
                  "ra_sigma": float("nan")}
    try:
        gp = optim_result.models[-1]
        space = optim_result.space
    except (AttributeError, IndexError):
        return nan_result

    try:
        # skopt.Space.rvs(random_state=...) accepts int / None / RandomState,
        # but NOT numpy.random.Generator.  Pass an int to stay compatible
        # across scikit-learn versions.
        samples_orig = space.rvs(n_samples=n_samples, random_state=int(seed))
        samples_trans = space.transform(samples_orig)
        pred_loss = gp.predict(samples_trans)
    except Exception as e:
        warnings.warn(f"GP-posterior uncertainty extraction failed: "
                      f"{type(e).__name__}: {e}")
        return nan_result

    opt_loss = float(optim_result.fun)
    keep_mask = pred_loss <= (opt_loss + delta_mV)
    kept = np.asarray(samples_orig)[keep_mask]

    if len(kept) < 5:
        # Surrogate too sharp around the optimum; return zero rather than
        # an unstable estimate.
        return {"cm_sigma": 0.0, "rm_sigma": 0.0, "ra_sigma": 0.0}

    # kept[:, i] are log-space values, so std is a log-space standard
    # deviation.  Interpretation: the physical parameter lies in the
    # approximate 68% interval [exp(q_opt - sigma), exp(q_opt + sigma)],
    # equivalently [p_opt * exp(-sigma), p_opt * exp(+sigma)].
    # This is a multiplicative (log-normal) uncertainty, which is the
    # natural representation for positive scale parameters.
    return {
        "cm_sigma": float(np.std(kept[:, 0], ddof=1)),
        "rm_sigma": float(np.std(kept[:, 1], ddof=1)),
        "ra_sigma": float(np.std(kept[:, 2], ddof=1)),
    }


# %% Cell 9 — Validation status classifier =====================================
def _classify_fit(
    train_rmsd: float,
    valid_rmsd: float,
    k_good: float,
    k_fail: float,
    train_rmsd_fail_mV: float,
    valid_rmsd_good_mV: float = DEFAULT_VALID_RMSD_GOOD_MV,
) -> str:
    """Map the (train_rmsd, valid_rmsd) pair onto a status label.

    Logic, applied in order:
      1. If training itself was poor (absolute train RMSD > ceiling),
         the fit failed regardless of validation.  This catches cells
         where the optimiser could not get close to the data at all.
      2. If validation is dimensionally tiny (≤ ``valid_rmsd_good_mV``),
         accept as "good" regardless of the train/valid ratio.  This
         escape-hatch handles the regime where the optimiser fits the
         training data extraordinarily tightly and the ratio becomes
         dominated by tiny floor-level noise rather than real model error.
      3. If validation is within k_good× of training, accept as good.
      4. If validation is moderately worse (within k_fail×), flag for
         refinement.
      5. Otherwise, fail.
    """
    if not np.isfinite(train_rmsd) or train_rmsd > train_rmsd_fail_mV:
        return "failed"
    if not np.isfinite(valid_rmsd):
        return "failed"
    if valid_rmsd <= valid_rmsd_good_mV:
        return "good"
    ratio = valid_rmsd / max(train_rmsd, 1e-9)
    if ratio <= k_good:
        return "good"
    if ratio <= k_fail:
        return "to_refine"
    return "failed"


# %% Cell 10 — Single-cell driver: fit_one_cell ================================
def fit_one_cell(
    cell: PassiveCell,
    cell_data: CellData,
    opt_in: OptimiserInputs,
    *,
    F: float = 1.9,
    n_calls: int = DEFAULT_N_CALLS,
    n_initial: int = DEFAULT_N_INITIAL,
    acq_func: str = DEFAULT_ACQ_FUNC,
    valid_window_ms_after_onset: float = DEFAULT_VALID_WINDOW_MS_AFTER_ONSET,
    k_good: float = DEFAULT_K_GOOD,
    k_fail: float = DEFAULT_K_FAIL,
    train_rmsd_fail_mV: float = DEFAULT_TRAIN_RMSD_FAIL_MV,
    valid_rmsd_good_mV: float = DEFAULT_VALID_RMSD_GOOD_MV,
    uncertainty_delta_mV: float = DEFAULT_UNCERTAINTY_DELTA_MV,
    uncertainty_n_samples: int = DEFAULT_UNCERTAINTY_N_SAMPLES,
    seed: int = 0,
    verbose: bool = True,
) -> PassiveFitResult:
    """Fit (Cm, Rm, Ra) for ONE cell and return a populated
    :class:`PassiveFitResult`.

    Workflow
    --------
    1. Build the loss function from the training bundles.
    2. Run ``gp_minimize`` with named-argument decoration so the loss can
       receive ``(Cm, Rm, Ra)`` as keyword arguments matching the
       ``Real(name=...)`` dimensions returned by :class:`PassiveSearchSpace`.
    3. Apply the best-fit parameters to the model and replay the validation
       Long-Square bundle; compute baseline-subtracted RMSD over the early
       window of the step (``valid_window_ms_after_onset`` after step onset)
       to minimise Ih contamination.
    4. Extract per-parameter GP-posterior uncertainty.
    5. Classify the fit status and assemble the result.

    All hard failures (e.g. NEURON crashes the optimiser cannot recover
    from) are caught and returned as ``status="failed"`` with the exception
    message in ``error_message``, so a batch driver never aborts.
    """
    t_start = time.perf_counter()
    sid = int(cell_data.specimen_id)
    layer = str(cell_data.metadata.get("structure_layer_name", "?"))
    dt_type = str(cell_data.metadata.get("dendrite_type", "?"))

    # Defensive: even though Phase 1 enforces this, double-check before the
    # expensive optimisation step.
    if not opt_in.train_bundles:
        return PassiveFitResult(
            specimen_id=sid, layer=layer, dendrite_type=dt_type, F=F,
            fit_target="?",
            cm_uF_per_cm2=np.nan, rm_Ohm_cm2=np.nan, ra_Ohm_cm=np.nan,
            cm_sigma=np.nan, rm_sigma=np.nan, ra_sigma=np.nan,
            train_rmsd_mV=np.nan, valid_rmsd_mV=np.nan,
            valid_to_train_ratio=np.nan,
            rin_MOhm_allen=opt_in.rin_MOhm, tau_ms_allen=opt_in.tau_ms,
            v_rest_mV=opt_in.v_rest_mV,
            validation_status="failed",
            n_calls=0, n_initial=0,
            wall_time_s=time.perf_counter() - t_start,
            error_message="No training bundles in OptimiserInputs.",
        )

    # Infer fit_target from the training bundle list
    polarities = sorted({b.polarity for b in opt_in.train_bundles})
    fit_target = polarities[0] if len(polarities) == 1 else "both"

    if verbose:
        print(f"[fit_one_cell] specimen {sid} (L{layer} {dt_type}, F={F}): "
              f"fit_target={fit_target}, n_calls={n_calls}")

    # --- Build loss --------------------------------------------------------
    loss_raw = _build_loss_function(
        cell=cell,
        train_bundles=opt_in.train_bundles,
        v_rest_mV=opt_in.v_rest_mV,
        train_window_ms=opt_in.train_window_ms,
    )

    # gp_minimize expects a function taking a list of params; use_named_args
    # bridges to the named-Real dimensions.
    dims = opt_in.skopt_dimensions

    @use_named_args(dims)
    def objective(**params) -> float:
        return loss_raw(params["Cm"], params["Rm"], params["Ra"])

    # --- Run gp_minimize ---------------------------------------------------
    try:
        result = gp_minimize(
            objective,
            dimensions=dims,
            n_calls=n_calls,
            n_initial_points=n_initial,
            acq_func=acq_func,
            random_state=seed,
            verbose=False,
        )
    except Exception as e:
        return PassiveFitResult(
            specimen_id=sid, layer=layer, dendrite_type=dt_type, F=F,
            fit_target=fit_target,
            cm_uF_per_cm2=np.nan, rm_Ohm_cm2=np.nan, ra_Ohm_cm=np.nan,
            cm_sigma=np.nan, rm_sigma=np.nan, ra_sigma=np.nan,
            train_rmsd_mV=np.nan, valid_rmsd_mV=np.nan,
            valid_to_train_ratio=np.nan,
            rin_MOhm_allen=opt_in.rin_MOhm, tau_ms_allen=opt_in.tau_ms,
            v_rest_mV=opt_in.v_rest_mV,
            validation_status="failed",
            n_calls=n_calls, n_initial=n_initial,
            wall_time_s=time.perf_counter() - t_start,
            error_message=f"gp_minimize crashed: {type(e).__name__}: {e}",
        )

    # result.x contains the log-space optima: exp() to recover physical units.
    cm_opt = float(np.exp(result.x[0]))
    rm_opt = float(np.exp(result.x[1]))
    ra_opt = float(np.exp(result.x[2]))
    train_rmsd = float(result.fun)
    if verbose:
        print(f"[fit_one_cell]   best fit: Cm={cm_opt:.3f}  "
              f"Rm={rm_opt:.0f}  Ra={ra_opt:.0f}  train RMSD={train_rmsd:.4f} mV"
              f"  [log-space: {result.x[0]:.3f}, {result.x[1]:.3f}, "
              f"{result.x[2]:.3f}]")

    # --- Noise estimation on TRAINING bundles -----------------------------
    # Computed once after the fit (not used by gp_minimize itself); needed
    # downstream by Phase 3 / post-processing for χ² thresholds in
    # profile-likelihood confidence intervals.  See uploaded
    # "Bayesian Optimisation and Likelihood Profile" notes.
    noise_sigmas = []
    noise_rhos = []
    noise_high_flags = []
    for tb in opt_in.train_bundles:
        sigma, rho, high = _estimate_noise(tb)
        noise_sigmas.append(sigma)
        noise_rhos.append(rho)
        noise_high_flags.append(high)

    # nanmean handles bundles where the pre-window was too short to estimate
    if noise_sigmas:
        with warnings.catch_warnings():
            # nanmean on all-NaN array warns; we want a quiet NaN result.
            warnings.simplefilter("ignore", category=RuntimeWarning)
            noise_sigma_mV = float(np.nanmean(noise_sigmas))
            noise_rho_lag1 = float(np.nanmean(noise_rhos))
    else:
        noise_sigma_mV = float("nan")
        noise_rho_lag1 = float("nan")
    noise_rho_high = bool(any(noise_high_flags))

    if verbose:
        if not np.isnan(noise_sigma_mV):
            print(f"[fit_one_cell]   training noise: "
                  f"σ={noise_sigma_mV:.4f} mV  ρ_lag1={noise_rho_lag1:.3f}  "
                  f"({len(opt_in.train_bundles)} bundle(s))")
        if noise_rho_high:
            print(f"[fit_one_cell]   WARNING: ρ_lag1 > threshold -- "
                  f"effective sample size reduced; apply n_eff correction "
                  f"before computing CIs in post-processing.")

    # --- Validate over ALL validation bundles (equal-weight mean) ----------
    valid_rmsd = float("nan")
    valid_per_bundle: List[Tuple[str, float]] = []
    if opt_in.validation_bundles:
        try:
            cell.set_passive(Cm=cm_opt, Rm=rm_opt, Ra=ra_opt)
            cell.set_e_pas(opt_in.v_rest_mV)
            for vb in opt_in.validation_bundles:
                try:
                    r = _rmsd_for_validation_bundle(
                        cell, vb, opt_in.v_rest_mV,
                        valid_window_ms_after_onset=valid_window_ms_after_onset,
                    )
                    valid_per_bundle.append((_bundle_label(vb), float(r)))
                except Exception as e_inner:
                    warnings.warn(
                        f"Validation simulation failed for bundle "
                        f"{_bundle_label(vb)} on cell {sid}: {e_inner}")
            finite = [r for _, r in valid_per_bundle if np.isfinite(r)]
            if finite:
                valid_rmsd = float(np.mean(finite))   # equal-weight mean
        except Exception as e:
            warnings.warn(f"Validation block failed for cell {sid}: {e}")

        if verbose and valid_per_bundle:
            print(f"[fit_one_cell]   validation per-bundle RMSDs:")
            for label, r in valid_per_bundle:
                print(f"      {label:24s}  {r:.4f} mV")
            print(f"[fit_one_cell]   validation mean RMSD "
                  f"({len(valid_per_bundle)} bundle(s)): {valid_rmsd:.4f} mV")

    ratio = valid_rmsd / max(train_rmsd, 1e-9) if np.isfinite(valid_rmsd) else np.inf
    status = _classify_fit(
        train_rmsd, valid_rmsd, k_good, k_fail, train_rmsd_fail_mV,
        valid_rmsd_good_mV=valid_rmsd_good_mV,
    )

    if verbose:
        print(f"[fit_one_cell]   ratio = {ratio:.2f}  ->  status = {status}")

    # --- GP-posterior uncertainty -----------------------------------------
    # Pass int seed (not a numpy Generator) — see _gp_parameter_uncertainty
    # docstring for the reason.
    sigmas = _gp_parameter_uncertainty(
        result, delta_mV=uncertainty_delta_mV,
        n_samples=uncertainty_n_samples, seed=seed + 1,
    )

    # Convergence trace for plotting (running min of loss)
    loss_history = list(np.minimum.accumulate(result.func_vals).tolist())


    # Residual noise at MLE for Phase 3 v2 parametric bootstrap
    try:
        residuals_sigma_mV, residuals_rho_lag1 = _estimate_residual_noise_at_mle(
            cell=cell,
            train_bundles=opt_in.train_bundles,
            v_rest_mV=opt_in.v_rest_mV,
            cm_mle=cm_opt, rm_mle=rm_opt, ra_mle=ra_opt,
            train_window_ms=opt_in.train_window_ms,
            pre_stim_window_ms=getattr(opt_in, "pre_stim_window_ms", None),
        )
    except Exception as _e:
        warnings.warn(f"[fit_one_cell] residual-noise estimation failed: {_e}")
        residuals_sigma_mV, residuals_rho_lag1 = float("nan"), float("nan")




    return PassiveFitResult(
        specimen_id=sid, layer=layer, dendrite_type=dt_type, F=F,
        fit_target=fit_target,
        cm_uF_per_cm2=cm_opt, rm_Ohm_cm2=rm_opt, ra_Ohm_cm=ra_opt,
        cm_sigma=sigmas["cm_sigma"],
        rm_sigma=sigmas["rm_sigma"],
        ra_sigma=sigmas["ra_sigma"],
        train_rmsd_mV=train_rmsd,
        valid_rmsd_mV=valid_rmsd,
        valid_to_train_ratio=ratio,
        rin_MOhm_allen=opt_in.rin_MOhm,
        tau_ms_allen=opt_in.tau_ms,
        v_rest_mV=opt_in.v_rest_mV,
        validation_status=status,
        n_calls=n_calls, n_initial=n_initial,
        wall_time_s=time.perf_counter() - t_start,
        error_message="",
        loss_history=loss_history,
        noise_sigma_mV=noise_sigma_mV,
        noise_rho_lag1=noise_rho_lag1,

        noise_rho_lag1_high=noise_rho_high,
        valid_rmsd_per_bundle=valid_per_bundle,
        n_validation_bundles=len(valid_per_bundle),
        residuals_sigma_mV=residuals_sigma_mV,
        residuals_rho_lag1=residuals_rho_lag1,
        # ----- Phase-3 hand-off -----
        gp_result=result,
        neuron_cell=cell,
        opt_inputs=opt_in,
    )


# %% Cell 11 — Multiprocessing worker (top-level for picklability) =============
def _fit_worker(args) -> PassiveFitResult:
    """Worker entry point for multiprocessing.Pool.  Each worker rebuilds
    its own NEURON model from the SWC path because NEURON sections are
    process-global and cannot be safely pickled across processes.

    gp_result and opt_inputs ARE picklable (sklearn / plain-Python objects)
    and are preserved so the parent can run Phase 3 after calling
    rebuild_neuron_cells_for_phase3().  neuron_cell holds NEURON Hoc handles
    and CANNOT be pickled; it is cleared to None before the result leaves
    the worker process.
    """
    (cell_data, opt_in, F, kwargs) = args
    # from phase1_data_loader import build_neuron_model
    cell = build_neuron_model(cell_data.swc_path, F=F)
    r = fit_one_cell(cell, cell_data, opt_in, F=F, **kwargs)
    # Drop the un-picklable NEURON cell before the result crosses the
    # process boundary.  gp_result and opt_inputs are left intact.
    r.neuron_cell = None
    return r


# %% Cell 12 — Batch driver: fit_cells =========================================
def fit_cells(
    cells_data: Sequence[CellData],
    opt_inputs: Sequence[OptimiserInputs],
    *,
    cells: Optional[Sequence[PassiveCell]] = None,
    F: float = 1.9,
    n_workers: int = 1,
    return_results: bool = True,
    **fit_kwargs,
) -> Tuple[pd.DataFrame, List[PassiveFitResult]]:
    """Fit a list of cells sequentially (default) or in parallel across
    worker processes.

    Parameters
    ----------
    cells_data, opt_inputs
        Lists of equal length, one entry per cell.  ``cells_data`` carries
        the morphology path and metadata; ``opt_inputs`` carries the
        training/validation bundles and the search space.
    cells
        Optional list of pre-built ``PassiveCell`` objects.  Used only when
        ``n_workers == 1`` (sequential execution); ignored otherwise because
        NEURON cells cannot be pickled across processes.  When ``None``, the
        sequential path also rebuilds each cell from its SWC.
    F
        Spine-area correction factor, applied uniformly to all cells in the
        call.  For mixed populations (e.g. spiny + aspiny in one call), set
        this on the basis of the dendrite type of the GROUP, not per cell —
        or call ``fit_cells`` separately per group.
    n_workers
        ``1`` (default): sequential within node.  ``> 1``: distribute cells
        across worker processes via ``multiprocessing.Pool``.  Each worker
        runs its own NEURON kernel; pick ``n_workers`` to match available
        CPU cores on the HPC node.
    fit_kwargs
        Forwarded to :func:`fit_one_cell` (n_calls, n_initial, acq_func,
        thresholds, etc.).

    Returns
    -------
    (results_df, results_list)
        ``results_df`` is a tidy DataFrame with one row per cell and
        provenance + parameter + status columns.  ``results_list`` is the
        full list of :class:`PassiveFitResult` objects (also includes the
        per-cell ``loss_history``, useful for plotting convergence curves).
    """
    if len(cells_data) != len(opt_inputs):
        raise ValueError(f"cells_data ({len(cells_data)}) and opt_inputs "
                         f"({len(opt_inputs)}) must have equal length.")

    results: List[PassiveFitResult] = []

    if n_workers <= 1:
        # Sequential path
        # from phase1_data_loader import build_neuron_model
        for i, (cd, oi) in enumerate(zip(cells_data, opt_inputs)):
            print(f"\n[fit_cells] === cell {i + 1}/{len(cells_data)} "
                  f"(specimen {cd.specimen_id}) ===")
            try:
                if cells is not None and i < len(cells):
                    pc = cells[i]
                else:
                    pc = build_neuron_model(cd.swc_path, F=F)
                r = fit_one_cell(pc, cd, oi, F=F, **fit_kwargs)
            except Exception as e:
                # Bare-minimum row so the batch never aborts
                r = PassiveFitResult(
                    specimen_id=int(cd.specimen_id),
                    layer=str(cd.metadata.get("structure_layer_name", "?")),
                    dendrite_type=str(cd.metadata.get("dendrite_type", "?")),
                    F=F, fit_target="?",
                    cm_uF_per_cm2=np.nan, rm_Ohm_cm2=np.nan, ra_Ohm_cm=np.nan,
                    cm_sigma=np.nan, rm_sigma=np.nan, ra_sigma=np.nan,
                    train_rmsd_mV=np.nan, valid_rmsd_mV=np.nan,
                    valid_to_train_ratio=np.nan,
                    rin_MOhm_allen=oi.rin_MOhm, tau_ms_allen=oi.tau_ms,
                    v_rest_mV=oi.v_rest_mV,
                    validation_status="failed",
                    n_calls=0, n_initial=0, wall_time_s=0.0,
                    error_message=f"{type(e).__name__}: {e}",
                )
            results.append(r)
    else:
        # Parallel path — multiprocessing.Pool
        import multiprocessing as mp
        ctx = mp.get_context("spawn")  # safer than fork with NEURON
        args_list = [(cd, oi, F, dict(fit_kwargs))
                     for cd, oi in zip(cells_data, opt_inputs)]
        with ctx.Pool(processes=n_workers) as pool:
            for i, r in enumerate(pool.imap_unordered(_fit_worker, args_list)):
                print(f"[fit_cells] worker returned {i + 1}/"
                      f"{len(cells_data)} (specimen {r.specimen_id}, "
                      f"status={r.validation_status})")
                results.append(r)

    # Tidy DataFrame
    rows = []
    for r in results:
        # asdict() calls copy.deepcopy on every field before returning.
        # The three Phase-3 hand-off fields hold live objects (NEURON Hoc
        # handles, sklearn GP, OptimiserInputs) that deepcopy cannot traverse.
        # dataclasses.replace() creates a shallow copy with only those three
        # fields nulled out — the originals in `results` are untouched and
        # still carry the live objects for Phase 3.
        import dataclasses
        r_export = dataclasses.replace(r,
                                       neuron_cell=None,
                                       gp_result=None,
                                       opt_inputs=None)
        d = asdict(r_export)
        d.pop("loss_history", None)
        d.pop("valid_rmsd_per_bundle", None)
        rows.append(d)
    df = pd.DataFrame(rows)

    if return_results:
        return df, results
    return df, []
def rebuild_neuron_cells_for_phase3(
    results: List[PassiveFitResult],
    cells_data: Sequence["CellData"],
    F: float,
    *,
    statuses: Sequence[str] = ("good", "to_refine"),
    verbose: bool = True,
) -> None:
    """Rebuild PassiveCell objects in the *parent* process after fit_cells().

    When fit_cells() is run with n_workers > 1, each PassiveFitResult comes
    back from the worker with neuron_cell=None (NEURON Hoc objects cannot
    cross process boundaries).  gp_result and opt_inputs ARE already
    populated by the worker.  This function fills in the missing
    neuron_cell field so that profile_likelihood_for_cell() can call the
    real NEURON simulator for CI boundary validation.

    Parameters
    ----------
    results
        The List[PassiveFitResult] returned by fit_cells().
        Modified **in-place**: neuron_cell is set on qualifying results.
    cells_data
        The same Sequence[CellData] passed to fit_cells().  Used to look
        up the SWC path for each specimen.
    F
        Spine-area correction factor (same value used in fit_cells()).
    statuses
        Only cells whose validation_status is in this set get a
        neuron_cell rebuild.  "failed" cells are always skipped because
        their best-fit parameters are NaN.
    verbose
        Print a short summary line when done.
    """
    # from phase1_data_loader import build_neuron_model

    sid_to_cd = {int(cd.specimen_id): cd for cd in cells_data}
    n_rebuilt = 0

    for r in results:
        if r.validation_status not in statuses:
            continue
        if r.gp_result is None:
            # Fit failed before the GP was trained (e.g. no training bundles).
            continue

        cd = sid_to_cd.get(int(r.specimen_id))
        if cd is None:
            warnings.warn(
                f"[rebuild_neuron_cells_for_phase3] specimen {r.specimen_id} "
                f"not found in cells_data — skipping."
            )
            continue

        try:
            cell = build_neuron_model(cd.swc_path, F=float(r.F))
            # Pre-apply best-fit parameters so the cell is already in the
            # right state when Phase 3's loss function first evaluates it.
            cell.set_passive(r.cm_uF_per_cm2, r.rm_Ohm_cm2, r.ra_Ohm_cm)
            cell.set_e_pas(r.v_rest_mV)
            r.neuron_cell = cell
            n_rebuilt += 1
        except Exception as e:
            warnings.warn(
                f"[rebuild_neuron_cells_for_phase3] failed for specimen "
                f"{r.specimen_id}: {type(e).__name__}: {e}"
            )

    if verbose:
        print(f"[rebuild_neuron_cells_for_phase3] rebuilt {n_rebuilt} "
              f"NEURON model(s) out of {len(results)} result(s).")

# %% Cell 13 — Population aggregator ===========================================
def aggregate_population(
    df: pd.DataFrame,
    *,
    group_by: Sequence[str] = ("layer", "dendrite_type"),
    accept_statuses: Sequence[str] = ("good",),
) -> pd.DataFrame:
    """Compute mean ± SD of (Cm, Rm, Ra) per group.

    Parameters
    ----------
    df
        Output of :func:`fit_cells`.
    group_by
        Columns to group on.  Default ``("layer", "dendrite_type")`` gives
        one row per (layer × cell-type) combination — exactly the
        granularity used in the project pipeline document for canonical
        parameter sets.
    accept_statuses
        Which validation statuses to include in the aggregation.  Default
        ``("good",)`` is conservative — rejects any cell that failed
        validation or needs refinement.  Pass ``("good", "to_refine")``
        to include cells flagged for Phase 3 refinement.

    Returns
    -------
    DataFrame with columns: ``[group cols], n, cm_mean, cm_std, rm_mean,
    rm_std, ra_mean, ra_std`` plus a ``status_summary`` showing the count
    of each status that contributed.
    """
    if df.empty:
        return df.copy()

    accepted = df[df["validation_status"].isin(accept_statuses)]
    if accepted.empty:
        warnings.warn(f"No cells matched accept_statuses={accept_statuses}; "
                      f"returning empty aggregation.")
        return pd.DataFrame()

    out_rows = []
    for keys, sub in accepted.groupby(list(group_by)):
        if not isinstance(keys, tuple):
            keys = (keys,)
        row = dict(zip(group_by, keys))
        row["n"] = int(len(sub))
        for p_col, p_label in [("cm_uF_per_cm2", "cm"),
                               ("rm_Ohm_cm2", "rm"),
                               ("ra_Ohm_cm", "ra")]:
            row[f"{p_label}_mean"] = float(sub[p_col].mean())
            row[f"{p_label}_std"] = float(sub[p_col].std(ddof=1)) \
                if len(sub) > 1 else 0.0
        # Status breakdown across the FULL group (incl. rejected)
        full_group = df.copy()
        for col, key in zip(group_by, keys):
            full_group = full_group[full_group[col] == key]
        status_counts = full_group["validation_status"].value_counts().to_dict()
        row["status_summary"] = "; ".join(
            f"{k}:{v}" for k, v in sorted(status_counts.items()))
        out_rows.append(row)

    return pd.DataFrame(out_rows)


# %% Cell 14 — Diagnostic plot helper ==========================================
def plot_fit_diagnostic(
    cell: PassiveCell,
    cell_data: CellData,
    opt_in: OptimiserInputs,
    result: PassiveFitResult,
    figsize: Tuple[float, float] = (13.0, 9.0),
    show: bool = True,
):
    """Diagnostic figure for one fitted cell, organised as a 2x2 grid.

        +-------------------------+-------------------------+
        | Training overlay        | SS held-out overlay     |
        | (fit polarity)          | (opposite polarity      |
        |                         |  -- empty if "both")    |
        +-------------------------+-------------------------+
        | LS validation overlay   | GP convergence          |
        | (smallest |amp| step)   | (loss vs iter, log y)   |
        +-------------------------+-------------------------+

    All three trace panels show experiment (black) vs. fitted model (red)
    with the per-protocol baseline-subtraction convention used by the loss
    function.  The fit/validation windows are highlighted as grey bands so
    the panels can be read at a glance.

    With ``fit_target="hyp"`` (default), validation_bundles holds the SS
    dep bundles followed by all LS bundles.  This plot dispatches on
    protocol type per bundle, so it works regardless of fit_target.
    """
    import matplotlib.pyplot as plt

    # Apply the fitted parameters to the model once.
    cell.set_passive(Cm=result.cm_uF_per_cm2,
                     Rm=result.rm_Ohm_cm2,
                     Ra=result.ra_Ohm_cm)
    cell.set_e_pas(opt_in.v_rest_mV)

    fig, axes = plt.subplots(2, 2, figsize=figsize)
    ax_train = axes[0, 0]
    ax_ss_val = axes[0, 1]
    ax_ls_val = axes[1, 0]
    ax_conv   = axes[1, 1]

    # ------------------------------------------------------------------
    # Helper that draws ONE bundle into ONE axis using the appropriate
    # simulator and baseline window for the protocol type.
    # ------------------------------------------------------------------
    def _overlay_bundle(ax, bundle: SweepBundle, panel_title_prefix: str,
                        rmsd_value: Optional[float] = None) -> None:
        if bundle is None:
            ax.text(0.5, 0.5, "no data for this panel",
                    ha="center", va="center",
                    transform=ax.transAxes, color="grey")
            ax.set_title(panel_title_prefix, fontsize=10)
            return

        if _is_brief_pulse(bundle):
            t_sim_s, v_sim = _simulate_square_subthreshold(
                cell, bundle, opt_in.v_rest_mV)
            pre_mask_exp = bundle.t < 0.0
            pre_mask_sim = t_sim_s < 0.0
            x_factor = 1e3
            x_label = "Time from pulse onset (ms)"
            band_lo, band_hi = opt_in.train_window_ms          # 1-100 ms
        else:
            t_sim_s, v_sim = _simulate_long_square(
                cell, bundle, opt_in.v_rest_mV)
            pre_mask_exp = bundle.t < bundle.stim_onset_s
            pre_mask_sim = t_sim_s < bundle.stim_onset_s
            x_factor = 1.0
            x_label = "Time from sweep start (s)"
            band_lo = 0.0
            band_hi = (bundle.stim_onset_s
                       + DEFAULT_VALID_WINDOW_MS_AFTER_ONSET * 1e-3)
            band_lo *= x_factor
            band_hi *= x_factor

        # Robust baselines: fall back to first sample if the pre-window is
        # empty for any reason (would have NaN'd the model trace before).
        base_exp = (bundle.v_mV[pre_mask_exp].mean()
                    if pre_mask_exp.any() else float(bundle.v_mV[0]))
        base_sim = (v_sim[pre_mask_sim].mean()
                    if pre_mask_sim.any() else float(v_sim[0]))

        v_exp_zero = bundle.v_mV - base_exp
        v_sim_zero = v_sim - base_sim

        ax.plot(bundle.t * x_factor, v_exp_zero, color="black",
                lw=1.0, label="experiment")
        ax.plot(t_sim_s * x_factor, v_sim_zero, color="tab:red",
                lw=1.0, label="model")
        ax.axvspan(band_lo, band_hi, color="0.9", alpha=0.4,
                   label="RMSD window")
        ax.legend(loc="best", fontsize=8, frameon=False)

        rmsd_str = (f"\nRMSD = {rmsd_value:.3f} mV"
                    if rmsd_value is not None and np.isfinite(rmsd_value)
                    else "")
        ax.set_title(f"{panel_title_prefix}{rmsd_str}", fontsize=10)
        ax.set_xlabel(x_label)
        ax.set_ylabel("V - baseline (mV)")

    # ------------------------------------------------------------------
    # Panel 1 (top-left): TRAINING -- first SS bundle in train_bundles
    # ------------------------------------------------------------------
    train_b = opt_in.train_bundles[0] if opt_in.train_bundles else None
    _overlay_bundle(
        ax_train,
        train_b,
        f"Training (SS, {train_b.polarity})" if train_b else "Training",
        rmsd_value=result.train_rmsd_mV,
    )

    # ------------------------------------------------------------------
    # Helper to look up the per-bundle validation RMSD by label
    # ------------------------------------------------------------------
    per_bundle_dict = dict(result.valid_rmsd_per_bundle or [])

    # ------------------------------------------------------------------
    # Panel 2 (top-right): SS HELD-OUT validation
    # First Square Subthreshold bundle in validation_bundles, if any.
    # Empty for fit_target="both".
    # ------------------------------------------------------------------
    ss_held_out = next(
        (b for b in opt_in.validation_bundles if _is_brief_pulse(b)),
        None,
    )
    if ss_held_out is not None:
        rmsd_val = per_bundle_dict.get(_bundle_label(ss_held_out))
        title = f"Validation: SS held-out ({ss_held_out.polarity}, " \
                f"{ss_held_out.amplitude_pA:+.0f} pA)"
    else:
        rmsd_val = None
        title = ('SS held-out validation (none -- '
                 'fit_target covers both polarities)')
    _overlay_bundle(ax_ss_val, ss_held_out, title, rmsd_value=rmsd_val)

    # ------------------------------------------------------------------
    # Panel 3 (bottom-left): LONG SQUARE validation
    # First non-brief bundle (smallest |amp| because the list is sorted).
    # ------------------------------------------------------------------
    ls_first = next(
        (b for b in opt_in.validation_bundles if not _is_brief_pulse(b)),
        None,
    )
    if ls_first is not None:
        rmsd_val = per_bundle_dict.get(_bundle_label(ls_first))
        title = f"Validation: LS ({ls_first.amplitude_pA:+.0f} pA, " \
                f"smallest |amp|)"
    else:
        rmsd_val = None
        title = "Long Square validation (none)"
    _overlay_bundle(ax_ls_val, ls_first, title, rmsd_value=rmsd_val)

    # ------------------------------------------------------------------
    # Panel 4 (bottom-right): GP convergence curve
    # ------------------------------------------------------------------
    if result.loss_history:
        ax_conv.plot(np.arange(1, len(result.loss_history) + 1),
                     result.loss_history, color="tab:blue")
        ax_conv.axvline(result.n_initial, color="0.6", lw=0.5,
                        linestyle=":", label="end of random init")
        ax_conv.legend(loc="best", fontsize=8, frameon=False)
        ax_conv.set_title(
            f"GP convergence\nfinal loss = {result.train_rmsd_mV:.4f} mV",
            fontsize=10,
        )
    ax_conv.set_xlabel("Iteration")
    ax_conv.set_ylabel("Best loss so far (mV)")
    ax_conv.set_yscale("log")

    # ------------------------------------------------------------------
    # Suptitle: parameters with GP-posterior uncertainties + summary stats
    # ------------------------------------------------------------------
    suptitle_lines = [
        f"Cell {result.specimen_id} -- L{result.layer} "
        f"{result.dendrite_type}  ({result.fit_target} fit, "
        f"F={result.F})",
        f"Cm = {result.cm_uF_per_cm2:.3f} +/- {result.cm_sigma:.3f}  |  "
        f"Rm = {result.rm_Ohm_cm2:.0f} +/- {result.rm_sigma:.0f}  |  "
        f"Ra = {result.ra_Ohm_cm:.0f} +/- {result.ra_sigma:.0f}",
        f"train RMSD = {result.train_rmsd_mV:.4f} mV  |  "
        f"validation mean RMSD = {result.valid_rmsd_mV:.4f} mV "
        f"({result.n_validation_bundles} bundle(s))  |  "
        f"status = {result.validation_status}",
    ]
    fig.suptitle("\n".join(suptitle_lines), fontsize=10, y=1.00)

    fig.tight_layout()
    if show:
        plt.show()
    return fig, axes


