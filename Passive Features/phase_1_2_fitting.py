    import matplotlib.pyplot as plt
    import matplotlib as mpl
    from matplotlib.collections import LineCollection
    from matplotlib.colors import Normalize
    from matplotlib.lines import Line2D

    # ----- Nested helper: 3D-point extraction --------------------------------
    def _get_section_3d(sec):
        """Return (xs, ys, zs, diams) from NEURON's 3D point store, or None
        if the section has fewer than 2 3D points (e.g. the Hay axon stub).
        """
        n = int(h.n3d(sec=sec))
        if n < 2:
            return None
        return (
            np.array([h.x3d(i, sec=sec) for i in range(n)]),
            np.array([h.y3d(i, sec=sec) for i in range(n)]),
            np.array([h.z3d(i, sec=sec) for i in range(n)]),
            np.array([h.diam3d(i, sec=sec) for i in range(n)]),
        )

    # ----- Nested helper: soma centroid --------------------------------------
    def _soma_centroid():
        pts = []
        for sec in cell.soma:
            pts3d = _get_section_3d(sec)
            if pts3d is not None:
                xs, ys, zs, _ = pts3d
                pts.extend(zip(xs, ys, zs))
        if not pts:
            return np.zeros(3)
        return np.mean(pts, axis=0)

    # ----- Nested helper: draw one category of sections ----------------------
    def _draw_sections(ax, sections, x_idx, y_idx, base_color):
        for sec in sections:
            pts3d = _get_section_3d(sec)
            if pts3d is None:
                continue
            coords = [pts3d[x_idx], pts3d[y_idx]]
            diams = pts3d[3]
            points = np.column_stack(coords)
            segs = np.stack([points[:-1], points[1:]], axis=1)
            lws = np.clip((diams[:-1] + diams[1:]) / 2 * diam_scale, 0.3, 5.0)

            if color_by == "F_factor":
                n_segs = len(segs)
                colors = []
                for k in range(n_segs):
                    frac = (k + 0.5) / n_segs
                    m = f_mults.get((sec.name(), round(frac, 6)),
                                    f_mults.get((sec.name(), 0.5), 1.0))
                    colors.append(f_cmap(f_norm(m)))
                lc = LineCollection(segs, linewidths=lws, colors=colors,
                                    zorder=2)
