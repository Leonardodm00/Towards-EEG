def plot_aligned_vs_jittered(df_aligned, df_jittered, jitter_matrix, x_offset=300.0):
    """
    Creates an interactive 3D plot comparing the aligned and jittered neuron skeletons.
    The jittered skeleton and its axis are displaced along the X dimension for clarity.
    """
    fig = go.Figure()

    # ---------------------------------------------------------
    # HELPER: Build Plotly Line Segments
    # ---------------------------------------------------------
    def build_segments(df):
        """Extracts parent-child nodes into a single list separated by None."""
        node_map = df.set_index('id')[['x', 'y', 'z']].to_dict('index')
        seg_x, seg_y, seg_z = [], [], []

        for _, row in df[df['p'] != -1].iterrows():
            pid = row['p']
            if pid in node_map:
                parent = node_map[pid]
                seg_x.extend([row['x'], parent['x'], None])
                seg_y.extend([row['y'], parent['y'], None])
                seg_z.extend([row['z'], parent['z'], None])

        return seg_x, seg_y, seg_z

    # ---------------------------------------------------------
    # TRACE 1: Strictly Aligned Skeleton (Original Position)
    # ---------------------------------------------------------
    ax, ay, az = build_segments(df_aligned)
    fig.add_trace(go.Scatter3d(
        x=ax, y=ay, z=az,
        mode='lines',
        line=dict(color='royalblue', width=2),
        opacity=0.5,
        name='Strictly Aligned',
        hoverinfo='skip'
    ))

    # ---------------------------------------------------------
    # TRACE 2: Jittered Skeleton (Displaced)
    # ---------------------------------------------------------
    jx, jy, jz = build_segments(df_jittered)

    # Apply the spatial offset to the X coordinates (ignoring 'None' separators)
    jx_offset = [x + x_offset if x is not None else None for x in jx]

    fig.add_trace(go.Scatter3d(
        x=jx_offset, y=jy, z=jz,
        mode='lines',
        line=dict(color='crimson', width=3),
        opacity=0.9,
        name='Jittered Morphology',
        hoverinfo='skip'
    ))

    # ---------------------------------------------------------
    # TRACE 3 & 4: The Axes
    # ---------------------------------------------------------
    # Find a good scale length based on the neuron's apical height
    axis_length = df_aligned['z'].max() if not np.isnan(df_aligned['z'].max()) else 500.0

    # True Z-Axis (Vertical, at origin)
    fig.add_trace(go.Scatter3d(
        x=[0, 0], y=[0, 0], z=[0, axis_length],
        mode='lines',
        line=dict(color='black', width=6, dash='dash'),
        name='True Z-Axis (0,0,1)'
    ))

    # Jittered Z-Axis (Displaced to match the jittered neuron)
    # We apply the exact same jitter matrix to the vertical unit vector
    j_z_vector = np.dot(np.array([0, 0, axis_length]), jitter_matrix.T)

    fig.add_trace(go.Scatter3d(
        x=[x_offset, x_offset + j_z_vector[0]], # Start at offset, point towards jittered trajectory
        y=[0, j_z_vector[1]],
        z=[0, j_z_vector[2]],
        mode='lines',
        line=dict(color='orange', width=6),
        name='Jittered Z-Axis'
    ))

    # ---------------------------------------------------------
    # LAYOUT
    # ---------------------------------------------------------
    fig.update_layout(
        title=f"Biological Jitter Validation (Offset: {x_offset} µm)",
        scene=dict(
            xaxis_title="X (um)",
            yaxis_title="Y (um)",
            zaxis_title="Z (um)",
            aspectmode='data',  # CRITICAL: Ensures 1:1:1 physical scaling
            bgcolor='white'
        ),
        width=1200,
        height=800,
        margin=dict(l=0, r=0, b=0, t=50),
        legend=dict(
            x=0.02, y=0.98,
            bgcolor="rgba(255, 255, 255, 0.8)",
            bordercolor="Black",
            borderwidth=1
        )
    )

    fig.show()


