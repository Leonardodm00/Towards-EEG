def rototranslate_and_jitter(input_dir,nid, metadata_df, k_neighbors=3, max_tilt_deg=10.0):
    """
    Loads a raw neuron CSV, calculates the base rototranslation matrix from the
    k-nearest neighbors, applies the alignment, and generates a second version
    with biological jitter (apical tilt + yaw).

    Parameters:
    - filepath: str, path to the raw neuron .csv file.
    - metadata_df: DataFrame, the alignment metadata loaded from CSV.
    - k_neighbors: int, number of nearest neighbors to average for alignment.
    - max_tilt_deg: float, maximum deflection angle for the apical trunk.

    Returns:
    - df_aligned: DataFrame, the strictly aligned neuron (scaled to um).
    - df_jittered: DataFrame, the aligned + jittered neuron (scaled to um).
    """

    # 1. Load the raw data
    filepath = os.path.join(input_dir, f"neuron_{nid}.csv")
    df = pd.read_csv(filepath)
    root_rows = df[df['p'] == -1]

    if root_rows.empty:
        raise ValueError(f"No root node (p == -1) found in {filepath}.")

    # Extract the target soma position in nanometers
    soma_pos = root_rows.iloc[0][['x', 'y', 'z']].values.astype(float)

    # ---------------------------------------------------------
    # STEP 1: CALCULATE THE NEIGHBORHOOD BASE ROTATION
    # ---------------------------------------------------------
    # Ensure rotation matrices in the metadata are properly formatted as arrays
    if isinstance(metadata_df['rotation_matrix'].iloc[0], str):
        metadata_df['rotation_matrix'] = metadata_df['rotation_matrix'].apply(ast.literal_eval)

    # Extract all reference somas from metadata
    reference_somas = metadata_df[['soma_x', 'soma_y', 'soma_z']].values

    # Calculate Euclidean distances to find the nearest neighbors
    distances = np.linalg.norm(reference_somas - soma_pos, axis=1)
    k_actual = min(k_neighbors, len(metadata_df))
    nearest_indices = np.argsort(distances)[:k_actual]
    nearest_metadata = metadata_df.iloc[nearest_indices]

    # Average the rotation matrices of the nearest neighbors
    matrices = np.array(nearest_metadata['rotation_matrix'].tolist())
    mean_matrix = R.from_matrix(matrices).mean().as_matrix()
    base_rotation_matrix = mean_matrix

    # ---------------------------------------------------------
    # STEP 2: BASE ROTOTRANSLATION (Strict Alignment)
    # ---------------------------------------------------------
    # Center the coordinates to the soma
    centered_coords = df[['x', 'y', 'z']].values - soma_pos

    # Apply the base alignment rotation matrix
    aligned_coords = np.dot(centered_coords, base_rotation_matrix.T)

    # Scale from nanometers to micrometers
    aligned_coords_um = aligned_coords / 1000.0

    # Create the strictly aligned DataFrame
    df_aligned = df.copy()
    df_aligned['x'] = aligned_coords_um[:, 0]
    df_aligned['y'] = aligned_coords_um[:, 1]
    df_aligned['z'] = aligned_coords_um[:, 2]

    # ---------------------------------------------------------
    # STEP 3: APPLY BIOLOGICAL JITTER
    # ---------------------------------------------------------
    # Convert max tilt to radians
    max_tilt_rad = np.radians(max_tilt_deg)

    # Draw random rotation angles
    tilt_x = np.random.uniform(-max_tilt_rad, max_tilt_rad)  # Pitch
    tilt_y = np.random.uniform(-max_tilt_rad, max_tilt_rad)  # Roll
    spin_z = np.random.uniform(0, 2 * np.pi)                 # Yaw

    # Construct a Scipy Rotation object using intrinsic Euler angles
    jitter_rot = R.from_euler('xyz', [tilt_x, tilt_y, spin_z], degrees=False)
    jitter_matrix = jitter_rot.as_matrix()

    # Apply the jitter matrix to the ALIGNED coordinates.
    jittered_coords_um = np.dot(aligned_coords_um, jitter_matrix.T)

    # Create the jittered DataFrame
    df_jittered = df.copy()
    df_jittered['x'] = jittered_coords_um[:, 0]
    df_jittered['y'] = jittered_coords_um[:, 1]
    df_jittered['z'] = jittered_coords_um[:, 2]

    # CRITICAL CHANGE: Now returning the matrix as well!
    return df_aligned, df_jittered, jitter_matrix
