def calculate_total_arbor_lengths(neuron_ids, input_dir='/content/drive/MyDrive/Colab Notebooks/Reconstructed neurons'):
    """
    Iteratively imports neurons and calculates their total arbor length.
    Uses vectorized NumPy operations for extremely fast Euclidean distance calculations.

    Args:
        neuron_ids (list): Array/List of neuron IDs to process.
        input_dir (str): Folder path containing the reconstructed CSVs.

    Returns:
        pd.DataFrame: A DataFrame containing the neuron IDs and their total lengths.
    """
    results = []

    print(f"📏 Calculating arbor lengths for {len(neuron_ids)} neurons...\n")

    for nid in neuron_ids:
        filepath = os.path.join(input_dir, f"neuron_{nid}.csv")

        # 1. Check if file exists
        if not os.path.exists(filepath):
            print(f"⚠️ File for neuron {nid} not found. Skipping.")
            results.append({'neuron_id': nid, 'total_length_nm': np.nan, 'total_length_um': np.nan})
            continue

        # 2. Load the DataFrame
        df = pd.read_csv(filepath)

        # Ensure required columns exist
        if not all(col in df.columns for col in ['id', 'p', 'x', 'y', 'z']):
            print(f"⚠️ Missing necessary coordinate/parent columns in {nid}. Skipping.")
            results.append({'neuron_id': nid, 'total_length_nm': np.nan, 'total_length_um': np.nan})
            continue

        # 3. Vectorized length calculation
        # Merge the dataframe with itself to align each node's coordinates with its parent's coordinates
        segments = df.merge(
            df[['id', 'x', 'y', 'z']],
            left_on='p',
            right_on='id',
            suffixes=('', '_parent')
        )

        # Calculate Euclidean distance for all valid segments at once
        distances = np.sqrt(
            (segments['x'] - segments['x_parent'])**2 +
            (segments['y'] - segments['y_parent'])**2 +
            (segments['z'] - segments['z_parent'])**2
        )

        # Sum all distances to get the total arbor length
        total_length_nm = distances.sum()
        total_length_um = total_length_nm / 1000  # Convert nanometers to micrometers

        print(f"✅ Neuron {nid}: {total_length_um:,.2f} μm")

        # 4. Store results
        results.append({
            'neuron_id': nid,
            'total_length_nm': total_length_nm,
            'total_length_um': total_length_um
        })

    # Return as a Pandas DataFrame for easy viewing and exporting
    return pd.DataFrame(results)


