import os
import numpy as np
import pandas as pd
import networkx as nx
import plotly.graph_objects as go
from plotly.subplots import make_subplots
from scipy.spatial.transform import Rotation as R

def align_and_plot_neurons_to_z_by_com(neuron_ids, input_dir='/content/drive/MyDrive/Colab Notebooks/Reconstructed neurons', percentile=95, show_plot=True):
    """
    Centers neurons, extracts the distal tail (>95th percentile), filters for Dendrites,
    reconstructs the hierarchy to isolate the largest single distal arbor,
    calculates the CoM alignment vector, rotates the neuron, and plots both
    the pre-rotation assessment and post-rotation result side-by-side.
    """
    aligned_neurons = {}

    print(f"📐 Aligning {len(neuron_ids)} neurons (Threshold: Top {100-percentile}% | Filter: Largest Dendritic Arbor | Vector: CoM only)...")

    for nid in neuron_ids:
        filepath = os.path.join(input_dir, f"neuron_{nid}.csv")

        if not os.path.exists(filepath):
            print(f"⚠️ File for neuron {nid} not found. Skipping.")
            continue

        df = pd.read_csv(filepath)

        # --- 1. Center the Neuron ---
        root_rows = df[df['p'] == -1]
        if root_rows.empty:
            print(f"⚠️ No root node (p=-1) found for {nid}. Skipping.")
            continue

        soma_id = root_rows.iloc[0]['id']
        soma_x, soma_y, soma_z = root_rows.iloc[0][['x', 'y', 'z']]
        df['x'] -= soma_x
        df['y'] -= soma_y
        df['z'] -= soma_z

        # --- 2. Calculate Distances & Isolate the 5% Tail ---
        coords = df[['x', 'y', 'z']].values
        distances = np.linalg.norm(coords, axis=1)
        distal_threshold = np.percentile(distances, percentile)
        distal_mask = distances >= distal_threshold

        # --- 3. Filter for Dendrites ---
        if 'annotated_type' not in df.columns:
            print(f"⚠️ 'annotated_type' missing for {nid}. Skipping.")
            continue

        dendrite_mask = df['annotated_type'].astype(str).str.contains('dendrite|apical|^1$', case=False, regex=True)
        candidate_nodes = set(df[distal_mask & dendrite_mask]['id'].values)

        if not candidate_nodes:
            print(f"⚠️ No distal dendrite points found for {nid}. Skipping.")
            continue

        # --- 4. Reconstruct Hierarchy (NetworkX) ---
        G = nx.DiGraph()
        G.add_nodes_from(df['id'])
        valid_edges = df[df['p'] != -1][['p', 'id']].values
        G.add_edges_from(valid_edges)

        primary_branches = list(G.successors(soma_id))
        largest_pool = []

        for pb in primary_branches:
            arbor_nodes = set(nx.descendants(G, pb))
            arbor_nodes.add(pb)
            pool = candidate_nodes.intersection(arbor_nodes)
            if len(pool) > len(largest_pool):
                largest_pool = list(pool)

        if len(largest_pool) < 5:
            print(f"⚠️ Not enough clustered points (<5) in the largest distal arbor for {nid}. Skipping.")
            continue

        pool_df = df[df['id'].isin(largest_pool)]
        distal_coords = pool_df[['x', 'y', 'z']].values

        # --- 5. Vector Calculation ---
        distal_center = np.mean(distal_coords, axis=0)
        v_com = distal_center / np.linalg.norm(distal_center)

        # --- 6. Calculate & Apply the Rotation ---
        z_axis = np.array([0.0, 0.0, 1.0])
        cross_prod = np.cross(v_com, z_axis)
        dot_prod = np.dot(v_com, z_axis)

        if np.linalg.norm(cross_prod) < 1e-6:
            rot_matrix = np.eye(3)
        else:
            angle = np.arccos(np.clip(dot_prod, -1.0, 1.0))
            rot_vec = (cross_prod / np.linalg.norm(cross_prod)) * angle
            rotation = R.from_rotvec(rot_vec)
            rot_matrix = rotation.as_matrix()

        rotated_coords = np.dot(coords, rot_matrix.T)

        # We also rotate the CoM vector itself so we can plot it in the new aligned space
        rotated_v_com = np.dot(v_com, rot_matrix.T)

        # --- 7. Plotting the Side-by-Side Assessment ---
        if show_plot:
            # Create a 1x2 subplot with 3D scenes
            fig = make_subplots(
                rows=1, cols=2,
                specs=[[{'type': 'scene'}, {'type': 'scene'}]],
                subplot_titles=(f"Pre-Rotation Assessment", f"Post-Rotation (Aligned to Z)")
            )

            # --- PANEL 1: PRE-ROTATION ---
            node_map = df.set_index('id')[['x', 'y', 'z']].to_dict('index')
            x_lines, y_lines, z_lines = [], [], []
            for _, row in df[df['p'] != -1].iterrows():
                pid = row['p']
                if pid in node_map:
                    parent = node_map[pid]
                    x_lines.extend([row['x'], parent['x'], None])
                    y_lines.extend([row['y'], parent['y'], None])
                    z_lines.extend([row['z'], parent['z'], None])

            fig.add_trace(go.Scatter3d(
                x=x_lines, y=y_lines, z=z_lines, mode='lines',
                line=dict(color='lightgrey', width=2), opacity=0.4, name='Original Skeleton', hoverinfo='skip'
            ), row=1, col=1)

            # Rejected Distal Points
            rejected_mask = distal_mask & (~df['id'].isin(largest_pool))
            rejected_df = df[rejected_mask]
            if not rejected_df.empty:
                fig.add_trace(go.Scatter3d(
                    x=rejected_df['x'], y=rejected_df['y'], z=rejected_df['z'],
                    mode='markers', marker=dict(size=2, color='orange', opacity=0.5),
                    name='Rejected Distal Points'
                ), row=1, col=1)

            # Chosen Distal Arbor Pool
            fig.add_trace(go.Scatter3d(
                x=pool_df['x'], y=pool_df['y'], z=pool_df['z'],
                mode='markers', marker=dict(size=4, color='crimson'),
                name=f'Target Arbor Pool (n={len(largest_pool)})'
            ), row=1, col=1)

            # Original CoM Vector
            scale = np.max(distances) * 0.95
            fig.add_trace(go.Scatter3d(
                x=[0, v_com[0]*scale], y=[0, v_com[1]*scale], z=[0, v_com[2]*scale],
                mode='lines+markers', line=dict(color='green', width=6),
                marker=dict(size=6, symbol='diamond'), name='Original CoM Vector'
            ), row=1, col=1)

            # --- PANEL 2: POST-ROTATION (ALIGNED) ---
            # Create a temporary dataframe just to easily map the rotated coordinates for Plotly
            df_rot = df.copy()
            df_rot['x'] = rotated_coords[:, 0]
            df_rot['y'] = rotated_coords[:, 1]
            df_rot['z'] = rotated_coords[:, 2]

            node_map_rot = df_rot.set_index('id')[['x', 'y', 'z']].to_dict('index')
            rx_lines, ry_lines, rz_lines = [], [], []
            for _, row in df_rot[df_rot['p'] != -1].iterrows():
                pid = row['p']
                if pid in node_map_rot:
                    parent = node_map_rot[pid]
                    rx_lines.extend([row['x'], parent['x'], None])
                    ry_lines.extend([row['y'], parent['y'], None])
                    rz_lines.extend([row['z'], parent['z'], None])

            fig.add_trace(go.Scatter3d(
                x=rx_lines, y=ry_lines, z=rz_lines, mode='lines',
                line=dict(color='royalblue', width=2), opacity=0.6, name='Aligned Skeleton', hoverinfo='skip'
            ), row=1, col=2)

            # Rotated CoM Vector (This should point perfectly straight up in the plot)
            fig.add_trace(go.Scatter3d(
                x=[0, rotated_v_com[0]*scale], y=[0, rotated_v_com[1]*scale], z=[0, rotated_v_com[2]*scale],
                mode='lines+markers', line=dict(color='green', width=6),
                marker=dict(size=6, symbol='diamond'), name='Aligned Z-Axis Vector'
            ), row=1, col=2)

            # Layout Setup for both subplots
            fig.update_layout(
                title=f"Neuron {nid} Alignment",
                scene=dict(
                    xaxis_title='X (nm)', yaxis_title='Y (nm)', zaxis_title='Z (nm)',
                    aspectmode='data', bgcolor='white'
                ),
                scene2=dict(
                    xaxis_title='X (nm)', yaxis_title='Y (nm)', zaxis_title='Z (nm)',
                    aspectmode='data', bgcolor='white'
                ),
                height=700, width=1400, margin=dict(l=0, r=0, b=0, t=50)
            )
            fig.show()

        # --- 8. Finalize Data Update ---
        df['x'] = rotated_coords[:, 0]
        df['y'] = rotated_coords[:, 1]
        df['z'] = rotated_coords[:, 2]

        aligned_neurons[nid] = df
        print(f"✅ Neuron {nid} successfully rotated to Z-axis using CoM.\n")

    return aligned_neurons




