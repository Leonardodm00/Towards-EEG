def plot_centered_neuron_gallery(neuron_ids, input_dir='/content/drive/MyDrive/Colab Notebooks/Reconstructed neurons', shift_nm=400000):
    """
    Loads, centers, and sequentially shifts neurons for visualization in a single Plotly 3D space.

    Args:
        neuron_ids (list): List of neuron IDs to process.
        input_dir (str): Folder path containing the reconstructed CSVs.
        shift_nm (float): Distance to shift each subsequent neuron (default 100,000 nm = 100 um).
    """
    fig = go.Figure()

    # Generate a robust color palette capable of handling many neurons
    colors = pc.qualitative.Alphabet * (len(neuron_ids) // 26 + 1)

    valid_count = 0

    for nid in neuron_ids:
        filepath = os.path.join(input_dir, f"neuron_{nid}.csv")

        if not os.path.exists(filepath):
            print(f"⚠️ File for neuron {nid} not found. Skipping.")
            continue

        df = pd.read_csv(filepath)
        root_rows = df[df['p'] == -1]

        if root_rows.empty:
            print(f"⚠️ No root node (p=-1) found for neuron {nid}. Skipping.")
            continue

        # 1. Center the neuron to (0,0,0)
        soma_x = root_rows.iloc[0]['x']
        soma_y = root_rows.iloc[0]['y']
        soma_z = root_rows.iloc[0]['z']

        df['x'] = df['x'] - soma_x
        df['y'] = df['y'] - soma_y
        df['z'] = df['z'] - soma_z

        # 2. Apply sequential shift (100 um = 100,000 nm along the X-axis)
        current_shift = valid_count * shift_nm
        df['x'] = df['x'] + current_shift

        # 3. Prepare fast line segments for Plotly
        node_map = df.set_index('id')[['x', 'y', 'z']].to_dict('index')
        x_lines, y_lines, z_lines = [], [], []

        # Filter for valid parent-child connections
        valid_segments = df[df['p'] != -1]

        for _, row in valid_segments.iterrows():
            pid = row['p']
            if pid in node_map:
                parent = node_map[pid]
                x_lines.extend([row['x'], parent['x'], None])
                y_lines.extend([row['y'], parent['y'], None])
                z_lines.extend([row['z'], parent['z'], None])

        neuron_color = colors[valid_count]

        # Add Skeleton Trace
        fig.add_trace(go.Scatter3d(
            x=x_lines, y=y_lines, z=z_lines,
            mode='lines',
            line=dict(color=neuron_color, width=2),
            name=f"Neuron {nid}",
            hoverinfo='name'
        ))



        print(f"✅ Neuron {nid} loaded, centered, shifted by {current_shift} nm, and plotted.")
        valid_count += 1

    if valid_count == 0:
        print("❌ No valid neurons were found to plot.")
        return

    # 4. Finalize Layout
    fig.update_layout(
        title=f"Gallery of {valid_count} Centered & Shifted Neurons (Shift: {shift_nm/1000} um)",
        scene=dict(
            xaxis_title='X (nm)',
            yaxis_title='Y (nm)',
            zaxis_title='Z (nm)',
            aspectmode='data', # Crucial to maintain true biological proportions
            bgcolor='white',
            xaxis=dict(showbackground=False),
            yaxis=dict(showbackground=False),
            zaxis=dict(showbackground=False)
        ),
        height=800,
        margin=dict(l=0, r=0, b=0, t=50),
        legend=dict(x=1.05, y=0.5)
    )

    fig.show()

