def plot_histogram_with_median(df, column='total_length_um', title="Distribution of Total Arbor Lengths"):
    """
    Constructs a histogram from a specified DataFrame column and overlays
    a vertical line representing the median value.

    Args:
        df (pd.DataFrame): The DataFrame containing the data.
        column (str): The name of the column to plot.
        title (str): The title of the plot.
    """
    if df is None or df.empty:
        print("❌ DataFrame is empty. Cannot plot.")
        return

    if column not in df.columns:
        print(f"❌ Column '{column}' not found in the DataFrame.")
        return

    # Drop any NaNs (e.g., from files that failed to load in the previous step)
    data = df[column].dropna()

    if data.empty:
        print("❌ No valid numeric data found to plot.")
        return

    # Calculate the median
    median_val = np.min(data)-(np.min(data)*0.1)

    # Initialize the Plotly figure
    fig = go.Figure()

    # Add the Histogram trace
    fig.add_trace(go.Histogram(
        x=data,
        nbinsx=20, # Adjust the number of bins as needed based on your array size
        marker_color='royalblue',
        opacity=0.75,
        name='Distribution'
    ))

    # Overlay the vertical line for the median
    fig.add_vline(
        x=median_val,
        line_width=3,
        line_dash="dash",
        line_color="firebrick",
        annotation_text=f"Median: {median_val:,.2f}",
        annotation_position="top right",
        annotation_font=dict(size=14, color="firebrick")
    )

    # Update the layout for a clean, academic look
    fig.update_layout(
        title=title,
        xaxis_title=f"{column.replace('_', ' ').title()}",
        yaxis_title="Number of Neurons",
        template="plotly_white",
        bargap=0.05, # Adds a small gap between bars
        height=500,
        width=800,
        showlegend=False
    )

    fig.show()

